<h3>Wednesday, May 22, 1996</h3>

<p>I went out today prepared for bugs. I heard that the black flies were out and about in the <b>White
  Mountains</b> last week, so I assumed that they would be approaching swarm proportions today. I
  wore a long-sleeved shirt, long pants, and a hat. I used plenty of bug repellent ("Skin so Soft"
  by Avon; it's the best thing there is against black flies). Yet, as luck would have it, there
  were almost no bugs in the mountains today! I didn't see one black fly. Something must be wrong.
</p>

<p>The weather was nice today. Mostly sunny but not too hot. It was fairly humid, but the
  relatively cool air prevented that from being a major problem. The weather report was calling
  for rain in the afternoon, but I didn't see it. Of course, what the weather report says and what
  actually happens in the White Mountains are often two different things.</p>

<p>The trail to Bridal Veil Falls is the <b>Coppermine Trail</b>. The trailhead is located along
  route 116 in <b>Franconia</b> about a mile north of its border with <b>Easton</b>. Turn down a
  dirt road at the small sign saying "Coppermine Village."</p>

<p>You can't drive very far down the dirt road. Almost immediately you will come to a sign that
  says "No parking beyond this point." Park before the sign. Continue walking down the dirt road,
  taking the right fork when you come to a much newer looking dirt road on the left. Soon you come
  to a hiker sign and the trailhead proper. There are lots of "No parking" signs around the
  area.</p>

<p>The trail is roughly marked with yellow blazes, but it is easy enough to follow. It ascends
  gradually but steadily. After a distance <b>Coppermine Brook</b> is visible to the right. The
  trail follows the brook, more or less, from that point on. After about two miles, the trail
  crosses the brook on a bridge and then very shortly after that comes to a shelter and the falls.
</p>

<p>Bridal Veil Falls is quite lovely. It consists of two parts. The lower part is a large, flat
  rock over which the water flows in a broad sheet and then into a pool. The upper part is an
  almost vertical drop of about 50 feet into an upper pool. The upper pool is not visible at all
  from the bottom. To see the upper part of the falls well, you have to scramble up to the edge of
  the upper pool. That was tricky today because most of the rocks were covered with wet, slimy
  moss.</p>

<p>The upper falls were outstanding! They poured over a large, imposing cliff into the upper pool
  in two steps. The water first drops maybe 25 feet and then slams into a large rock. There it
  changes direction and jumps the remaining distance into the upper pool, sending out a great
  spray in the process. I could easily feel the droplets of water blowing against my face from the
  other side of the pool. It was wonderful.</p>

<p>Highly recommended.</p>

