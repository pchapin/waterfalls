<h3>Saturday, May 27, 1995</h3>

<p>Along Rt. 125 just after crossing into Middlebury from Ripton, the Middlebury River passes
  through a deep, narrow gorge. There were no signs, but my friend, John, and I found a turn-off
  on Rt. 125 at about the right spot and parked there.</p>

<p>The turn-off was not very senic. In fact, quite a lot of trash had been thrown over the side of
  the road at that point and had collected into big piles a few feet below the road. Furthermore,
  there were lots of dead and rotting animal carcasses strewn about among the trash. Apparently
  this was a favorite spot for depositing road kills.</p>

<p>There were a couple of other cars parked in the turnoff, and there we met someone who had just
  been fishing in the gorge. He pointed out a faint trail that leads steeply downhill through a
  pine forest to the water.</p>

