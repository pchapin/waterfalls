<h2>Directions</h2>
<p>There is another <a href="screwauger2.html">Screw Auger Falls</a> in Maine. It is located in
  Central Maine in Gulf Hagas.</p>

<p>
  <b>From the South</b>
</p>
<p>Turn north onto Maine route 26 from US route 2 in Newry (0.0mi/0.0km). Screw Auger Falls is
  located right along route 26 on the west side of the road inside Grafton Notch State Park. At
  9.7mi/15.5km you come to a very well-marked turnoff for the falls. There is a sizable parking
  area there, a picnic spot, and other facilities.</p>

<hr/>
<h2>Description</h2>
<p>Screw Auger Falls is a popular waterfall. If the weather is good and the season is right,
  expect to find plenty of other people there. These falls might make a good family outing. After
  picnic in the woods beside the stream, everyone could make the short walk alongside the stream
  to the falls themselves. On a hot day, there are plenty of places where you could splash your
  feet in the water.</p>

<p>Above the falls, the rocks are smooth and very sculpted. In this regard, these falls resemble
  the nearby <a href="step.html">Step Falls</a>. There are many smooth, open rocks on which you
  can sit and enjoy the water. Downstream a bit from the parking area, the Bear River pours
  dramatically into a deep, narrow ravine. The sides of the ravine are sheer. The state of Maine
  has erected fencing to protect the unwary from falling. I never like fences of this sort around
  a waterfall, but considering the popularity of the spot, it's easy access, and the sudden fall
  into the ravine, I think it is understandable in this case.</p>

<p>The falls are not exceptionally high, but they do drop cleanly a good 15 feet and maybe more.
  The river surges through the narrow ravine a few dozen feet before opening out again. The falls
  and the ravine into which they flow are not really very extensive.</p>

<p>It isn't really possible to get down below the fallsâ€”at least it wouldn't be easy to do so. The
  fences prevent the obvious approaches, so one has to enjoy the falls by looking down at them
  from above. That is not the best vantage point to view a waterfall.</p>

<p>The information presented on signs at the state park indicates that at the end of the ice age,
  the Bear River was once far bigger than it is today. It was the run-off of the glacier that once
  existed in Grafton Notch, and it was filled with abrasive, ground rock. It was at that time when
  most of the sculpting of the rocks around the river was done. No doubt it was also then when the
  ravine into which Screw Auger Falls flows was created.</p>

<p>This is a nice waterfall that is very much worth seeing. However, it is not the most impressive
  waterfall in New England by any means.</p>

