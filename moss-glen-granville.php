<h3>Saturday, March 25, 1995</h3>

<p>There are actually two Moss Glen Falls in Vermont. The other one is in <a
    href="moss-glen-stowe.html">Stowe</a>. These falls are in <span class="other-name">Granville
    Gulf</span> right along <span class="road-name">Rt 100</span>. You can see it wiz by as you
  drive. There is a slightly wide spot in the road just a little way North of the falls for
  parking. Since it's right in the middle of Granville Gulf, there really isn't a lot of space to
  carve out a parking lot.</p>

<p>The falls are pretty neat. A small stream flows down the side of Granville Gulf and spills over
  a fairly high rock outcropping. It's not a clean drop, so there is lots of splashing and
  spraying. Alas, the location is rather beat-up. I was disgusted to see that a large tree had
  been cut down to make the falls more visible from the road (or so I assume). There was also a
  fair amount of litter around.</p>

<p>To the North of the main falls and right near the parking area there is a smaller stream that
  spills over the same rock formation. It's not as steep there, but it was still very interesting.
  The view was more wooded and less intimidating. I actually liked the smaller falls better.</p>

<hr/>
<h3>Saturday, November 18, 1995</h3>

<p>I visited these falls again with my friend John and his girlfriend. We were on our way back to
  my house from a genealogy trip, and John wanted to try his hand at photographing a waterfall.</p>

<p><img src="photos/jbedard.jpg" alt="John Photographing Moss Glen Falls" style="float:
    right; padding: 10px;"/>We arrived at the falls relatively late in the afternoon. The skies
  were overcast with snow flurries sprinkling down on us. John had to use his tripod to get the
  photographs he wanted because it was so dark. I just held my camera by hand.</p>

<p>Since my last visit, someone had put up an elaborate boardwalk. The boardwalk was carefully
  fenced in to discourage, I suppose, people from wandering all over the falls and killing
  themselves. Personally, I didn't like it at all. For one thing, it was hard to get a good
  picture without also getting that boardwalk into the field of view. For another thing, I don't
  entirely approve of attempts to "tame" nature. I felt that the beauty of the spot was ruined by
  the addition of all that safety.</p>

<p>The falls themselves were their usual self. They were surrounded by snow and ice and looked
  very cold.</p>

