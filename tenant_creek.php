<h2>Directions</h2>
<p>
  <b>From Northville (Great Sacandaga Lake)</b>
</p>

<p>To reach the trailhead for Tenant Creek Falls, take NY 30 and, traveling North from the area of
  the Great Sacandaga Lake, turn right 0.5 mile (0.8 km) past the bridge over East Stony Creek
  onto Old Northville Road. Continue 1.5 miles (2.5 km) on this road and turn left at East Stony
  Creek Road. Here a customary brown and yellow Department of Environmental Conservation (DEC)
  sign gives mileages to some destinations. Drive down East Stony Creek Road for 7.4 miles (12.3
  km) until a parking area is seen on the left with room for about 5 or 6 cars. The road is
  winding and made of dirt at the end; however, it is easily passable by normal cars. There are a
  couple of forks in the road; for the most part, stay to the left (there are exceptions) and try
  to look for road signs. You will know when you are at the trailhead as the parking area is upon
  the bank of East Stony Creek. Don't give up if you take a wrong turn. Turn around and take a
  right when you get back to the road you want. Take care to grant privacy to the owners of the
  old Brownell Lumber Camp, the land to the right, by staying on the trails.</p>

