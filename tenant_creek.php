<h2>Description and Notes</h2>
<p>
  <b>July 28, 1998</b>
</p>

<p>From the parking area walk north on the trail following blue DEC markers. After about 5 or 10
  mins you will reach a sturdy wooden bridge that crosses Tenant Creek. Before the bridge a hard
  to miss sign says "falls" and points right. This is the trail leading to Tenant Creek Falls.
  Before taking the turn look up and down the Creek from the bridge. You can see water cascading
  over boulders for some distance. It's very nice. About a half of a mile from the turn, over an
  easy, flat, and well-defined unmarked trail, you will come to the first waterfall of Tenant
  Creek on your left.</p>

<p>This waterfall alone is a worthwhile destination. Crystal clear water spreads out in an
  upside-down V shape over an inclined rock slope and a couple of ledges. The water splits at the
  top of the falls to form two practically identical parallel cascades. At the bottom the
  cascading water collects in a large, round pool that is waist deep. I walked through the pool
  right up to the falls and even carefully climbed a short distance up the side of the falls,
  choosing a place that wasn't too steep or slippery. ADK's <a
      href="references.html"> <i>Guide to Adirondack Trails: Southern Region</i> </a> gives a
  perfect description of the rocks and cliffs around the falls which create a "small natural
  ampitheater." Exploration of some kind on the rocks and a long rest are practically mandatory
  here. The falls are, by my estimation, about 50 feet high.</p>

<p>After about another mile of walking along a fairly easy trail, passing a marshy area and
  climbing up a small ridge, the second waterfall of Tenant Creek is reached. This one was
  definitely the least beautiful of the three. It's just a small cascade, maybe 30 feet high, over
  slightly inclined broken-up rock.</p>

<p>About another 250 yards upstream from these falls are, in my opinion, the best of the three
  waterfalls that grace Tenant Creek. The falls are at the back of a large, fairly deep pool
  surronded by small cliffs. They are about 30 feet high. The water cascades over many ledges,
  this time with no incline, to the pool. On the left side of the falls, the water falls with less
  interruption. Explore the area for different angles from which to view the falls. I got what I
  considered the best picture from the rocks below the pool at the base of the falls.</p>

<p>All three falls give the feeling of great remoteness; it's quite clear that not many people
  visit these falls. Even a story on the falls last summer in the Times Union, the newspaper for
  Albany, NY, hasn't taken this feature away. I visited just a few days after that story ran and
  only met one other group along the trail. Don't even be dismayed if the small lot at the
  trailhead is full—they are probably headed for Wilcox Lake, accessible by the same
  trailhead.</p>

<p>Overall, the falls are very pretty and worth the short, easy, pleasant walk to reach
  them—especially the first falls. They aren't spectacular falls, but rather places from which to
  attain solitude in the midst of beauty.</p>

