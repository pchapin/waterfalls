<h2>Directions</h2>
<p>
  <b>From Pawlet</b>
</p>
<p>Starting at the center of <span class="town-name">Pawlet</span> at the junction of routes 30
  and 133, drive north on route 30 for about 2 miles, past the intersection with <span
      class="road-name">Waite Hill Road</span> on the right, to the intersection with <span
      class="road-name">River Road</span> on the left.</p>

<p>Follow River Road for a bit more than a mile until you reach the junction with <span
    class="road-name">route 153</span>. Turn left at the junction and continue a few feet to the
  brige over the<span class="river-name">Mettawee River</span>. There is a parking area large
  enough for a couple of cars just before the bridge. The falls are below and under the bridge.
</p>

<hr/>

<h2>Description</h2>

<p>My original information on these falls was sent to me by Dean Goss in the mid 1990s. That
  information described them as a "25-foot drop on the Mettawee River." The location I describe
  above is the exact location of the falls according to ... However, what I discovered at that
  location was extremely minimal. The Mettawee River flows over nothing more than a rocky outcrop
  into a large, open pool. The total drop is definitely less than ten feet and might be closer to
  five feet. These falls are little more than a short section of rapids.</p>

<p>It is possible that my previous information on these falls was actually describing the nearby
  <a href="buttonfalls.html">Button Falls</a>. It is also possible that the location information I
  followed is inaccurate.</p>

