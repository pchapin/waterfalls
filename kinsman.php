<h3>Wednesday, May 22, 1996</h3>

<p>Kinsman Falls is on <a href="casbrk.html">Cascade Brook</a> just above a whole series of
  impressive cascades. The easiest way to get to the falls is to start at <a
      href="basin.html">The Basin</a>.</p>

<p>Follow the <b>Basin-Cascade Trail</b> from the west side of the trails surrounding The Basin.
  Take plenty of time to enjoy the cascades along Cascade Brook, but return to the trail as the
  cascades appear to run out. About 0.5 miles from the start of the trail, there is a sign for
  Kinsman Falls. You can't see it from the trail, but you can take a side trail down to the pool
  just below the falls.</p>

<p>The falls are not very high. However, they are perfectly vertical. The falls appear to just
  drop out of a narrow ravine in the rock and directly into a rather large pool. There was quite a
  bit of water going over them today; so much so that the base of the falls was a churning pile of
  foam. Despite the small size, the sound was an impressive rumbling.</p>

<p>The main trail continues on, and shortly another side trail leads you to the rocks at the top of
  the falls. I didn't find that view nearly as interesting, however.</p>

