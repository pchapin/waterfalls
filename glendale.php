<h2>Notes</h2>
<dl>
  <dt>
    <b>Sunday, July 7, 1996</b> <br/>
  </dt>
  <dd>
    <p>Today my daughter Hillary and I visited these falls again. I was surprised at how little
      water was in the stream. This has been a very rainy summer, and most of the streams in
      Vermont are filled to the brim. Yet at Glendale Falls, it was possible to scramble over the
      rocks and jump over the stream with ease (in many places).</p>

    <p>There were a lot of people here today as well. I don't remember ever seeing it so crowded.
      Many people were swimming in the pool at the bottom of the falls. It wasn't a particularly
      hot day, but the water didn't seem overly cold either.</p>
