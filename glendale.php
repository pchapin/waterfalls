<h2>Directions</h2>
<p>The description below gives the approach from <b>Huntington</b>. This is the approach I know
  best. It is the approach my parents always used.</p>
<ol>
  <li>
    <p>Take the Westfield exit off I-90 and leave Westfield on Rt. 20 going west. Drive to
      Huntington and there turn right across the river on state route 112.</p>
  </li>
  <li>
    <p><em>Immediately</em> after crossing the bridge, take a sharp left turn onto Basket Street.
      The road doubles back alongside the bridge toward the river.</p>
  </li>
  <li>
    <p>This new road follows the river for some distance. After a minute or so, there is a fork.
      Basket Street continues uphill to the right, but you should take the left branch here. At a
      point 1.5 miles outside of town, there is another fork to the right that goes uphill. At
      that junction there is a sign that says, "Welcome to the Skyline Trail." Take the uphill
      road. Follow that road for 2.5 miles. When it feels like it's getting near the top of the
      hill, there will be a right-hand turn in a relatively open area of the road. Take that
      turn.</p>
  </li>
  <li>
    <p>The road shortly begins to descend the other side of the hill. It's very a long and
      dramatically straight descent. At the bottom of the hill, 2.1 miles from the turn at the
      top, the road turns toward the left and begins to parallel the Middle Branch of the
      Westfield River.</p>
  </li>
  <li>
    <p>Follow the river for 5.0 miles. Look for the crossing into Middlefield. There is a sign,
      but it's pretty inconspicuous. As I recall, the road surface changes character at that
      point, but they were doing some repaving along there, so I'm not sure what it's like
      now.</p>
  </li>
  <li>
    <p>In a relatively open area, there will be a road coming down from the left. This occurs <em>just</em>
      before the main road crosses a stream (Glendale Brook!). There are very few side roads, so
      you probably won't miss it. Take this left.</p>
  </li>
  <li>
    <p>The road climbs steeply and in less than a mile comes to the parking area for Glendale
      Falls. You won't miss it. There is a very obvious sign for the falls (the first you'll
      see).</p>
  </li>
</ol>
