<h2>Description</h2>

<p>This is one of the most fascinating waterfalls in New England. It's located on a back road in
  <b>Middlefield</b> rather far from anything. The falls themselves are owned by the Trustees of
  Reservations, a Massachusetts-based conservation group. I've known about these falls for a long
  time. My parents brought me here when I was a child, and I have returned often.</p>

<p>Glendale Falls is large. There is no single drop that's more than 10 feet, but the entire falls
  complex drops at least 150 feet overallâ€”maybe more. The stream that flows over the falls splits
  into many streamlets that each find their own way down a steep, rocky slope. In some places
  water rages through narrow channels. In other places it glides across wide, flat rock faces. In
  still other places it churns and tumbles down complex rock steps. Some of the streamlets flow
  over the open rock section of the falls. Other streamlets flow through the adjacent forest. Some
  streamlets carry significant amounts of water while others are just trickles.</p>

<p>The entire show flows eventually into a large, deep pool at the bottom of the falls. From there
  the stream continues on its way to the <b>Middle Branch of the <a
      href="http://bondo.wsc.mass.edu/html/dept/garp/wrwa/wrwa.htm">Westfield River</a></b>.</p>

<p>If you are the sort of person who likes to watch water snake and wind, Glendale Falls is for
  you. During the dry season, you can walk all over the rocks and get up close and personal to
  each little streamlet as it finds a path down to the pool. When there is more water in the
  stream, it's more difficult to get around on the rocks, but there are more falls to look at in
  compensation.</p>

<p>This is an excellent waterfall for all seasons. It is a "must-see" if you are touring
  waterfalls in New England.</p>

