Overview: Nice waterfall in Central Maine.
Location: Near Gulf Hagas in the Ki Jo-Mary multiple use forest.
Stream  : Gulf Hagas Brook
Height  : Three drops. Middle one is the largest at about 15 feet.
Finding : A bit tricky. Use good maps.
Access  : Requires moderate hike and a stream fording. Difficult in high water.
Swimming: Some possibilities.
Basking : Fair.
Privacy : Remote location, but a popular spot. Not crowded, but you won't be alone.
Legal   : Public access. Fee required: $7.00 for me in 1997.
