<h2>Description</h2>
<p>Gulf Hagas, and the area immediately around it, is a waterfall enthusiast's paradise. Inside
  the gulf itself, there are no less than five named waterfalls. That is in addition to Screw
  Auger Falls just before the gulf. The entire hike along the rim of the gulf and back again is
  quite significant and should not be attempted without a full day to spend and proper
  preparations. This description only covers the more easily accessible Screw Auger Falls.</p>

<p>Gulf Hagas Brook tumbles down a steep slope on its way to the Pleasant River. On its way it
  forms several interesting cascades. It is not always easy to approach the brook from the Rim
  Trail. The terrain is rugged. However, there are three interesting drops that can be seen.</p>

<p>The first drop is only a short distance from the stream crossing I mentioned in the directions.
  The brook divides into two flows and both arch out gracefully over a rock face to splash into a
  pool at the bottom. The pool is nestled in a crevase in such a way as to give the spot a hidden,
  secluded feeling. Approaching these falls is tricky, and the scramble down to the water's edge
  should only be attempted with great care. Quite a few years ago, however, I once did go swimming
  in the pool below these unnamed falls.</p>

<p><img src="photos/screw22.jpg" style="float: left; padding: 10px" alt="Screw Auger Falls"
/> The main part of Screw Auger Falls is a bit farther down the trail. There a short side trail
  leads out onto a high, narrow outcropping of tree-covered rock. From there it is a fairly easy
  scramble down into a large, open bowl into which the falls flow. At the north end of the bowl,
  the falls drop impressively into a big pool. The stream then flows across the bottom of the
  bowl, through a narrow channel, and then on toward the Pleasant River.</p>

<p>It is easy to walk around among the broken rocks that are strewn about below the falls. The
  spot is fairly open to the sky and bright. In fact, it almost looks a little messy.</p>

<p>Farther down the trail there is another short side trail that approaches the brook. There the
  water flows over some low cascades that are covered over by the thick forest. This last drop is
  the lowest and least interesting of the drop. However, it is also the friendliest and in some
  ways the best for just sitting by the water. In addition, the swimming would probably be the
  best at these falls. One pool even had a bit of a gravel beach.</p>

