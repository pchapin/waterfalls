<h2>Directions</h2>
<p>There is another <a href="screwauger1.html">Screw Auger Falls</a> in Maine. It is in Grafton
  Notch State Park.</p>

<p>
  <b>From Milo, Maine</b>
</p>

<p>Starting at the center of Milo at the junctions of routes 6, 16, and 11 (0.0mi/0.0km), drive
  north on route 11 toward Brownville. At 12.9mi/20.6 km, turn west onto the road to Katahdin Iron
  Works. This turn is marked, but the road is a minor road, so watch for it. The road to the iron
  works is dirt, but it is of good quality. At 19.6mi/31.1km you will come to the old iron works.
  There you will have to stop and register before you can enter the Ki Jo-Mary multiple use
  forest. The attendant can give you up-to-date information on road and trail conditions. Maps of
  the area are also available there. When you return, you will have to check out.</p>

<p><img src="photos/screw21.jpg" style="float: right; padding: 10px" alt="Screw Auger Falls"
/>After registering, continuing driving along the road to the parking area at 26.3mi/42.0km. This
  parking area is on the north side of the road, and it is marked. The blue blazed trail leaves
  from the back of the parking area.</p>

<p>In a short distance, the trail connects to the Appalachian Trail. Almost immediately after that
  point, it crosses the Pleasant River. There is no bridge; wading is necessary. In low water,
  wading across the Pleasant River is easy enough (watch your step, the rocks are slippery). In
  high water it is impossible. There is another approach to the falls that involves more driving,
  more walking, and a smaller stream crossing. It may be possible to use that approach during
  high-water conditions. Ask the attendant at the Katahdin Iron Works.</p>

<p>Once on the other side of the river, the trail (now the Appalachian Trail) climbs gradually
  alongside the river, following it upstream. The walking is not too difficult, although the
  ground is rough. After about 1.5 miles, the Appalachian Trail turns north (right), and the trail
  you are interested in goes straight ahead. Soon it crosses Gulf Hagas Brook.</p>

<p>Turn left (downstream) and follow the brook toward Gulf Hagas. You will be following the Rim
  Trail. Screw Auger Falls is about halfway from the brook crossing to the Gulf. The distance from
  the brook crossing is not far. Before you reach the main part of Screw Auger Falls, you will go
  by an interesting lesser waterfall. Below the main part of the Screw Auger Falls, there is also
  another small drop that is worth investigating.</p>

