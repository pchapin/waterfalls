<h3>Sunday, September 29, 1996</h3>
<p>These falls were first suggested to me by Diane Mead, a reader of this website. She told me
  that "the hike is long and rather steep... but the view of the falls is worth it." She also
  directed me to the Manchester <a href="http://www.vtweb.com/highland/">Highlands Inn</a> page.
  They have a nice description there of the <a
      href="http://www.vtweb.com/highland/hikes.html">Lye Brook Falls hike</a>.</p>

<p>Today was a cool (but not cold) fall day with clear air and a nice touch of color on the trees.
  Although there was a lot of blue sky around, the sky was thickly overcast all during my hike to
  and from the falls. Such is the nature of New England weather, I suppose.</p>

<p>Locating the trailhead was easy enough with the help of the fine directions provided by the
  Highlands Inn. Although the trail starts at the end of an obscure side road, it is well-marked
  and obvious when you arrive. The trail itself is well tended and well traveled. Although it goes
  into the <b>Lye Brook Wilderness</b>, it was clear that the falls are a popular destination.
  When I arrived today, the parking area was almost full.</p>

<p>The trail climbs gradually at first through a mostly Maple-Beech forest. After crossing through
  a windfall, it begins climbing more steeply. The area was beautiful. The forest was rich and
  green. The trail snaked dramatically along the side of the steep ravine, and I could hear <b>Lye
    Brook</b> roaring in the valley below.</p>

<p>After about two miles of walking, a well-marked side trail diverged to the right toward the
  falls. This side trail held to a contour as it followed a very old railroad grade. I could just
  make out bits and pieces of old railroad ties rotting on the ground along the trail. The feeling
  that I was clinging to the side of a steep embankment was even more noticeable along this
  section due to the narrowness of the trail.</p>

<p>After about 1/3 of a mile, I started hearing the falls ahead. Then, in only a few yards, the
  trail passed out from behind a rock face and into full view of the falls.</p>

<p>They were wonderful! This was truly one of the highest waterfalls I've yet seen in Vermont.
  From a rock lip high above, the stream fell down over two major rock faces. The rocks were
  horizontally aligned layers, causing the stream to take what looked like hundreds of small steps
  as it splashed from layer to layer.</p>

<p>The main distraction was that the trees on the other side of the lower portion of the stream
  had been blown over in recent times. As a result, the lower part of the falls looked a bit torn
  up and unsightly. It would have been a much more scenic spot if those trees had still been
  standing. However, I'm not offended by the work of natural forces, and in a few years the matter
  will probably have "corrected" itself.</p>

<p>I scrambled around on the rocks a bit trying to get a good picture, but it was hard to get a
  shot of the falls that would both show the entire waterfall and also give a sense of scale.</p>

<p>Highly recommended if you don't mind hiking.</p>

