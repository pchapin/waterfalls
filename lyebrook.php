Location: Manchester, VT
Height  : 100 feet (estimate)
Finding : Trailhead on a side road just outside of Manchester.
Access  : Moderate. Requires 2.3 mile hike (one way).
Swimming: No.
Legal   : Public lands. Inside Lye Brook Wilderness Area.
