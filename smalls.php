<h2>Thursday, June 13, 1996</h2>

<p>This waterfall is located right along route 4 about halfway between <b>Rangeley</b> and <b>Phillips</b>.
  It is well-marked by the state of Maine and comes with a large parking lot, a picnic area, and
  drinking water.</p>

<p>An obvious trail goes down from the parking lot (by way of several rock steps) to the far end
  of the pool into which the main falls empty. From the other side of the bridge across the
  stream, there is a good spot for a <a href="photos/smalls1.jpg">picture</a> [142 KBytes]. The
  trail then informally climbs the far bank for a better look at the falls. Here is a <a
      href="photos/smalls2.jpg">picture</a> [144 KBytes] of a section that can't be seen from
  below. There are fences in places to prevent foolish people from falling in, but thankfully, the
  fences don't follow the entire length of the stream.</p>

<p>Smalls Falls is very nice. In addition to the main drop, there are a couple of the lesser, but
  still very interesting, drops above. The water seems to almost hop from pool to pool on its way
  down to the bottom. In this respect, I was reminded of <a href="lana.html">The Falls of Lana</a>
  in central Vermont.</p>

<p>Above the main falls, there were still several other <a href="photos/smalls3.jpg"
>interesting cascades</a> [112 KBytes]. The walls above the falls changed into gentler
  embankments, so it was easy to climb out on the rocks and sit right next to the stream. In some
  ways I liked the upper sections better, although they were not as "senically impressive." I
  spent quite a bit of time lounging around on the rocks.</p>

<p>Nice falls. Worth the trip. Bring a picnic lunch and don't expect to be alone.</p>

