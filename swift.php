<hr/>
<h2>Wednesday, June 12, 1996</h2>
<p>These falls are right along route 17 a few miles north of <b>Rumford/Mexico</b>. It is not
  marked, but it is clearly a popular swimming hole, so there is a large, obvious parking area
  beside the road. In fact, while I was there several people came to swim in the river below the
  falls.</p>

<p>The falls themselves were not particularly high. There were two drops of about five feet each.
  However, there was a lot of interesting sculpted rocks in the area. Also, the <b>Swift River</b> is
  a relatively large river, so the amount of water moving over the falls was more than I normally
  see.</p>

<p>Interesting, but not spectacular. Stop if you are in the area, but don't make a special
  trip.</p>

