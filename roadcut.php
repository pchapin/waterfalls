<h1>Road Cut Falls</h1>
<p>Many times road cuts interrupt the path of a small stream which then turns into a waterfall as
  it flows down the side of the road cut. These falls are artificial in the sense that they were
  created by humans. In this respect, they are similar to <a
      href="urban.html">urban falls</a>.</p>

<p>There are many road-cut falls, mostly unnamed. Typically, they are seasonal, appearing only in
  the Spring or after a heavy rainstorm. In the summer there may only be a small trickle of water
  wetting the rocks beside the road. Nevertheless, some road-cut falls are quite spectacular --
  every bit as much as the more "traditional" waterfalls. Road cuts are often very steep and
  sometimes they are rather high. Some road-cut falls are definitely worth looking at.</p>

<p>On this page I summarize some of the road cut falls I have stopped to investigate.</p>

