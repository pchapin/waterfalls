<h2>Notes</h2>
<dl>
  <dt>
    <span class="notes-entry-header">Saturday, April 26, 1997</span>
  </dt>
  <dd>
    <p>My friend John and I were in the New Marlborough area doing some genealogical research
      today. I told him that no visit to New Marlborough would be complete without stopping by
      Campbell Falls. John readily agreed.</p>

    <p>The falls were in good form when we stopped to see them. Everything was more or less
      exactly like it was last year, except that the weather was sunnier. We only stopped for
      enough time to climb down to the bottom of the falls and snap a few pictures.</p>

    <p>John and I also visited <a href="umpachene.html"><span
        class="waterfall-name">Umpachene Falls</span> </a> in New Marlborough today.</p>
  </dd>

  <dt>
    <span class="notes-entry-header">Thursday, April 4, 1996</span>
  </dt>
  <dd>
    <p>I visited these falls on my way home from a hikeq to <a href="race.html"><span
        class="waterfall-name">Race Brook Falls</span> </a>. It was getting rainy and dark, but
      I'm glad I stopped. Campbell Falls was an impressive sight!</p>

    <p>I first went down the west side of the stream. I was able to get a very interesting picture
      of the main falls, but I was not able to climb all the way to the bottom that way. However,
      by taking the trail on the east side of the stream, just past the fences, I was able to go
      all the way down to the water's edge just below the falls. From there I could get a nice
      view of the falls, including both the large, impressive upper section and the smaller lower
      section. By scrambling over some rocks, I was able to sit at the mouth of the ravine and
      watch just the upper falls in their full glory.</p>

