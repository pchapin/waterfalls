<h2>Directions</h2>
<p>These directions are current as of April 1996</p>

<p>The Falls are located in a state forest right on the border between Massachusetts and
  Connecticut. Locating the falls is a bit tricky. From the center of <span
      class="town-name">Southfield</span>, take the <span class="road-name">Norfolk Road</span>
  south. Only a few feet before entering Connecticut, there is an old, rusted sign on the right
  that points down a dirt road called <span class="road-name">Campbell Falls
    Road</span>. Follow that road for a short distance until it crosses the <span
      class="stream-name">Whitney River</span>. There really aren't any signs or parking areas.
  However, there are a couple of wide places in the road where you can turn off.</p>

<hr/>
<h2>Description</h2>

<p>The Whitney River flows under the road and almost at once over the falls. The state of
  Massachusetts has put up some fences by the road to discourage foolish people from diving into
  the falls. However, the fences don't actually go all the way down the ravine. The falls still
  feel wild.</p>

<p>Campbell Falls actually has two sections; an upper part that is higher and then a lower part.
  The stream flows through a short, rocky trench between the two falls. The best view is, as is
  often the case, from below. There is a rough, unmarked trail leaving the road on the left side
  of the stream that takes you down to the pool at the base of the falls. It's a short walk but a
  bit of a scramble in places (no problem for an experienced hiker).</p>
