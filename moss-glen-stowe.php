<hr/>
<h3>Saturday, April 1, 1995</h3>

<p>There are actually two <b>Moss Glen Falls</b> in Vermont. The other one is in <a
    href="moss-glen-granville.html">Granville</a>. These falls are just a little way off Rt 100,
  North of the center of <b>Stowe</b>. You have to fork right off the main road, take a right turn
  onto a dirt road a few hundred yards from Rt 100, and then drive about a half-mile or so to
  where the road makes an abrupt bend and crosses a stream. You can park your car there and follow
  an obvious trail to the falls. It isn't a long walk, and the trail is easy going (though it was
  a bit muddy for me).</p>

<p>These falls are one of the highest in Vermont. The trail climbs up to the top of a rocky knoll
  from which there is an excellent view of it framed between the branches of hemlock trees. There
  are two sections. The upper part is a narrow fall into a large pool. The pool then drains over a
  steep rock face some fifty feet or more to the river below. From the knoll you can't see into
  the pool, but both falls are easily visible.</p>

<p>The trail continues up the side of the falls to a place where you can look down into the stream
  as it pours over the rock face. It's also possible to scramble down to the bottom of the main
  falls, although that route is rough and requires considerable caution.</p>

<hr/>
<h3>Sunday, December 3, 1995</h3>

<p>Today I visited these falls again to try and get a winter picture of them. I was with my friend
  Laura. There were a few inches of snow on the ground, but not enough to keep us from walking to
  the falls with ordinary boots (snowshoes or skies were not required). The trail to the falls was
  packed down, so evidently others visit these falls in the winter too.</p>

<p>We've had some fairly cold weather lately, so the falls were quite choked with ice. The main
  falls resembled a rock cliff covered with ice and didn't really look all that much like a
  waterfall. You could see the water peeking out here and there from behind the ice coating.</p>

<p>The stream above the main falls was somewhat more interesting. Although filled with a lot of
  ice, there were still many nice views of it wandering and splashing its way through the narrow
  gorge just above the falls. We looked over the edge with extra caution on account of the
  snow-covered ground.</p>

<hr/>
<h3>Thursday, August 15, 1996</h3>
<p>
  <img src="photos/mgstowe1.jpg" style="float: right; padding: 10px" alt="Moss Glen Falls"/>
</p>

<p>Today my daughter, Hillary, and I visited these falls. I wanted to get a good picture of them
  to replace the corrupted black and white photo I've had on this page for the last year and a
  half.</p>

<p>We arrived in the late afternoon as the sun was starting to move well into the west. It angled
  into the glen in a beautiful and peaceful way. The falls themselves were shaded, and the whole
  atmosphere seemed timelessâ€”almost primeval.</p>

<p>The water was very low, so Hillary and I waded up the stream into the lower gorge just below
  the falls. From there we got a very dramatic view of the stream pouring over the lower wall in
  many narrow ribbons. We couldn't see the entire falls from there because the upper portions
  slanted back and out of our line of sight, but the view was impressive anyway. The steep rocky
  walls on either side of the gorge gave me the impression that we were in a huge hall, open to
  the sky, with the waterfall like some great throne.</p>

<p>Hillary and I splashed around in the stream below the falls and took several pictures. We then
  climbed up the trail to the top of the gorge and took a couple more pictures of the falls from
  that vantage point.</p>

<p>A very nice waterfall. One of Vermont's finest.</p>

