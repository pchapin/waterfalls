<h1>List of Waterfalls</h1>
<hr/>

<p>What follows is a list of waterfalls in the New England region for which I have some specific
  information. I have personally visited most of the falls listed below and have included my
  comments and pictures in each document (either as links or as in-line images, depending on their
  size). However, I have not visited some of the falls listed below. Nevertheless, I may have
  comments or pictures (or links to such things) from other people, or I may have some other
  information about them.</p>

<p>For each waterfall, I include a header in its document that summarizes the basic facts about
  that waterfall. I hope you find these summaries useful and accurate. I have prepared a <a
      href="summary.html">document that describes what the various summary headers mean</a>. In
  the future I may offer a way to search these documents based on the summary headers.</p>

