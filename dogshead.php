<h3>Sunday, May 28, 1995</h3>

<p>These falls are in the <b>Lamoille River</b> as it flows through <b>Johnson</b>. Actually, I
  had to cross the river and drive along a back road for a little ways before I came to the falls.
  There was no sign, but there is a turnoff and I was able to just see the falls from the road as
  I approached.</p>

<p>The spot was rather unusual. Most of the land around was relatively flat. However, suddenly
  there was a ridge of rock that cut across the river and ran for what looked like quite a ways in
  either direction. At the point where this ridge met the river, there was an unusual rock
  formation that resembles a dog's head overhanging the flow. The resemblance was easy to see
  because someone had painted an eye on it at the appropriate spot. In fact, the whole location
  looked like a favorite party spot for the residents of Johnson. I noticed a couple of fire pits
  about, and there was some graffiti painted on the rocks.</p>

<p>The falls were interesting because of the volume of water that goes over them. It wasn't to be
  compared with <a href="niagara.html">Niagara</a>, of course, but the entire Lamoille River is a
  lot more water than I usually see going over a waterfall. The falls were not very high but there
  was a lot of churning and frothing as the river narrowed to a channel only a few feet wide.</p>

<hr/>
<h3>Saturday, January 24, 1998</h3>

<p>A reader of this page sent me some additional information about the area around Dog's Head
  Falls. Apparently about 200 yards downstream there is a natural bridge formation that crosses
  the river. Evidently the entire flow of the river goes under this bridge when the water level is
  low. If you're in the area of Dog's Head Falls, be sure to look for this interesting geological
  feature as well.</p>

<hr/>
<h3>Sunday, November 22, 2015</h3>

<p>This is the first time I visited a waterfall for this website in many years!</p>

<p>The latest DeLorme Atlas and Gazetteer for Vermont no longer marks the location of Dog's Head
  Falls. To find it, start in the center of Johnson and take <b>Railroad Street</b> south out of
  town. Immediately after crossing the Lamoille River, turn left onto <b>River Road</b>. The road
  stays near the river for a while and then climbs a hill. It continues along and above the river
  and then gradually returns to the river's level. Shortly after the road crosses the <b><a
      href="http://lvrt.org/">Lamoille Valley Rail Trail</a></b> there is a turn-out on the left
  (north) side of the road. Park at the turn-out and then walk across the Rail Trail following
  obvious paths to the falls.</p>

<p>I'm not sure of the legal status of these falls. There are no signs prohibiting access and
  plenty of evidence that many people visit them. I assume access is allowed.</p>

<p>Nobody else was visiting the falls today, and I was there in the late afternoon, giving the
  location a serene, quiet feeling. There was some litter and graffiti but not as much as I recall
  from 20 years ago. Overall, the location seemed to be in good shape. The painted eye on the
  "dog" was gone.</p>

<p>The falls were much as I remembered them: a place where the entire Lamoille River churns
  through a narrow gap in a vein of resistant rock. There is lots of evidence around of a time
  when the river was higher, working on rock that is now far above the current water line.</p>

<p>In addition to the picture at the top of this page, I took the following pictures. The first is
  a view across the river from beside the top of the falls showing how the tranquil Lamoille
  transitions into the raging flow squeezed between the rocks. The second picture is of the "dog's
  head" from which the falls are named. The picture at the top of this page was taken while
  standing on that head!</p>

