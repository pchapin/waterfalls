<h2>Description</h2>
<p>
  <img src="photos/chapel-brook-2s.jpg" style="float: right; padding: 10px"
       alt="Chapel Brook Falls"/>
</p>

<p>Chapel Brook Falls is not a large waterfall, but it is a very pretty one. The main fall is the
  one farthest downstream. When you first come to the stream, you will be looking at a long
  sloping rock over which the stream flows in a narrow sheet. At the bottom of the sloping rock
  there is a large pool. Just below this first step is another, steeper sloping rock with another
  pool. Below that the main falls is a nearly vertical drop of about 15 feet.</p>

<p>There are plenty of rocks around, so it is easy to get right out near the water. In fact, you
  can stand just a few feet in front of the main falls without much trouble, making for a
  spectacular view. There is a pool below the main falls as well, offering some possibilities for
  wading.</p>

