<h2>Notes</h2>
<dl>
  <dt>
    <br/> <b>Sunday, November 10, 2002</b> <br/> <br/>
  </dt>
  <dd>
    <p>Today Hillary and I visited these falls for the second time. The weather was cloudy, and
      there were very few leaves on the trees, but the falls seemed very similar to last year in
      terms of the amount of water flowing over them. The lower falls seemed especially
      beautiful.</p>

    <p>Hillary and I also took some time to follow the trail across the road from the falls up to
      Chapel Ledge. It was a short walk and the view was nice. Although there were no waterfalls
      along the trail, I would still recommend it as an interesting side trip.</p>
  </dd>

