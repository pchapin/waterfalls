<hr/>
<p>
  <i><b>Chapel Brook Reservation</b>: 128 acres of Hemlock-Northern Forest, including Pony
    Mountain (Chapel Ledge) and Chapel Brook. The Brook drops in a series of three waterfalls, 10,
    15 and 25 feet high. Owned by the <a href="http://www.thetrustees.org">Trustees of
      Reservations</a>, the area has value as a wildlife habitat for hiking and picnicking &amp;
    for its scenic beauty.</i>
</p>
<h2>Directions</h2>
<p>
  <b>From Northampton, MA</b>
</p>

<p>Take MA route 9 west out of <b>Northampton</b> to the small town of <b>Williamsburg</b>. This
  is a fairly short distance: only a few miles. In the center of Williamsburg, right by a gas
  station, turn north onto <b>North Street</b>. You will immediately cross a bridge and come to a
  four-way intersection. Go straight.</p>

<p>After a short distance, North Street will turn into <b>Ashfield Road</b>. Stay on Ashfield Road
  for several miles. It will cross into the town of <b>Conway</b>, where it becomes <b>Main Poland
    Road</b> and then, a mile or two later, it will cross into Ashfield, where it becomes <b>Williamsburg
    Road</b>. These town line crossings are marked, but you will have to watch for them. The signs
  are somewhat obscure.</p>

<p>After another mile or so, the road goes down a short but moderately steep hill and crosses <b>Chapel
  Brook</b>. There is some parking just past the small bridge on the left, but you could park
  along the roadside in several places nearby. There is a sign for the Chapel Brook reservation,
  but it isn't easy to see until you are pulling into (or driving by) the parking area.</p>

<p>From the parking area there is a trail that heads upstream. Don't go that way. The falls are on
  the other side of the road and downstream from the bridge. There is an old road (now more like a
  trail) that leaves the main road just before the bridge. Down that road a few yards is a sign
  pointing to an informal trail that leads to the falls. The trail is short. The falls are close
  to the road. The footing is a little tricky in places, but not bad. It shouldn't present a
  problem for anyone except the most unsteady. I do suggest wearing the appropriate footwear. The
  forest floor can be muddy.</p>

