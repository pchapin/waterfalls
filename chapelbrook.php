    <p>Today my daughter Hillary and I visited Chapel Brook Falls for the first time. It was a
      beautiful, sunny fall day, and there were many fallen leaves around and in the stream. The
      sloping rocks above the main fall were covered with leaves. However, many leaves were still
      on the trees, painting the forest around the falls in yellows, reds, and oranges.</p>

    <p>Despite the dry summer, the water level in Chapel Brook seemed normal. It was easy enough
      to walk around on the rocks, but the stream still had plenty of water to make the falls look
      good.</p>

    <p>There were quite a few people around---not surprising considering the weather and the
      weekend. Most people seemed to be hiking upstream, however. I think there might be some sort
      of overlook that way. The falls themselves were relatively quiet.</p>

