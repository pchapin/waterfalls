<h1>Photographing Waterfalls</h1>

<p>Waterfalls make difficult subjects. It is challenging to capture the true spectacle of an
  impressive waterfall on film. The sight and sound of the moving water, the smell of the forest,
  and the feeling of the cool, moist air blowing off the falls are all part of the total
  experience. Yet with a little practice you can get very nice pictures that convey something of
  the nature of the waterfall.</p>

<h2>The Right Equipment</h2>

<p>Photography is a hobby that has no limit when it comes to the amount of money you can spend on
  equipment. When it comes to waterfalls, however, I have the following suggestions.</p>

<p>First, a zoom lens is very helpful. You can't always stand in the best spot when you are at a
  waterfall. Steep rock faces and raging water often conspire to make the only safe locations very
  unphotogenic ones. I've found that a zoom lens allows me to get <a
      href="NH/photos/kinsman.jpg">just a little closer</a> or <a href="VT/photos/bartlett.jpg">just
    a little further back</a> when there would otherwise be no way I could move in those
  directions. I use a 28mm to 105mm zoom lens. Although I find that many of my pictures are taken
  close to the 50mm "standard" lens focal length, I've often needed to slide my lens toward either
  end of its range. The wide-angle settings tend to be more useful than the telephoto ones.</p>

<p>Many waterfalls are located in dark spots. The bottom of heavily forested glens and gorges
  typically get very little sun. As a result, long exposures are often necessary&#x2014;and
  sometimes desirable for certain effects (see below). Hand-holding long exposures is challenging.
  Thus, a tripod is a handy accessory for waterfall work. Be sure to get one that is useful on
  rough, uneven ground. You should be able to adjust the length of the legs independently.</p>

<p>A polarizing filter can also be useful for making the sky more dramatic and for removing glare
  from pools of water.</p>

<h2>The Right Location</h2>

<p>Almost without fail, waterfalls look better <a href="VT/photos/moss-glenn-granville-01.jpg">from
    below</a> than they do from above. If at all possible, try to get below the falls when taking
  pictures of them.</p>

<p>Try to include enough context to get some scale. <a href="VT/photos/bailey-01.jpg">People are
    good</a> for that, although too many people are a distraction. Yet even trees, logs, and other
  natural items can serve to put the falls into context. For example, the log at the bottom of <a
      href="VT/photos/hamilton1.jpg">this picture</a> is enough to give the viewer a feeling for
  the scale of the falls. In contrast, you would never know that these falls are about 80 feet
  high from <a href="MA/guest_photos/bashbish.jpg">this picture</a>!</p>

<p>It is usually best to avoid direct, straight on shots. Take your picture at a slight angle if
  you can. Sometimes it's very difficult to find the best angle from which to photograph a
  waterfall. I've been at falls where I really wanted to be on the other side of the stream but
  was unable to get there because of rushing water. Of course, if you are very serious about
  getting the best picture, you might consider driving or hiking around the long way to get to the
  other side, or you might bring whatever is necessary to wade the stream. Rock climbing gear
  might also be useful in some cases so that you could scale the wall opposite the falls and get
  the best view. I don't go to those extremes.</p>

<p>Whatever you do, avoid getting fences, cars, parking lots, or signs in the picture (unless it
  is your intention to make an "artificial" item your subject and not the waterfall). Such things
  are a major distraction in the final image. Some waterfalls are very much fenced in, and it can
  be difficult to get a shot that displays the falls in its natural surroundings. It saddens me to
  see the ambiance of a magnificent waterfall ruined by fences and walkways.</p>

<h2>Exposure Times and Film Speed</h2>

<p>The slower the film, the higher the resolution. If you want poster-quality pictures, don't use
  high-speed film. On the other hand, as I mentioned above, many waterfalls are in dark locations.
  If you have, for example, hiked five miles to get to the falls and didn't bring your tripod, you
  may need high-speed film to get a good picture.</p>

<p>Long exposures will cause the water to blur in the final image, giving the falls a streaming,
  feathery look. Very fast exposures will freeze the water, resulting in a stop-action appearance.
  Both effects can be very interesting. The streaming look can make a small, unimpressive
  waterfall very interesting. Stop-action tends to look better if there is a lot of spray.</p>

