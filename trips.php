<h1>Waterfall Trips</h1>
<p>Over the months several people have asked me if I was interested in organizing public waterfall
  trips around New England. That idea does interest me. There are legal and safety issues that
  need to be worked out, but I would like to eventually offer some informal waterfall trips on a
  regular basis.</p>

<p>In the summer of 1997 a friend of mine (who I met on the 'net) suggested trying to set up such
  a trip for central Massachusetts. The idea was to look at some falls that are close to the
  Boston area. After some discussion we decided to limit participation to "invitation only" and
  focus mostly on our personal friends. This decision was reached after talking with the <a
      href="http://www.outdoors.org">Appalachian Mountain Club</a> regarding liability issues
  surrounding trips that are open to public participation.</p>

<p>It is my hope that eventually we could arrange some public trips. In any case, there are
  several possible waterfall "tours" that people might be interested in making with or without the
  help of an organized trip. This page will summarize some of these tours and will hopefully help
  those of you who are interested in planning a day of waterfall hopping. Browsing through my
  individual descriptions does not make it easy to figure out which falls can be readily visited
  together.</p>

<hr/>
<h2>Upper Central Massachusetts: Royalston</h2>
<p>The small town of Royalston, MA is blessed with three interesting waterfalls that are very
  worth visiting. See the individual pages for directions and detailed descriptions. They are all
  close enough together so that the order in which you visit them is not important.</p>

<ul>
  <li><a href="MA/doanes.html">Doane's Falls</a>. This is the largest and most spectacular of
    Royalston's waterfalls. It is located by the road so that it is also the most accessible.
    However, to see all the falls requires a short hike down the stream.
  </li>
  <li><a href="MA/royalston.html">Royalston Falls</a>. This waterfall requires a hike of up to 1.5
    miles one way, depending on how far you dare drive down the access road. It has a remote
    feeling to it that is quite nice.
  </li>
  <li><a href="MA/spirit.html">Spirit Falls</a>. This waterfall requires a hike of perhaps one mile
    over easy terrain. It is the smallest of the three falls in terms of water volume and probably
    the most susceptible to drying up in certain seasons.
  </li>
</ul>

<p>All three of these falls are on land owned by the <b>Trustees of Reservations</b>, a
  Massachusetts-based conservation group. All falls have clear public access.</p>

<p>Parking is available at all the falls, but it is not extensive at any of them. You will
  probably have to park along the road at Royalston Falls before you set out to hike. There may be
  space available there for four or five cars. Parking at Doane's Falls is plentiful, but the
  parking area is often full. It is a popular waterfall. There is additional parking on the other
  side of the bridge that is there. Parking at Spirit Falls is a bit scarce. There is clearly
  space for two or maybe three cars. Additional cars might be able to park along the road, but the
  road is not well suited to it at that spot. Thankfully, Spirit Falls is not a popular location
  so that most likely you will be the only people there when you arrive.</p>

<p>To visit all three falls would require several hours and a total of 6 miles of walking over
  easy terrain. However, because of the proximity of the falls to each other, it is easy to modify
  plans at the last minute and take in only one or two instead. I suggest that you be sure to
  visit Doane's Falls as that is the most spectacular.</p>

<p>There are no eating places (that I could see) in Royalston. However, Athol to the southwest and
  Gardner to the east both have a variety of restaurants. Packing a lunch is also a good option on
  this trip.</p>

