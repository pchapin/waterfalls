Overview: A wild falls just a short distance from the road.
Location: Hancock, VT
Stream  : Robbins Branch of the White River
Height  : 50 feet (estimate) in two drops.
Finding : No landmarks, but close to the road.
Access  : Moderate. Short hike with bushwacking.
Swimming: A small pool below the lower falls would be nice for splashing.
Basking : Some spots, but the falls is well forested and not too sunny.
Legal   : Uncertain.
