<h2>Directions</h2>
<p>
  <b>From Hancock</b>
</p>
<p>Starting at the junctions of route 100 and route 125 in the middle of Hancock (0.0 miles),
  follow route 125 west toward Middlebury. The road is clear and easy to follow as it follows the
  Hancock Branch of the White River. After some distance, the road passes the entrance to <a
      href="texas.html">Texas Falls</a>. At 5.5 miles, the road turns to the right and begins
  climbing earnestly into Middlebury Gap. At that point a guard rail starts on the left side of
  the road. Just before the guard rail, there is a small, unmarked turnout where you should park.
  This spot is near the base of one of the ski lifts of the Middlebury Snow Bowl ski area.
  However, there are no signs of any kind.</p>

<p>
  <img src="photos/bailey-02.jpg" alt="Bailey Falls" style="float: right; padding: 10px"/>
</p>

<p>From the turnout, walk down the embankment toward the stream and toward the ski area. You
  should see a small, wooden bridge across the stream that I assume is maintained by the ski area
  for their purposes. Cross the bridge and walk up the gentle rise on the other side to a small
  building at the base of the ski trails. Walk to the left along the edge of the ski trail for
  about 30 or 40 yards.</p>

<p>At any point along this walk you can cut into the woods and strike toward the stream that runs
  parallel to the ski trail. However, if you follow the ski trail, you will shortly come to an
  obscure, overgrown road that leads into the woods. If you follow that road, you will quickly
  come to the stream just a little ways above the main falls. There is no bridge, but the stream
  is fairly easy to cross. In this way you can scramble downstream to view the falls from either
  side.</p>

<p>It is only a short distance from the ski trail to the falls. You should be able to hear the
  falls from the ski trail—particularly in high water. However, the falls have cut a ravine that
  is tricky to climb down. The walls of the ravine are not sheer, but they are very steep. Do not
  attempt to climb down them unless you are comfortable with such hiking. There are no trails or
  signs; you are on your own when you approach these falls. Despite the ravine, however, the falls
  are still quite approachable. The terrain by the falls themselves is easier. If you cut in from
  the ski trail too early, you may want to walk upstream a little before getting close to the
  water. The other side is not so steep. You can also cross the stream above the falls and hike
  down the other side to approach the falls.</p>

<p style="clear: right"/>
<p>
  <b>From Middlebury Gap</b>
</p>
<p>Follow route 125 east for 0.9 miles. On the right side of the road, a guard rail will end and
  there will be a small, unmarked turnout just after the rail. This is the same turnout mentioned
  above. Park at this turnout and follow the directions above to the falls.</p>

