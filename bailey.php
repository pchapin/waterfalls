<h2>Description</h2>
<p>Sometimes the most beautiful things are in spots that nobody seems to know about. Bailey Falls
  is one of those things. This dramatic and beautiful waterfall is not marked and has no trails
  approaching it. Yet it is only a few yards away from one of Vermont's major highways. No doubt
  countless thousands of people have passed right by these falls without knowing they were there.
  When the leaves are off the trees, it is possible to see these falls from the road, but only
  just barely. I've caught a couple of glimpses of the falls as I've driven from Middlebury to
  Hancock, but it is difficult to see them even when you know just where to look. In the summer it
  is impossible to see the falls from the road.</p>

<p>
  <img src="photos/bailey-03.jpg" alt="Bailey Falls" style="float: left; padding: 10px"/>
</p>

<p>Approaching Bailey Falls is not trivial. The distance is short, but the route is rugged. I've
  come up from below, but I had to ford a moderately large stream (the falls are on a tributary of
  the stream that parallels the road) and push through a fair amount of brush. I've approached the
  falls from the nearby ski trail, but I had to scramble down the steep embankment of the ravine
  bordering the stream. This entailed sliding down wet, slippery leaves on the seat of my pants
  using trees to stop my fall. I've also approached the falls from above, hiking well up and
  around to find a suitable crossing and a moderate slope to the stream. Don't try to approach
  these falls if you aren't willing to do a little scrambling in the woods.</p>

<p>The effort to see these falls is well worth it. They are far more dramatic and beautiful, in my
  opinion, than the popular and nearby <a href="texas.html">Texas Falls</a>. The small stream that
  powers the falls begins by spurting over a rocky lip some fifty feet above the bottom of the
  falls. The water pours down a rocky chute and then changes direction to flow over a large
  outcropping in a wide, lacy curtain. There are a number of open rocky spots below the falls from
  which you can get a very excellent view. It is also possible to approach the falls closely at a
  point between the upper and lower sections.</p>

<p style="clear: left"/>

<p>Below the main falls the stream pours over a couple of minor drops as it passes between the
  steep walls of a small ravine. One of those minor drops has a small, shallow pool at the bottom
  that might be fun to splash around in. However, the area is heavily shaded and probably buggy
  during the warm times of the year. Also, there probably wouldn't be enough water in the stream
  late in the year to make "swimming" possible.</p>

<p>The stream above the falls has another small drop into a large pool that is very interesting
  and pleasant. On the far side of the pool from the falls, there is a small, rocky "beach" that
  would make a wonderful place to sit in the sun and read, write, or picnic. Although this little
  drop is not nearly as impressive as the main falls, it is still a very nice spot.</p>

