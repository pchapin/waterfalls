<h2>Notes</h2>
<dl>
  <dt>
    <b>Saturday, May 17, 1997</b>
  </dt>
  <dd>
    <p>I was looking for something to do today and decided to visit these falls. The water was
      high, but not excessively so. The air was cool, and the sun played about the clouds. The
      falls were very beautiful and impressive. I scrambled down the ravine from the ski trail and
      jumped across the stream with moderate difficulty. From a rocky spot well below the falls I
      was able to gaze up at them in their fullness. With the sun on the white water, and the blue
      sky overhead, the view was truly stunning. What a fine waterfall!</p>

    <p>I climbed up alongside the falls and spent quite a bit of time just sitting next to the
      stream watching and listening to the water. Nobody else interrupted my time there. I even
      spent some time with the small falls upstream from the main falls. It was a great day to
      visit Bailey Falls.</p>
  </dd>

  <dt>
    <b>Sunday, May 12, 1996</b>
  </dt>
  <dd>
    <p>The water was very high today—mostly due to rain. On my way home from visiting my friend
      Kim I decided to stop and see how Bailey Falls was doing.</p>

    <p>The first odd thing was the snow. It was snowing steadily in<b>Middlebury Gap</b> where
      there was even an accumulation of about one inch. Such is the nature of spring in Vermont.
      The ground was soggy, however, because it has been warm for the last few weeks.</p>

    <p>I knew I wasn't going to be able to cross the stream as John and I had done last year on
      account of the high water. So I walked a short way up one of the <b>Middlebury Snow Bowl</b>
      ski area trails and then cut into the woods at a point about even with the falls. This
      approach worked very well. In short order I came out right at falls about midway from the
      base to the top. It was awesome!</p>

    <p>I hadn't properly appreciated last year how impressive these falls are. John and I
      approached from below, and we only really bothered to get a good look at the lower falls.
      However, the upper falls are every bit as large and interesting as the lower ones. In a
      sense, John and I only saw half the waterfall last year.</p>

    <p>I climbed out onto a rocky prominence and sat for a while in the damp breeze flowing off
      the falls. It was wet and cold, but also exhilarating. Afterwards I scrambled up to the top
      of the falls just to be sure that I wasn't missing anything else. A short ways upstream
      there was, in fact, another small drop. But compared to the falls below it was pretty
      insignificant.</p>
  </dd>

  <dt>
    <b>Saturday May 27, 1995</b>
  </dt>
  <dd>
    <p>I visited these falls with my friend, John.</p>

    <p>From the turn-off a little ways below <b>Middlebury Gap</b> we walked into the woods. Right
      by the road there was a small stream flowing down from the gap. However, the falls were not
      on that stream. Instead, they were on a tributary that flowed into the stream on the other
      side. The leaves had just recently come out, so it took a little looking before we noticed
      the falls through the trees.</p>

    <p>The falls themselves were very nice. The water formed a graceful curtain that draped down
      over a rock outcropping. The stream that flowed over the falls was not too large, so was it
      easy to climb up on the rocks right next to the falls. Surrounding the falls were lots of
      fern and moss. The scene was very peaceful.</p>

    <p>Highly recommended—if you don't mind a little bushwacking.</p>
  </dd>
</dl>

