<h2>Wednesday, June 12, 1996</h2>

<p>I was on my way from one fall to another when I spotted these falls beside the road. Although
  labeled in <i>The Maine Atlas and Gazetteer</i> (by the DeLorme Mapping Company), it was not
  noted as a "scenic waterfall." The falls were nice, but I have a feeling they wouldn't be much to
  see under normal conditions. Today the streams were very high due to heavy thunderstorms that
  had passed through only a few hours earlier.</p>

<p>The falls are right along route 120 just a couple of miles east of the center of <b>Andover</b>.
  Although not particularly high, the spot consists of the <b>Ellis River</b> tumbling over a
  large, rocky outcropping in an attractive way. The river divided into two major streamlets as it
  flowed over the falls (<a href="photos/ellis.jpg"
  >Picture</a> [120 KBytes]).</p>

<p>The spot was clearly well known to the local residents. There was evidence that it was a party
  spot or a swimming hole. Just below the falls there was a large pool where the river collected
  itself again before continuing its journey to the Atlantic.</p>

<p>Worth stopping by if you're in the area, but don't make a special trip.</p>

