<h2>Directions</h2>
<p>I have not yet personally visited these falls. However, Deanna Flynn has sent me some
  information about them, and I am going to include that information here (with permission). The
  falls are marked on the latest edition of DeLorme's <i>Main Atlas and Gazetteer</i>. See <a
      href="references.html">my references</a> for more information. Based on that map, I can give
  the following approximate directions.</p>

<p>From Bingham, ME take route 16 south (or is it west?). The road immediately crosses the
  Kennebec River. Right after the river crossing, turn right onto a side road. That road follows
  close to the river for some distance and eventually skirts the edge of Wyman Lake. About a mile
  after leaving the lake, the road comes to the area of the falls. I do not know what that area
  looks like, but Deanna tells me that there is a sign for the falls posted by the road.
  Furthermore, the path to the falls is apparently well-marked. The roads leading to the falls are
  all paved.</p>

<hr/>
<h2>Description</h2>

<p>The following information was sent to me by Deanna. She visited these falls during the summer
  of 1997. She also sent me the picture of the falls displayed above.</p>

<p>
  <i>"It was kind of a rough hike in although the trails were well maintained. I guess anything is
    rough walking with a two-year-old! I believe there may have been more to see also. This
    picture [above] was taken at the bottom of the falls where the stream feeds into the lake. I
    wish the color was better in my picture, but you can get the idea of it. It was not
    spectacular, but it was a nice waterfall. We waded in the pool at the bottom which I would
    think would be much deeper in the spring. When we were there the water seemed quite low. If
    you are ever up that way, it might be worth a stop."</i>
</p>

<p>Thank you for the notes! I'll have to check it out sometime. It does look like a nice
  waterfall.</p>

