<h2>Wednesday, June 12, 1996</h2>
<p>
  <img src="photos/rumford.jpg" style="float: right; padding: 10px" alt="Rumford Falls"/>
</p>
<p>I was driving through downtown <b>Rumford</b> when I saw these falls. Just as I was turning
  west on US route 2 at its junction with route 108, I looked across the <b>Androscoggin River</b>
  to see a spectacular waterfall some ways in the distance! Thinking quickly, I pulled into a
  tourist information center that was across the street and from its parking lot I was able to get
  a very nice view of the waterfall.</p>

<p>Rumford Falls (as it is called on <a href="references.html"> <i>The Maine Atlas and
      Gazetteer</i> </a>) is a massive waterfall where the Androscoggin River pours over a large
  rocky spot. The Androscoggin is a major river; one of the most significant waterways in central
  Maine. To see such a large river pouring down maybe 100 feet in a series of wild, violent
  cascades was an impressive site!</p>

<p>Alas, I was some distance from the falls themselves. What looked like some sort of power
  station was built into the rocks next to the falls and, as I drove up the road later, I saw that
  it was clearly off limits to ordinary people like me. Thus getting close to this rather
  impressive waterfall would be difficult. It might be possible to bushwack to it from the other
  side of the river, but I didn't investigate that.</p>

<p>Rumford Falls is obviously an example of an <a href="urban.html">urban falls</a>. Although the
  rock formation over which it pours is clearly natural, the drop has also clearly been harnessed
  for electrical power. Such is the nature of many New England waterfalls.</p>

<p style="clear: right"> Worth a stop for sure if you are driving through Rumford. Bring a
  spotting scope.</p>

<hr/>
<h2>Monday, June 22, 1998</h2>
<p>A reader of this site who grew up in Rumford pointed out to me in an email that Rumford Falls
  is the largest waterfall in terms of water volume east of Niagara Falls. I hadn't realized that!
  Thanks for the information!</p>

