<h2>Wednesday, June 12, 1996</h2>

<p>Heavy rains this afternoon due to thunderstorms filled the rivers and streams to flood capacity
  and beyond. As a result, these falls were extremely spectacular.</p>

<p>I followed <b>East Hill Road</b> out of <b>Andover</b> for several miles. At the spot were <b>Frye
  Brook</b> crosses under the road there is a trailhead. It is marked with a sign that it is
  easily seen from the car if you are watching for it (but also easily missed). On the other side
  of the road from the sign there is an obvious parking area.</p>

<p>The trail climbs up along Frye Brook, which today was a raging torrent of amazing proportions.
  The trail itself was covered with water, and I often had to jump small little streams that were
  gurgling their way downhill to add their contribution to the river.</p>

<p>After a short distance, the trail climbed up a steep incline and came to the first falls (<a
    href="photos/cata1.jpg">Picture</a> [133 KBytes]). These falls plummeted down a narrow rock
  crevice and into a large pool. It dropped over 50 feet with a great rush and noise. The problem
  was that a large shoulder of rock prevented me from seeing the main part of the falls. From
  below, I could see the outlet of it into the pool, and from above I could sit right at the lip
  of the falls where it began its plunge. However, I could not get a view of the entire falls! I
  think the best view would be from the other side of the stream. There the sides of the ravine
  above the pool were not quite as steep, and it looked as if a person could get into an ideal
  viewing position. But the stream was uncrossable and I didn't want to double back to the road in
  order to bushwack up the other side.</p>

<p>I followed the trail upstream and soon came to the second falls (<a
    href="photos/cata2.jpg">Picture</a> [128 KBytes]). Here the brook flowed over a large, rocky
  outcrop. Unlike the lower falls, I was able to get a good view of this one. Amazing! The stream
  was blasting itself against a large rock that was itself completely submerged in raging foam. As
  the water flowed around the rock, it spurted over it in a spectacular plume that shot many feet
  into the air. Even where I was, 20 or 30 feet away, I was sometimes splashed by the droplets
  raining down from that plume!</p>

<p>I followed the trial upstream some more and before long came to the third fall. Here the trail
  crossed the stream to what looked like a picnic area. Crossing would have been unthinkable, but
  I didn't mind as it looked like there were no more falls anyway.</p>

<p>At the third fall, the stream plunged into a narrow, rocky chasm maybe 10 or 15 feet deep.
  There was a rock platform just a little ways downstream that allowed me to look right up into
  the chasm. It would have been a great picture. However, today the force of the water was so
  great that I was being showered with mist and wind as I stood on the platform. I could barely
  open my eyes to see the falls myself; I was not about to take my camera out! I'm sure that in
  more subdued times, it would be a lovely spot.</p>

<p>Highly recommended.</p>

