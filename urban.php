<h1>Urban Falls</h1>
<p>Many cities and towns in New England have waterfalls in them. These falls are mostly
  human-made. In this sense they are "artificial" falls and are similar to <a
      href="roadcut.html">road cut falls</a>. Urban falls typically flow over dams and other urban
  waterworks. They are often bound up closely with the history of the town in which they appear.
  Many old New England mills depended on water power as their primary source of energy. By drawing
  water from above the falls and releasing it below, a significant amount of work could be
  extracted from the flowing water.</p>

<p>The number of urban waterfalls in New England is vast. Many towns have one. Although my mind
  boggles at the prospect of doing so, I would like to catalog those falls as well as the natural
  waterfalls in the area.</p>

<p>This page is the start of that effort.</p>
