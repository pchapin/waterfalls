<p>
  <i>In a later note, Kim added:</i>
</p>

<p>"There are also some very remote falls in the backcountry, most of which are very difficult to
  get to, in terms of time and access."</p>

<p>"In the Second College Grant and Pittsburg/Clarksville area in New Hampshire, there are two
  "squeeze hole" falls, which were well known in the days of the river drive loggers. They are
  Hells Gate Falls and Little Hells Gate Falls, both of which are narrow passages and brief drops
  in the Dead Diamond River. These falls used to hang up the log drives in some years because it
  was easy for logs to jam into the narrow passes."</p>

<p>"There are also several remote mountain gorges through which water descends. They are not
  falls, but they are beautiful areas with steep or cliff-like walls. One is accessed through a
  gate just off Route 16 a few feet before it passes into northern Maine from New Hampshire. This
  is the access road to the Second College Grant. By walking in a mile, you arrive at a beautiful
  gorge."</p>

<p>"The other is Canyon Gorge on the so-called Canyon Trail in the farthest northeast region of
  Pittsburg hard by the Canadian Border and in Indian Stream, at its very source. I have yet to
  see it, as it takes a terrible amount of time to get there. Yet it is one of the very few
  natural features in the far north that has ever had a trail cut in to it."</p>

