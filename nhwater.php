<h1>Other New Hampshire Waterfalls</h1>
<p>
  <i>The following falls in New Hampshire (and some just across the border in Maine) were brought
    to my attention by various people. These are all falls that I have not yet seen myself. As I
    visit these falls, I will take them off this list and add them to my main list. The
    information here may overlap somewhat with my list of <a
        href="wmfalls.html">White Mountain waterfalls</a>.</i>
</p>
<hr/>
<p>
  <i>The following notes were sent to me by Dean Goss on June 27, 1997. Thank you, Dean for this
    information! Dean has also sent me information about some <a
        href="vtwater.html">Vermont waterfalls</a> as well.</i>
</p>
<ul>
  <li><b>Pond Brook Falls</b> in the Nash Stream Reserve north of Stark. This is loosely modeled
    on Step Falls but nowhere near the size. A few minor cascades above a long granite chute
    through which the water races, and a few minor cascades below. A great western exposure would
    make for great photos, alas the sun was out, so I bagged taking any shots.
  </li>

  <li><b>Dixville Flume</b>: Rt. 26 just past The Balsams. A miniature version of the Franconia
    flume, no hike required.
  </li>

  <li><b>Huntington Cascades</b>: about .5 miles east of the Dixville Flume. A short walk to an
    extremely photogenic waterfall, with another nice waterfall just upstream. The lower falls are
    a 15' drop where the stream splits into 3 chutes. The upper falls is a 40' drop no more than
    2-3' wide. Both here and at Dixville Flume the sun was kind enough to vanish in the haze
    allowing me the comparative luxury of .5-2 second exposures.
  </li>

  <li><b>Mother Walker Falls</b>: Grafton Notch, Maine. A very small waterfall in the "cave"
    formed between the gorge wall, and a <em>huge</em> talus boulder. Difficult to see, but a neat
    formation.
  </li>

  <li><b>Thompson Falls</b> in Pinkham Notch, very early in the AM. What a great waterfall! Do you
    have <a href="references.html">Bruce Bolnick's "Waterfalls of the White Mountains?"</a> If so,
    his description of the lowest falls is dead accurate. It looks like water cascading over the
    lip of a giant clamshell. The sun was very low in the eastern sky, allowing a full 3 seconds
    of exposure. I got the photos back, and was blown away by the results. The chutes and other
    cascades above are equally beautiful. This was a very nice waterfall encounter.
  </li>

  <li><b>Crystal Cascade</b>, well, not quite. Even though I reached the Pinkham's Notch
    campground at 6:15 AM, the crowd on the trail was legion! I bolted for the car.
  </li>

  <li><b>Winneweta Falls</b>, in Jackson. The mosquitos were awful, but miraculously absent at the
    falls. This waterfall was very attractive, a 30' set of steps over mossy rock. The only
    drawback was that all the ideal vantage points for the purpose of photography were in midair
    in the gorge. I eventually got my shots by climbing up the face of the falls and standing in
    the flow.
  </li>
</ul>

