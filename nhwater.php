<p>
  <i>The following notes were sent to me by Roy Sunter on June 22, 1998. These are falls in
    south-western New Hampshire.</i>
</p>

<ul>
  <li>
    <p><b>Pulpit Falls</b></p>

    <p>From the intersection of Rts 119, 10, &amp; 78 in Winchester, NH, travel S on Rt 10
      (towards Northfield, MA). After about 2.6 miles, the road will be winding downhill, curving
      to the right. Look for a pull-off on the right immediately after the curve, and a jeep trail
      on the left. Park in the pull-off and take the jeep trail on foot.</p>

    <p>There are one or two signs painted on rocks pointing out "Falls Trail," or some such; but I
      have never had any luck following these; they always seem to peter out, and I end up
      bushwhacking. The falls are easy enough to find this way; just follow the sound of running
      water to the brook's edge and head upstream.</p>

    <p>The falls themselves are not particularly high (maybe 10–15 ft total drop), but if you're
      bushwhacking upstream along the brook's edge, their sudden appearance is quite dramatic. The
      undergrowth you've been pushing through suddenly opens up at a bend in the stream, affording
      an unobstructed view of the falls. Depending on the time of year, there can be up to three
      separate, parallel cascades, the middle one in free space, the others following the rock
      bed.</p>

    <p>A trail continues upstream through some interesting rock ledges and ends at a small beaver
      pond.</p>

    <p>This is a rather modest little waterfall and may not warrant a day trip of itself; but it's
      worth adding to your itinerary if you've planned a trip to some of the other nearby falls
      already mentioned on your page (<a href="royalston.html">Royalston Falls</a>, <a
          href="doanes.html">Doanes Falls</a>, <a href="spirit.html">Spirit Falls</a>).</p>
  </li>

  <li>
    <p><b>Fay Falls</b></p>

    <p>From the center of Walpole, NH, take Prospect Hill Road, and about 0.2 mi later bear right
      onto County Road. Follow County Road through some very scenic country for about 3.5
      miles.</p>

    <p>The falls are not visible from the road, and the landmarks I use to find them are somewhat
      subtle. After you've traveled for about 3.5 miles on County Road, you should be bearing
      uphill and turning rather sharply to the left. Look for a white farm gate on the left and a
      jeep trail on the right. I have not visited the falls since I was a teenager, and the jeep
      trail was not there at that time, so I cannot say for sure if it leads to the falls; but it
      will certainly take you near enough the water to hear it, from which point anyone with any
      experience hunting waterfalls should be able to find them.</p>

    <p>Alternatively, you might continue on County Rd for a few more tenths of a mile. The road
      crosses the stream just as the woods give way to open field and a gorgeous open-topped
      drumlin on your left. There's enough room to squeeze your car off the road. Bushwhack
      downstream from there to the falls. Be forwarned, though, that you may pass through or near
      private property along the way.</p>

    <p>The falls themselves drop perhaps 20–25 feet, and during the summer may slow to a trickle.
      Their most interesting feature for me was the talus caves large enough for a chubby
      13-year-old to squeeze into and emerge from behind the cascade. That same 13-yr old gave no
      thought to the fact that wild animals resentful of his intrusion might call those caves home
      and take unkindly to his sudden appearance. With that caveat (pun intended) in mind, these
      falls are worth including in a day trip to the unsung wild places in the Monadnock Region of
      N.H., if not justifying a trip to themselves.</p>
  </li>
</ul>

