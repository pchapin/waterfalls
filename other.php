<h1>Other Waterfall Links</h1>

<p>The links below are all to sites like this one: made by individual waterfall enthusiasts
  interested in sharing their experiences. In the interest of keeping this list manageable and
  relevant, I only link to sites that cover similar or nearby geographic regions as I do. There
  are many waterfall sites on the Internet, and this is not intended to be a complete list.</p>

<p>Unfortunately, many waterfall sites that once existed on the Internet seem to be gone, or
  perhaps moved to locations I don't know about. Many of the sites that remain appear
  unmaintained, much as this site was until recently. I'm not sure why this has come to pass, but
  it feels sad.</p>

<p>The list below is sorted in alphabetical order by the last name of the site's maintainer.</p>

<ul>
  <li><p>Jon Binder, who has contributed to this page in the past, has created a nice site
      featuring <a href="http://www.angelfire.com/ny4/waterfalls/home.html">Waterfalls of the
        Eastern US</a> (right now mostly New York and North Carolina with a few falls from some
      other states). His photographs are excellent.</p></li>

  <li><p>Matt Conheady has a page featuring <a href="http://www.nyfalls.com">Upstate New York
        Waterfalls</a>.</p></li>

  <li><p>David Ellis has a nice page featuring the <a href="http://www.ctwaterfalls.com"
      >Waterfalls of Connecticut</a>. His page includes both photographs and descriptions.</p>
  </li>

  <li><p>Scott Ensminger has a nice compilation of <a
          href="https://falzguy.com/">Waterfalls of Western New York State</a>. His page contains
      information on waterfalls that anyone can visit, including photographs and other facts.</p>
  </li>

  <li><p>Robert Glaubitz has created an <a
          href="http://www.aria-database.com/waterfall/">Eastern Waterfall Guide</a> featuring
      information on waterfalls in the eastern United States. His site contains pictures,
      descriptions, and directions. He also has some tips on finding and photographing waterfalls.
    </p></li>

  <li><p>Greg Parsons, one of the authors of the book <i>New England Waterfalls</i>, maintains a
      corresponding website at <a href="https://www.newenglandwaterfalls.com">NewEnglandWaterfalls.com</a>.
      The coverage area of the book and site is essentially the same as for this site, although
      the style is quite different.</p></li>

  <li><p><a href="http://www.naturalhighs.net/waterfalls/">Ruth's Waterfalls</a> features the
      waterfalls of Rochester, New York, and the Finger Lakes region of New York. It is a very
      nice page with many pictures, directions, and descriptions. I especially like the clickable
      map.</p></li>
</ul>

