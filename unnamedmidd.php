<h3>Saturday, May 17, 1997</h3>
<p>These falls were easy to see from the road today. They were, however, on the far side of the
  stream that paralleled the road. To get to the falls, I had to cross that stream, and that was a
  bit difficult. With proper wading gear, it would have been easy enough, but the water was much
  too cold and swift to wade barefoot. I was able to get across by walking on a tree that had
  fallen over the stream, but I ended up getting a bit wet anyway.</p>

<p><img src="photos/unnamedmidd4.jpg" style="float: right; padding: 10px"
        alt="Unnamed Falls"/> The falls cascaded down the steep ground toward the stream below.
  The rocks around the falls were covered with vegetation, including many ferns that were just
  starting to grow again after a long winter (spring came late to Vermont this year). The ground
  was covered with large boulders and footing was difficult. It would have been easy to
  accidentally put a foot into a leaf-covered crack between two boulders and break a leg. I do not
  recommend that you approach these falls unless you are comfortable with bushwhack hiking over
  difficult terrian.</p>

<p>The falls were exquisitely beautiful, however. As the water splashed and played its way from
  rock to rock, it looked like a true deep forest waterfall cascading through thick, moist
  vegetation. It was the very icon of a mountain stream. Higher up the stream spilled into a large
  (20 or 30 foot) rock chute. As it did so, it arced outwards in a graceful curve. Truly, these
  falls were a wonderful find!</p>

