<h2>The Lower Falls</h2>
<p>I have not yet investigated these falls, although I have seen them from the road below the
  turnout. They are well back from the road, but they appear to fall down the same formation as
  the upper falls I described above. The same stream needs to be crossed, and then you would need
  to bushwack into the woods perhaps 100 yards to the falls themselves. I will try to check these
  falls out during the 1998 season.</p>

