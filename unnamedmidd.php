<h1>Unnamed Falls in Middlebury Gap</h1>
<p>Sometimes you see surprising things. During the spring run-off season, two impressive falls
  appear on the east side of Middlebury Gap in Vermont's Green Mountains. As far as I know, they
  have no name. They are also not particularly easy to approach. However, they are very
  interesting and worthwhile waterfalls.</p>

<p>In this document I will describe both of them, although I have so far only spent much time with
  one.</p>

<hr/>
<h2>Directions</h2>
<p>Both falls can be approached if you park your car at the turnout on the south side of Rt 125
  located 4.4 miles west of Hancock and 1.9 miles east of Middlebury Gap. There are no signs. In
  the summer these falls are not visible from the road. In fact, I doubt if they even exist.
  However, both are visible from the road (if you are alert) during the time after the main
  snowmelt and before the leaves have grown on the trees.</p>

<p>There are two falls involved. The "upper" one is uphill from the turnout a short distance and
  relatively near the road. The "lower" one is downhill from the turnout about the same distance
  and well back from the road.</p>
