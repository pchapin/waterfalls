<h3>Friday, April 5, 1996</h3>

<p>Chesterfield Gorge is owned by the <b> <a href="http://www.thetrustees.org/">Trustees of the
  Reservations</a> </b>, a Massachusetts organization that owns a number of interesting natural
  areas in the state. The reservation is located on <b>River Road</b>. From the center of West
  Chesterfield, turn south off route 143. Follow the signs. The gorge is easy to find.</p>

<p>Today I visited this spot with my friend John and his girlfriend. We didn't stay long. We
  parked in the deserted parking area (too early in the season for most people) and walked down to
  the edge of the gorge. The gorge was right next to the parking area---there was no significant
  hiking involved.</p>

<p>The gorge itself was interesting enough, but not particularly spectacular. The <b>Westfield
  River</b> was large and deep as it flowed through the gorge. However, there weren't any
  significant waterfalls. The gorge was about fifty feet deep and had sheer walls that went right
  down to the water. You couldn't get down into the gorge so all the views were from above. That
  is less than ideal.</p>

<p>This spot is worth a stop if you are driving through, but I wouldn't go out of my way to see
  it.</p>

