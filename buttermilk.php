<h3>Saturday, August 17, 1996</h3>

<p><img src="photos/buttermilk-02.jpg" style="float: right; padding: 10px" width="300"
        alt="Buttermilk Falls"/> Today my daughter Hillary and I visited these falls for the first
  time. They were fairly easy to find. Start by taking Rt. 103 west out of <b>Ludlow</b>, Vermont.
  Just after the junction with Rt. 100 start looking for a VFW hall on the right side of the road.
  At the VFW hall turn right onto a paved side road. Follow that road 1.3 miles to the falls.
  There is no sign and no obvious parking area. However, there is a road/trail that decends (on
  the right) from the paved road toward the stream. If you are looking for it, you shouldn't have
  any trouble finding it. Today there was a "Vermont Bicycle Tours" van parked there and a number
  of people standing around. It was easy to locate.</p>

<p>The falls themselves were not particularly high. They poured over a ten-foot drop and into a
  large, deep pool of clear water. Although the bottom of the pool looked a bit rocky, it also
  looked like a very nice swimming hole. In fact, while we were there a woman stopped by and took a
  dip.</p>

<p>Quite a few people were there today. It was obvious that a group of bicyclists were stopping
  for a break from their tour and to enjoy a swim. The well-worn trails (and bits of litter) made
  it clear that the spot was a popular swimming hole.</p>

<p>Hillary and I explored the stream down below the falls. We spent some time splashing our feet
  in a pool below a lower, smaller cascade. Hillary even waded around for a bit. We then explored
  the stream above the falls. There was quite a crowd of people swimming in the pool just above
  the falls. Thankfully (for them) the current was not very strong today.</p>

<p>Interesting falls, but I have a feeling they would have been much more spectacular in high
  water.</p>

