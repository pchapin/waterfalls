<h2>Description</h2>
<p>There are three different things that need describing.</p>

<ol>
  <li>
    <b>Wadsworth Falls State Park</b>
    <p>The waterfall is not at the main park. Instead, the park features an extensive picnic area
      and a lake for swimming. Facilities include: changing rooms, restrooms, grills, and a
      telephone. The lake is clearly artificial. I do not know for sure what times of year
      swimming is allowed (or even possible). In late March, when I was there, the lake was
      drained. There did not appear to be a fee for the park, but I was there out of season.</p>
  </li>

  <li>
    <b>Little Falls</b>
    <p>This waterfall is on a small tributary of the Coginchaug River. It might be dry during some
      times of the year. However, it is a very nice waterfall. The cut-off trail from the
      direction of the main park approaches these falls from below. It comes up the ravine from
      downstream and right up to the very base of the falls. From the trail you get an excellent
      view of the stream as it flows down over a large rocky outcropping in a curtain. The trail
      then climbs up to the top of the falls before rejoining the main trail.</p>
  </li>

  <li>
    <b>Wadsworth Falls</b>
    <p>The main falls are fairly large. The Coginchaug River is a considerable river, so the
      quantity of water flowing over the falls makes for an impressive display. The river flows
      over a single, vertical drop of 10 to 15 feet in height. The area around the falls is well
      groomed and quite nice. There are a couple of picnic tables, but actually you could just
      spread a blanket on the grass and be comfortable. There are steps leading down to the falls,
      but the area is not overdone.</p>
  </li>
</ol>

