<h2>Notes</h2>
<dl>
  <dt>
    <b>Tuesday, June 14, 2005</b>
  </dt>
  <dd>
    <p>I visited these falls with my wife, Sharon, to get some digital pictures for this site. It
      was very warm today (around 90 degrees F), so we didn't do any significant walking. There
      was less water over the falls than I remembered from my last visit. This made the falls less
      imposing but in some ways more interesting. Instead of being a wall of water, today one
      could see the rocks over which the falls flowed.</p>
  </dd>

  <dt>
    <b>Monday, March 30, 1998</b>
  </dt>
  <dd>
