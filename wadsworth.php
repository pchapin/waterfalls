<h2>Directions</h2>
<p>
  <b>From Middletown</b>
</p>

<p>From route 9 take exit 15 onto route 66 west (0.0mi/0.0km). Follow route 66 through town. You
  will go by <a href="http://www.wesleyan.edu/">Wesleyan University</a> and the junction with
  route 3. Continue along route 66 until you reach the junction with route 157 at 1.5mi/2.4km.
  Turn on to route 157 south. Route 157 is a narrow, winding road that runs through a pleasant
  residential area. It parallels the Coginchaug River, more or less, on the way to Wadsworth Falls
  State Park. You will come to the main entrance of the park at 3.0mi/4.8km.</p>

<p>If you park your car at Wadsworth Falls State Park, you will need to hike along a trail
  approximately 1.6 miles to reach the falls. The hike is a pleasant walk in the woods. It is not
  difficult. The trail also goes by "Little Falls." This additional waterfall is an interesting
  and worthwhile stop.</p>

<p>The "Main Trail" leaves the picnic area at the state park. It is wide and obvious. About 1/3 of
  the way to Wadsworth Falls, a cut-off trial turns toward Little Falls. The cut-off trail
  continues past Little Falls and rejoins the main trail some distance farther on. The main trail
  then comes down to route 159, where you can cross the river on the road and come to Wadsworth
  Falls. You can also get an interesting view of the falls from the south side of the river
  (before crossing) by walking through the brush and scrambling down the steep bank. In some
  places the bank on that side of the river is a vertical drop, so please be careful if you
  attempt to do this.</p>

<p>If you would rather avoid the hike to the falls, you can drive to them instead. From the
  Wadsworth Falls State Park main entrance (0.0mi/0.0km) continue on route 157 south. At
  1.1mi/1.8km turn left on to Cherry Hill Road. This occurs after you go through a small village.
  You will come to a fork in that village. Be sure you bear to the left. Once on Cherry Hill Road,
  continue to the parking area for the falls at 1.2mi/1.9km. The parking area is on the left just
  before the bridge over the Coginchaug River.</p>

