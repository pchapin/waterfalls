<h3>Wednesday, May 22, 1996</h3>

<p>If you visit <a href="basin.html">The Basin</a>, you really should take a few minutes to walk
  up the <b>Basin-Cascades Trail</b>. This trail starts on the western edge of the maze of paths
  surrounding The Basin. It very quickly reaches <b>Cascade Brook</b> and runs parallel to the
  brook for some distance.</p>

<p>When you get to the brook, don't bother following the trail. Just scramble up the rocks next to
  the brook. The views are excellent! When I first stepped out onto the rocks, I was reminded at
  once of <a href="../ME/step.html">Step Falls</a> in Maine. The similarity shouldn't be surprising.
  Both falls are in the White Mountains, and both run over the same basic granite rock structure.
</p>

<p>The rock was worn smooth by the action of the water. In many places it was a wide expanse of
  flat stone tilted downhill into which the water has worn smooth dips and potholes. Today the
  water level was high, and it surged over the rock surface with lots of wild action. In some
  places the water would spout five feet into the air as it raced out of a dip or pothole (<a
      href="photos/casbrk.jpg">Picture</a> [127 KBytes]).</p>

<p>The cascades seemed to go on and on. Each turn of the stream brought new wonders into view. In
  one place the stream was focused into a channel only five feet wide. It surged and churned
  against a low cliff. In other places the water was draped over a wide expanse of rock and it
  poured down in thin, delicate falls everywhere.</p>

<p>After about 0.5 mile, the trail reached <a href="kinsman.html">Kinsman Falls</a>. Shortly above
  the falls, it crossed the stream and continued on to the <b>Cascade Brook Trail</b>. Supposedly
  there are more falls higher up, but I didn't explore them today. There was no bridge across the
  brook, and the water was too high for me to wade across.</p>

<p>Now I have an excuse to return some other time!</p>

