<h2>Directions</h2>
<p>The access road to Arethusa Falls is right along US Route 302 in Crawford Notch. Look for it on
  the west side of the road just north of the southern park entrance. There is a large parking
  area just off Route 302 and a smaller one just up the access road. If possible, you should try
  to park in the smaller parking area. Doing so takes a couple of hundred yards off your hike.</p>

<p>From the back of the lower parking area there is a trail that heads uphill through the woods.
  Follow the signs to "The Falls." Shortly you will come to the upper parking area where there is
  a house and a small ice cream shop as well (handy for after returning from the falls).</p>

<p>The main trail leaves from the left (south) side of the upper parking area. In a short distance
  the <i>Bemis Brook Trail</i> leaves to the left. You can go that way and visit <a
      href="bemis.html">Bemis Brook Falls</a> and <a href="coliseum.html">Coliseum Falls</a> as
  well. The Bemis Brook Trail rejoins the main trail farther up. (Note: the ascent from Bemis
  Brook Falls back to the main trail is quite steep and difficult).</p>

<p>The main trail climbs steadily for some distance. For a while Bemis Brook remains well below
  the trail and to the left. However, higher up the brook and trail are side by side. Finally, the
  trail crosses the brook on a wooden bridge and then shortly afterward comes to the falls from
  below.</p>

<hr/>

<h2>Description</h2>
<p>Arethusa Falls (pronounced ARA-THUSE-A by the local residents) is supposed to be the highest
  waterfall in New England. Various values of height are reported in different references, but
  heights in the 150– 200-foot range seem the most reliable. However, when you actually see the
  waterfall, it does not seem nearly that high. My personal estimate would put it around 100 feet—
  not much different than the nearby <a href="ripley.html"
  >Ripley Falls</a>.</p>

<p>Arethusa Falls is a spectacular waterfall. The trail comes out immediately below the falls, and
  right away one faces an imposing rock wall over which the water cascades. The drop is nearly
  vertical. It is difficult to see the top of the falls because of the angle of the rock. It is
  possible that what appears to be the top might actually just be a rock lip and that there might
  be significantly more waterfall above that point. If so, that would explain the difference
  between the reported and apparent height.</p>

<p>There are plenty of flat rocks below the waterfalls for tired hikers to stretch out and enjoy
  the view. If you want to take pictures, however, you'll need a wide angle lens or you'll have to
  back down the stream a bit. There is a pool at the bottom of the falls in which people might
  splash their feet. However, the pool does not look like a very satisfactory swimming spot to
  me.</p>

