<hr/>
<h3>Sunday, July 21, 1996</h3>
<p>
  <img src="photos/giant.jpg" style="float: right; padding: 10px" alt="Giant Falls"/>
</p>

<p>To get to <b>Giant Falls</b>, you must hike up the <b>Peabody Brook Trail</b>. This trail
  starts from the <b>North Road</b> in <b>Shelburne</b>. I suggest that you consult the <i><a
      href="references.html">AMC White Mountain Guide</a></i> for more information. I will say
  that you can't park right at the trail head. The trail starts at the end of a private drive only
  100 feet (or less) from someone's home. They have placed "No Parking" signs around the area.
  Instead, you have to park at a small, unmarked turnoff just east of where the North Road crosses
  <b>Leadmine Brook</b>. Then you have to walk a short distance down the road to get to the trail
  head. This is all explained in <i><a href="references.html">Waterfalls of the White
    Mountains</a></i>.</p>

<p>The trail crosses <b>Peabody Brook</b> shortly after leaving the North Road and then follows
  along a logging road for a while. Eventually it turns off the logging road and starts climbing
  steeply, slabbing along the side of a rather deep ravine with the brook at the bottom. After a
  fair amount of climbing a well-marked side trail leaves to the left. That side trail more or
  less follows the contour lines into the ravine and then follows the stream for a short distance
  to the falls themselves.</p>

<p>Giant Falls is... well... giant! As you walk up the trail, you find yourself in a large, almost
  bowl-like valley. At the head of the bowl, the stream flows down over a large rock face. I was
  able to climb up onto a rock opposite the main falls and sit gawking up at the impressive, water
  coated rock face. From that location I could barely see the top of the falls, perhaps 100 or
  more vertical feet above me.</p>

<p>After spending several minutes enjoying the spot, I then hiked back to the main trail and
  climbed farther up hoping to get to the top of the falls. The trail skirted the top of the bowl
  (from which there were some interesting views of the Presidential Range). Just as the trail
  turned sharply to the right, it came to the stream again. I bushwacked downstream a short
  distance to the top of the falls, where I sat on a rock and admired the view.</p>

<p>It wasn't clear to me that I was sitting at the same "top" I had seen from the bottom. I think
  I was, but it is possible that where I was sitting was not visible at all from the bottom. I
  think that, short of bushwhacking up the steep side of the falls, it would be very difficult to
  really see all of this waterfall!</p>

<p>Nice falls. It's worth the trip if you don't mind a little hike.</p>

