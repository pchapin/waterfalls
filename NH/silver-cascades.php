<h3>Tuesday, July 16, 1996</h3>

<p>Today Hillary and I spent the day in <b>Crawford Notch</b> enjoying waterfalls. After stopping
  by <b><a href="flumecas.html">Flume Cascades</a> </b>, we visited <b>Silver Cascades</b> only a
  hundred yards or so further down the road. These falls are located right along Rt. 302 just a
  short distance south of the height of land in the notch. There was a parking area across the
  road from the falls making access easy. You do have to cross the busy highway to get to the
  falls, but that isn't much of a problem.</p>

<p>To get the best view of Silver Cascades, you really need to walk up the stream a little way.
  Although you can see the main part of the fall from the road, the view from the road is a bit
  too far away. The walk to the falls is short, however, and quite simple.</p>

<p>There had been an unusually large amount of rain in the region this summer, and so there was
  quite a bit of water pouring over the cascades. They were very beautiful. High up, the stream
  flowed over a wide rock in a graceful curtain. But the lower part of the falls crashed down a
  narrow crack in the rocks and exploded into the lower pool with intensity.</p>

<p>An excellent waterfall.</p>

