<h3>Wednesday, May 22, 1996</h3>
<p>The Basin is a significant tourist attraction in the <b>Franconia Notch</b> region of New
  Hampshire. There are large, dramatic signs for it on either side of the Notch Parkway. There are
  large parking areas nearby that support both cars and campers. There is a paved walkway leading
  down to a place where you can view The Basin itself.</p>

<p>Hardly anyone was there today. It's too early in the season, and Wednesday afternoon is most
  likely not the busiest time for tourists anyway. I walked down to a place where you can stand
  right next to the river and view The Basin (not the paved route). It was close enough and low
  enough that I'm sure it must flood during the spring high-water season.</p>

<p>The Basin is an interesting sight. The <b>Pemigewasset River</b>, just a small stream at this
  point, falls over a small waterfall and into a large, round pothole. The pothole measures a good
  15 feet across and has been carved out of solid rock by the force of the river as it rolled
  smaller rocks and stones around the inside of the eroding pothole. The bedrock outcrops into the
  river just downstream from the pothole, and it has been smoothed by the action of the water. It
  almost looks melted.</p>

<p>I suspect that most of the erosion done at The Basin was done when the glaciers were melting
  off of New England several thousand years ago. At that time the river would probably have been
  higher, and it would have been filled with abrasive ground rock from the glacial run-off.</p>

<p>The Pemigewasset River is very beautiful. It is very clear and has a rich, emerald green color
  in the deeper sections. A little upstream from The Basin, the river cascades over a nice, but
  small fall and splits into two sections. One section fumes down a channel maybe only two feet
  wide right next to the trail. It is very unusual and interesting.</p>

<p>If you visit The Basin, however, you really should walk up the <a href="casbrk.html"
  >Cascade Brook Trail</a>. The Basin isn't much of a waterfall, but there are some falls on <b>Cascade
    Brook</b> that shouldn't be missed.</p>

