<h3>Monday, July 22, 1996</h3>
<p>
  <img src="photos/glenellis.jpg" style="float: right; padding: 10px" alt="Glen Ellis Falls"
  />
</p>

<p>Today I visited these falls just after a hike up <a href="../../100highest/eisenhower.html"
>Mt. Eisenhower</a>. They are very easy to find because they have a well-marked parking area along
  Rt. 16 just south of the height of land in Pinkham Notch. From the parking area you can walk
  under the road through a nice little tunnel to get to the falls themselves. Needless to say,
  this is a not a remote, hard to access waterfall!</p>

<p>The trail to the falls is wide and well graded. You could probably push a wheelchair along it
  without a problem. It follows the <b>Ellis River</b>, which is a medium-sized stream at this
  point. The river itself is rather senic as it flows around many sculptured granite boulders.</p>

<p>The trail decends a long stairway (no wheelchair access, really!) to a point just below the
  falls where there is a small observation area. The falls are quite impressive. Today the water
  in the river was high, and so the force and energy of the falls was probably greater than
  normal. The river drops over a 50 or 60-foot cliff into a pool. Surrounding the pool are even
  higher rock walls. The water doesn't drop straight down, but instead seems to fall at a slight
  angle. In that respect the falls are very distinctive.</p>

<p>There were many people there today. In fact, it was practically a mob scene on the path above
  the falls and on the steps. I didn't mind all the people. For many of them, this might be the
  only waterfall they will see in the White Mountains. I overheard one child asking his parents if
  there were any other waterfalls to look at. I couldn't help but suggest <a
      href="silver-cascades.html">Silver Cascades</a> in <b>Crawford Notch</b> since I just saw
  that one a few days ago. His mother thanked me, and we went our separate ways, but I got a kick
  out of that conversation.</p>

