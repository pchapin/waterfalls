<h3>Wednesday, June 25, 1997</h3>

<p>These falls were visible from Rt 13 on the south edge of town. They were well-marked and easy
  to find. They were located in a state park with extensive facilities. Swimming in the pool at
  the base of the falls appeared to be encouraged, although when Hillary and I were there, the
  swimming was closed due to the lack of a lifegaurd. (It was an early morning).</p>

<p>The falls were quite beautiful. Just a few feet from the parking lot, the stream cascaded down
  a large, slanting rock face into a generous pool. The rock, as appeared to be typical for that
  area, was layered into thin, horizontal sheets. Thus, the water appeared to be taking hundreds
  of small steps on its way to the bottom.</p>

<p><img src="photos/nybuttermilk3.jpg" style="float: right; padding: 10px" alt="Buttermilk
    Falls"/> Hillary and I crossed a wooden bridge at the outlet of the pool and then walked up
  the trail on the far side of the stream. The trail climbed steeply in places, but nicely formed
  steps made the ascent easy enough. The trail followed right alongside the stream, and soon we
  came to more cascades higher up. The state of New York discourages people from scrambling out on
  the rocks, although at one point it was clearly safe to do so and we did anyway! We visited
  three different sections of the falls, but there appeared to be even more higher up. It was hot,
  and we had other plans, so we didn't follow the trail to the end. I expect that it would be
  rewarding to do so, however.</p>

