</pre>
<h3>Wednesday, June 25, 1997</h3>

<p>This waterfall was easy to locate. Hillary and I travelled along Rt 89 north from Ithaca. Only
  a few miles outside of town, we came to the well-marked Taughannock Falls State Park. To see the
  falls, we had to park on the west side of the road. The parking area on the east side of the
  road was for the beach on Cayuga Lake.</p>

<p>We then hiked 0.75 miles along the Gorge Trail to reach the falls. The trail was as wide as a
  small road and very level. It was an easy hike, although it was very hot and humid the day we
  were there, and that made it somewhat uncomfortable. The trail stayed close to the stream, but a
  number of other people who were there just walked up the streambed. In some places there were
  large expanses of flat rock next to the stream, making such walking easy. In other places it was
  necessary to wade in the stream.</p>

<p>Below the falls, Taughannock Creek flows in a deep and rather spectacular gorge. The rim of the
  gorge was up to 400 feet above the floor. The south side was sloped enough to allow trees to
  take hold in many spots along the wall, but the north side was practically sheer. The
  steep-sided gorge made an interesting contrast with the nearly level floor where the stream
  flowed.</p>

<p><img src="photos/taughannock1.jpg" style="float: right; padding: 10px" alt="Taughannock
    Falls"/> The gorge ended in a large, amphitheater-like bowl. The walls were sheer all around.
  No trees could grow. At the head of the bowl, the stream poured from a notch and fell straight
  over 200 feet to a large pool at the bottom. Taughannock Falls is one of the highest single
  vertical drops in the East. At first, because there were no good points of reference, it was
  hard to tell how high the falls were. Then a few people scrambled down near the base of the
  falls (against the rules posted on the signs that warned of falling rock). With those people
  there for a scale, the true size of the waterfall became apparent. It was indeed very
  impressive!
</p>

