<h1>Finger Lakes Waterfalls</h1>

<p>Although not really part of New England, I have visited some falls in nearby New York. In this
  document I will describe the falls I've seen in the Finger Lakes region. This is a rich area for
  waterfalls. If you are interested in waterfalls and are touring the Northeastern United States,
  be sure to check this region out. There are many more falls and gorges in that area than I have
  listed here!</p>

