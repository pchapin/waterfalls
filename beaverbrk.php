    <p>It was a beautiful day at first, but clouds started to move in later in the day. I was
      surprised at how few people were around. Evidently weekdays in June are not busy times in
      the White Mountains. I was also pleasantly surprised at how few bugs there were today. There
      were some black flies about, but they were not nearly as much of a problem as I would have
      expected for the time of year. The air was cool today and that probably supressed them.</p>

    <p>I spent about three hours at the cascades. I took my time and enjoyed many spots along the
      trail. I took many pictures. When I got back to the car, my legs were tired. This was my
      first real hike of the season, and it was steep!</p>
