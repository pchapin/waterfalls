<h1>Beaver Brook Cascades</h1>
<pre>
Overview: Impressive set of Cascades on the side of Mt. Moosilauke.
Location: Kinsman Notch in the White Mountains.
Stream  : Beaver Brook
Height  : 1000 vertical feet of cascades. No single drop more than about 50 feet.
Finding : Easy.
Access  : Steep, rough hike. However, the first cascade is a short, easy walk.
Swimming: No.
Basking : Several places. Heavily wooded.
Privacy : The trail is a popular route up Mt. Moosilauke.
Legal   : White Mountain National Forest. Public land.
</pre>
<hr/>
<h2>Directions</h2>

<p>
  <b>From the South</b>
</p>

<p>From the junction of New Hampshire Rt 112 and Rt 118 (0.0mi/0.0km) follow 112 into Kinsman
  Notch. At 3.6mi/5.8 km, there is a hiker's parking area on the left (west) side of the road.
  This is just before the Appalachian Trail crosses the road. It is well-marked. The parking area
  contains space for quite a few cars. Restrooms are also available.</p>

<p>Toward the back of the parking area, there is a sign with information about the trails in the
  area. The trail begins behind the sign. Almost immediately the trail comes to a tee junction.
  The right-hand turn leads back to the road. The left-hand turn leads to the falls. The trail
  then crosses Beaver Brook, passes through the woods for a short distance, crosses the brook
  again, and then starts to climb.</p>

<p>The first cascade is reached about 0.3mi/0.5km from the parking area. At this point there has
  been minimal climbing. From the first cascade the trail ascends steeply beside the stream for
  about 0.8mi/1.3km. There are numerous views of cascades all along the trail. The trail is rough
  and very steep. In places wooden steps and handrails have been provided to make the climb
  easier. I do not recommend this trail unless you are an experienced hiker.</p>

<p>Relatively high up, the trail comes right next to the stream where there is a large, flat rock
  that looks like a good resting spot. Above this point the cascades diminish. The trail then
  climbs somewhat away from the stream and soon bends to the left where it follows a tributary of
  Beaver Brook on its way to the summit of Moosilauke. If you turn around at the flat rock, you
  will have seen most of the cascades there are to see.</p>

<p>
  <b>From the North</b>
</p>

<p>From the junction of New Hampshire Rt 112 and Rt 116 (0.0mi/0.0km) follow Rt 112 into Kinsman
  Notch. The road passes Beaver Pond on the right and then comes to the crossing with the
  Appalachian Trail at the height of land. Almost immediately after that crossing at 5.0mi/8.0 km,
  there is a hiker's parking area on the right (west) side of the road. From here follow the
  directions outlined above.</p>

<hr/>
<h2>Description</h2>
<p>Very impressive!</p>

<p>In less than one mile, Beaver Brook falls 1000 feet down the side of Mt. Moosilauke and into
  Kinsman Notch. During that trip the stream flows over numerous cascades. The exact number of
  significant drops is difficult to count. However, once the show has started, the stream flows
  from one waterfall to the next until it starts to level out high above the notch.</p>

<p>The Beaver Brook Trail is an arduous climb, ascending with steep continuous grades. However,
  you may not find it difficult because you will be tempted to stop every few feet to enjoy yet a
  new aspect of the stream! The trail stays close to the stream most of the way, although there
  are a few spots where you will want to scramble through the brush and over a few rocks in order
  to get a better view. <em>Please use caution!</em></p>

<p>There are two cascades especially that stand out as excellent. The first is right at the
  bottom. Here you will come out onto a rocky shoulder with the stream flowing dramatically down a
  steep face in front of you. The stream flows in a narrow ribbon, changing direction slightly as
  it does. It then runs out a bit before collecting itself to flow down a narrow chute at the base
  of the rocky shoulder. The drop is perhaps 30 or 40 feet and is quite lovely. It is also rather
  easily accessible. This cascade occurs before the trail begins any serious climbing.</p>

<p>The second especially good spot is about halfway up. Here the stream flows down a nearly
  vertical rock face of about 50 feet. It splashes at the bottom of the face and immediately flows
  on and down over rough terrian. Ferns, moss, and trees adorn the sides of the stream and hang
  out over the falls, making the spot the very icon of a mountain waterfall. It is very
  beautiful.</p>

<p>In addition to these two spots, there are numerous other places of interest along the stream.
  Virtually every bend in the trail brings into view a new cascade of some kind. There are many
  falls here that if taken by themselves would each be a wonderful find, yet that seems like just
  another turn in Beaver Brook!</p>

<p>Note also that the Beaver Brook Trail is part of the Appalachian Trial. It is also a popular
  route for climbing Moosilauke. Consequently, you should expect a fair number of people on the
  trail.</p>

