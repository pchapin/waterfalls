<h3>Saturday, April 22, 1995</h3>

<p>Despite the name, there are actually waterfalls here. This spot is very easy to find. It's
  located on Rt. 17 only a mile or so East of its junction with Rt. 116 just North of
  <b>Bristol</b>. There is a sign by the road and a small parking area.</p>

<p>There is a trail going from the parking area down along<b>Baldwin Creek</b> to the falls. As is
  typical for such a public spot, there are fences along the edge of the ravine to prevent the
  foolish from hurting themselves.</p>

<p>The falls themselves are quite interesting. Baldwin Creek flows first over a five or six-foot
  drop and then over another slightly higher drop into a narrow channel only a couple of feet wide
  and with walls that are at least 20 feet high. The channel has many twisted potholes in it.</p>

<p>It's difficult to get a really good look at the lower falls because of the narrow ravine into
  which it flows. I climbed down to the bottom of the channel and scrambled over some rocks so
  that I could look up the channel. It was an interesting vantage point.</p>

<hr/>
<h3>Tuesday, February 20, 1996</h3>

<p>I returned to these falls today to see what they looked like in the winter months. This was my
  vacation week, so it was easy enough to find the time to get out. However, it wasn't very
  wintery. We've had some unusually warm weather, so most of the snow was melted.</p>

<p>Nevertheless, the falls were well shaded and encased in ice. In fact, they really were not very
  interesting. You could only see a few hints of running water through the occasional hole in the
  ice. Mostly they looked like an ice and snow-covered wall of rock.</p>

<p>The most interesting part of the experience was the sound. The usual waterfall sound was
  muffled to a faint swishing noise. It almost sounded like a large washing machine was running
  under the ice. It was strange.</p>

