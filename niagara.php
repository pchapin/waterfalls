<h3>Monday, June 23, 1997, and Tuesday, June 24, 1997</h3>
<p>My daughter Hillary and I took a trip to visit these famous waterfalls. Although not in New
  England, Niagara Falls are not that far from where I live, and I felt the need to see them. I've
  been to Niagara Falls before—more than once—but not since starting my work on this website. My
  daughter had never seen them before.</p>

<p><a href="guest_photos/niagara2_large.jpg"> <img src="guest_photos/niagara2.jpg"
                                                   style="float: right; padding: 10px"
                                                   alt="Niagara Falls"/> </a> As measured by the
  volume of water that flows over the falls, Niagara Falls is the largest waterfall in the world.
  The Niagara River carries 1.6 million (US) gallons per second. However, these days between 50%
  and 75% of that water is diverted to power turbines to generate electricity. As much as 3,000
  megawatts of electricty is generated in this way. Consequently, there is much less water flowing
  over the falls today than in years past. Nevertheless, it is still an impressive sight.</p>

<p>The height of Niagara Falls is not world-class. Although I don't normally see waterfalls with a
  175-foot single vertical drop, there are many waterfalls in the world that are far higher. In
  fact, <a href="arethusa.html">Arethusa Falls</a> in the White Mountains of New Hampshire and <a
      href="finger.html#taughannock">Taughannock Falls</a> in the Finger Lakes region of New York
  both boast single vertical drops in excess of 200 feet. Niagara does not even make the chart
  when compared to the truely high waterfalls of the world (Angel Falls in Venezuela is the
  highest at an astonishing 3281 feet).</p>

<p>Hillary and I arrived in Niagara Falls, Ontario in the late afternoon on Monday. After securing
  a place to stay the night, we walked down to the river to see the falls. Niagara is a type of <a
      href="urban.html">urban waterfall</a>. Although the falls are natural, they exist between
  two small cities and are thus surrounded by people and by the works of humanity. We approached
  the falls on city streets, crossing roads on walk signals and dodging cars. In addition, the
  falls have attracted a huge tourist market complete with gift shops, tours, boat rides, and many
  other things. Niagara Falls is about as far from a wilderness waterfall as it is possible to
  get.</p>

<p>Yet the sheer power of this massive waterfall overwhelmed all the tourist shops. Despite the
  masses of people, Niagara is a wild waterfall! As Hillary and I walked down the city streets
  toward the falls, I noticed a distinctive smell in the air. It was the smell of freshly
  vaporized water. It was the smell of a waterfall. I am used to smelling that smell in the deep
  forest glades that surround a mountain stream. Yet there it was on the city streets of Niagara
  Falls, Ontario: fully half a mile away from the waterfall itself!</p>

<p>The Canadians have created a very nice park along the edge of the gorge below the falls. You
  can walk alongside the gorge (well fenced off, of course) on paved walkways among gardens and
  lawns and well-groomed trees. In addition, there are nicely placed overlooks to make viewing the
  falls easier. Upstream from the falls is a large parking area from which you can take a shuttle
  bus to points along the river. All in all, it was a very pleasant spot. It was also very
  popular. The whole time Hillary and I were there, the place was packed with people, and at the
  overlooks people crowded against the fence shoulder to shoulder. It was a profound testimony to
  the fascination we all seem to have with waterfalls.</p>

<p>Niagara Falls themselves come in two parts. The American Falls flow over the edge of the gorge
  on the American side directly across the river from the Canadian park I described above. The
  American Falls crash down onto a large pile of imposing rocks. These rocks reduce the free fall
  height of the American falls considerably, but they also add quite a bit of interest to the
  falls.</p>

<p><a href="guest_photos/niagara3_large.jpg"> <img src="guest_photos/niagara3.jpg"
                                                   style="float: left; padding: 10px"
                                                   alt="Niagara Falls"/> </a> A bit farther
  upstream is the larger Horseshoe Falls. These falls are slightly lower than the American Falls,
  and they carry 90% of the water that flows over Niagara (although that fact is not evident from
  looking at the falls: both of them seem quite impressive). The Horseshoe Falls do not cut
  straight across the river but bend in a giant horseshoe shape. The lip of the falls is 2200 feet
  across with the river flowing nearly 40 mph as it spills over the edge. When the water crashes
  into the pool below, a cloud of mist is forced into the air well above the height of the falls
  themselves—perhaps extended as much as 300 feet above the lower level of the river. This is not
  something you see every day.</p>

<p>The fenced walks bring you right up to the lip of the Horseshoe where you can stand and look at
  the water rushing over the edge only a few feet away. I was impressed by the speed and power of
  the water and by its depth. It appeared to me that the river was still several feet deep even at
  the lip of the waterfall. This was no small stream trickling down a rock face.</p>

<p>Hillary and I did take in a few of the attractions of the area. We visited the falls at night
  and watched as different colored lights were shined on them. It was a bit odd-looking, but neat
  in a way. The next day, we rode to the top of the Skylon tower 520 feet above the city to view
  the falls from above. It was interesting to see them all in one sweeping view, but it also made
  it clear how low Niagara really is. A truly high waterfall would be taller than the Skylon
  tower.</p>

<p>Finally, we took an elevator down to a platform at the base of the Horseshoe Falls. We were
  given cheap plastic raincoats to wear as we stood below the falls and gawked up at it. It was
  good that we had the raincoats. The mist was so thick that it quickly soaked everything. Even
  with the raincoats we got rather wet. From there Hillary and I followed an underground tunnel to
  a couple of portals that looked out from behind the falls. The view was somewhat uninteresting.
  It was just a sheet of white water. What was more interesting to me was the noise. The very
  walls of the tunnel seemed to shake with a powerful throbbing as if some great engine was
  laboring nearby.</p>

<p>Hillary and I decided not to take the boat ride up to the falls. The <i>Maid of the Mist</i>
  takes people to the foot of the Horseshoe, and it looked a bit disconcerting to my daughter. I
  could understand that.</p>

<p>Niagara is interesting from a geological point of view. The falls are only about 15,000 years
  old. They came into existence at the end of the last ice age. At that time they were much
  farther downstream than they are today. During the last 15,000 years they have cut back into the
  riverbed 7 miles to reach their current location. Downstream from the falls is a long, deep
  gorge. Until recently the falls were cutting back at the rate of 3 feet per year. However, in an
  effort to stablize the falls (humans don't like natural changes, it seems), some modifications
  were made to the rock face over which the falls pour. Currently, they are cutting back at the
  rate of only 1 foot every 10 years.</p>

<p>It is in the nature of waterfalls to cut into their channel. What makes Niagara a bit unusual
  is the fact that the falls have remained vertical despite 15,000 years of erosion. Ordinarly
  falls tend to level themselves out, leaving behind only a gorge. However, the rock at Niagara is
  such that a hard layer rests on top of much softer rock. The backwash from the falls erodes the
  soft rock away, leaving the hard layer on top to extend out from the retreating cliff face.
  Eventually a chunk of the hard layer breaks off, and the falls move back a few feet. The process
  then repeats itself. The hard rock on top inhibits the normal leveling process and keeps the
  falls vertical despite the high erosion rate.</p>

<p>The appearance of Niagara Falls has changed over recorded history. My parents have photographs
  of the American Falls taken in 1952 that show distinct differences from its current appearance
  [I will try to get these photographs and scan them in for you to see what I mean]. Apparently,
  if left to its own devices, the Horseshoe Falls (the faster moving of the two) would cut off the
  American Falls and dry it up in only about 100 more years. It is rare to see such geologic
  changes in such a short time. I guess that's what 1.6 million gallons of water per second can do
  for you!</p>

<p>If you are into waterfalls, you should try to see Niagara at some time in your life. It is
  certainly worth visiting.</p>

