<h2>Directions</h2>

<p>US Route 5 goes through the center of East Barnet. In the middle of town, the Stevens River
  flows under the road. This waterfall is under and below that bridge, although the very top of
  the waterfall is visible from Route 5.</p>

<p>There is a side road that goes to the bottom of the falls. It connects with Route 5 immediately
  north of the bridge. The best overall views of the falls are from that side road.</p>

<hr/>
<h2>Description</h2>

<p>Stevens River Falls is in the middle of East Barnet and thus surrounded by houses. In addition,
  the Route 5 bridge goes right over the falls, and one pier of the bridge is anchored in the
  rocks immediately by the falls. A close approach to the waterfall is impossible without crossing
  what looks like private yards. This detracts considerably from the beauty of the falls.</p>

<p>The falls themselves pour over a crest of rock and flow along a kind of chute. They go over
  several steps before reaching the bottom of the chute where the stream flows through a narrow,
  steep walled cut. While not remarkable, the falls have the potential of being very nice.
  Unfortunately, they are not in the best setting. I can't recommend making a special trip to see
  these falls. However, if you happen to be going through Barnet, it would be worth swinging by to
  view them.</p>

