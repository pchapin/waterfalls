<h3>Tuesday, July 16, 1996</h3>
<p>
  <img src="photos/flumecas.jpg" style="float: left; padding: 10px" alt="Flume Cascades"/>
</p>

<p>Today my daughter Hillary and I spent the day in <b>Crawford Notch</b> enjoying waterfalls. We
  started with <b>Flume Cascades</b>. These falls are located right along Rt. 302 just a short
  distance south of the height of land in the notch. There was a parking area across the road from
  the falls making access easy. You do have to cross the busy highway to get to the falls, but
  that isn't much of a problem.</p>

<p>Flume Cascades tumbles down the side of <b>Mt. Jackson</b> in a long, straight fall. Due to
  heavy, extended rains, the lakes and rivers in New England were all unusually full. Thus, the
  small stream making up this cascade had a reasonable amount of water in it. I suspect that under
  more normal conditions, this is not a particularly spectacular waterfall.</p>

<p>It looked to us like it wouldn't have been too difficult to scramble up the side of the
  mountain alongside the stream. I bet there were some great views of Crawford Notch to be had
  from higher up. We didn't make the climb today, but perhaps we will another time.</p>

<p>I wouldn't make a trip just for this waterfall, but since it is so close to <a
    href="silver-cascades.html">Silver Cascades</a>, you should certainly plan to take it in when
  you visit those other falls.</p>

