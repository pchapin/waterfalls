<p>
  <i>The following information and photographs were sent to me by Shannon, a reader of this
    website. I thank her for this information!</i>
</p>

<p>Stepstone Falls is located in the northwest corner of RI's Arcadia State Park. Rocky trails
  lead hikers through rolling, densely forested hills. Small creeks and ponds dot the landscape.
  The tallest hills in the Park provide panoramic views that stretch into Connecticut. Arcadia has
  the best trails in RI for hikes, biking, horseback and bird-watching. Stepstone Falls is a
  popular highlight among visitors.</p>

<p>As these photos were taken, some trees were just starting to show the first tiny signs of
  impending buds. Blue sky &amp; sun fought to peer through high clouds. Very little sound could
  be heard over the thunder of the Falls River, thick with snow runoff. It runs with surprising
  power despite being only about 12" deep. A crossing might be attempted in one calm-looking spot,
  but only a foot downstream the current suddenly displays its true strength, cascading
  mercilessly over the biggest of the steps. Underestimation here might just be a bad thing.</p>

<p style="text-align: center;">
  <img src="guest_photos/Stepstone-1.jpg" alt="Stepstone Falls"/>
</p>

<p>The creekbed has been easily overtaken by this increase. Another contributing factor is the
  shallow angle descending into the water. One can stand several feet away from the water and
  still be level with it. Many additional little falls are created as side-channels reach over the
  stone riverbank surface.</p>

<p>This is an incredibly sharp contrast to the same photographer's last visit, during a drought.
  The river was shallow enough to be crossed easily without losing one's balance. Lunch was had on
  the same flat rock surface which today is part of the flow. Summer's gurgling sounds would be
  dwarfed by spring's high-voltage rushing.</p>

<p>Today a hiker definitely hears the falls long before approaching. Upstream the creek seems
  small, charming and babbling. Once it passes under the upper footbridge, the wildness releases
  over each of the falls like lawyers following an ambulance. The steps combine their individual
  musical pitches into a wonderful jet-engine symphony. Past the lower bridge, the
  suddenly widened river becomes downright serene. Looking at it brings an unexpected urge to find
  a canoe.</p>

<p>These different faces of the river provide countless opportunities for great photographs. Of
  course, right after both cameras were filled up, the sun came out and the photographer saw a
  shot she hadn't noticed before. She now looks forward to further afternoons spent watching and
  listening to the beautiful thunder over the steps. Maybe she can even capture some of the great
  photographs someday.</p>

<p>Information about the park can be found on the State Parks website: <a
    href="http://www.riparks.com"> <i>http://www.riparks.com/</i> </a>.</p>

