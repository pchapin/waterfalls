<h3>Thursday, July 18, 1996</h3>

<p>It was a beautiful day when I hiked up to <span class="waterfall-name">Katahdin Stream
        Falls</span>. The trail left<span class="other-name">Katahdin Stream Campground</span> on
  the southwest side of <span class="other-name">Mt. Katahdin</span>. Getting to the trail head is
  not difficult, but it does take time. Mt. Katahdin is located in <span
      class="other-name">
        <a href="http://www.mainerec.com/baxter1.html">Baxter State Park</a>,
      </span> which is in a relatively remote part of Maine. In addition, the park roads are very
  bumpy. Their speed limit is 20 mph, and if you value your vehicle I would suggest you observe
  that limit. As a result, it takes quite a bit of time to drive the eight miles or so from the
  park entrance at <span class="other-name">Tongue Pond</span> to Katahdin Stream Campground.</p>

<p>The trail to the falls is the <span class="trail-name">Hunt Trail</span>. This is a section of
  the <a href="http://www.fred.net/kathy/at.html">Appalachian Trail</a>. In fact, the northern end
  of the AT is on the summit of Mt. Katahdin so end-to-end hikers coming from the south see
  Katahdin Stream Falls as the last waterfall on their long journey.</p>

<p>Although the trail to the summit is five difficult miles, the trail to the falls is only about
  1.2 relatively easy miles. The first part of the trail runs alongside <span
      class="stream-name">Katahdin Stream</span> and climbs steadily but gradually. After about
  one mile, the trail begins climbing more steeply, crosses the stream, and shortly comes to the
  falls. The falls are right by the trail.</p>

<p><img src="photos/katahdin2.jpg" alt="From Trail" style="float: right; padding: 10px"/> There
  are many interesting vantage points to view these falls. I scrambled down the embankment below
  the trail some distance below the falls and got several interesting views (see the picture at
  the top of this page). It was impossible to scramble all the way down to the stream; it flowed
  through a narrow gorge with vertical walls some twenty feet high. I suppose it might have been
  possible to wade up the stream from below. Although that would have probably given a very
  interesting view of the falls, I didn't try it.</p>

<p>Farther up the trail, it is easy to climb right down to the rocks next to the main part of the
  falls. In fact, I was even able to climb out onto the rocks and sit only a few feet from the
  full force of the cascade! Even farther up the trail, you can climb out onto the rocks at the
  lip of the falls. From there you get a good view of the gorge below and the world beyond.</p>

<p>The trail then turned away from the falls and continued its ascent up the mountain. A large
  white "4 Miles" sign was painted on one of the rocks indicating the remaining distance to the
  summit.</p>

<p>I spent an hour at Katahdin Stream Falls lounging on the rocks and taking pictures.</p>

<p style="clear: right;">A very nice waterfall. Well worth the trip if you're in the area of
  Baxter State Park.</p>

