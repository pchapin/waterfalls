<h2>Wednesday, June 12, 1996</h2>

<p>There were thunderstorms this afternoon with very heavy rainfall just before and during my trip
  to these falls. Although I managed to avoid the worst of the rain while I was actually looking
  at the falls, I did have to sit in my car for a while waiting for the rain to lighten first.</p>

<p>Originally, I was intending to visit <b>Dunn Falls</b>. I followed <b>East Hill Road</b> out of
  <b>Andover</b> for several miles. The road parallels the <b>West Branch of the Ellis River</b>
  for most of the trip. Finally, it climbs to a notch where the Appalachian Trail crosses the
  road. The trail crossing is not marked and very easy to miss. However, the classic white blazes
  are visible from the car if you are looking for them and drive slowly. (Note: The trail crossing
  occurs somewhat below the height of land)</p>

<p>The trail descends an embankment and then crosses an "unnamed stream" on its way to Dunn Falls.
  However, today, due to all the heavy rains, that little stream was swollen to huge proportions.
  There was no bridge and there was no way I could have crossed it without special equipment.</p>

<p>However, a blue-blazed trail marked as the "<b>Cascade Trail</b>" followed the stream downhill.
  The sign indicated that it reached "the falls" after some distance, suggesting that it doubled
  back in some way or that perhaps there were some other falls below.</p>

<p>I followed the Cascade Trail and was richly rewarded. The stream, filled to capacity with wild
  runoff, raged at my side. Although there were no large falls on the stream, there were many
  cascades, some significant and all impressive -- today at least. The water was brown with
  sediment and the noise was at times deafening.</p>

<p>The trail itself was a very pleasant walk through the woods. Today lightning cracked and
  flashed overhead, and significant streams of water poured across the trail from the heights
  above, giving the walk a wild, exciting feeling. I expect that in more subdued times, it would
  be much more peaceful.</p>

<p>Finally, after perhaps a half-mile, the trail crossed the stream. Again, the crossing was
  impossible, so I had to retrace my steps back to my car. Although I didn't get to see Dunn
  Falls, I was still quite satisfied with what I found. In calmer weather, this would be a nice
  spot to explore further.</p>

