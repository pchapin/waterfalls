<h3>Tuesday, July 16, 1996</h3>
<p>
  <img src="photos/coliseum2.jpg" width="350" style="float: right; padding: 10px"
       alt="Coliseum Falls"/>
</p>

<p>Today Hillary and I spent the day in Crawford Notch enjoying waterfalls. After stopping by <a
    href="flumecas.html">Flume Cascades</a> and <a href="silver-cascades.html">Silver Cascades</a>,
  we took a hike along Bemis Brook to visit Coliseum Falls and <a
      href="bemis.html">Bemis Brook Falls</a>. Bemis Brook also flows over <a
      href="arethusa.html">Arethusa Falls</a>, the highest in New Hampshire (New England?), but we
  didn't feel like following the trail that far upstream.</p>

<p>To get to the falls, start down the well-marked <i>Arethusa Falls Trail</i>. After a very short
  distance, the <i>Bemis Brook Trail</i> diverges to the left. The trail then follows along Bemis
  Brook and shortly comes to Coliseum Falls. There Bemis Brook flows over a low, but very nice
  waterfall and then slides down a wide expanse of flat rocks. The rocks below the falls are in
  some ways the most interesting part. There you can easily walk next to the brook as it surges
  along the left side of the channel.</p>

<p>A very pleasant waterfall. The rocks below the falls would make a good picnic spot.</p>

