<h1>Nancy Cascades</h1>

<h3>Saturday, August 12, 1995</h3>

<p>The weather report threatened rain and thunderstorms today. However, it didn't actually look
  all that bad, so I decided to try my luck anyway. As it turned out, I only ran into a few
  sprinkles on the trail. Nevertheless, the humidity was very high and that made hiking a sweaty
  affair.</p>

<p>Nancy's Cascades are located 2.2 miles along the <b>Nancy Pond Trail</b> from Rt. 302. The
  trail head is located a few miles south of <b>Crawford Notch</b> and was not especially
  well-marked. However, the number of cars there today made it easy to spot.</p>

<p>The trail climbs a total of 1500 feet up to the falls. Most of the time it follows <b>Nancy
  Brook</b>. Sometimes the brook is easy to see and other times you can really only just hear it.
  The trail crosses the brook once and then again just below the falls.</p>

<p>The falls themselves were quite impressive. Your initial view is of Nancy Brook spilling over a
  steep rock face maybe 70 feet high. The stream pours down into a small pool and then ambles away
  from the rock face as an ordinary-looking mountain stream. I climbed out onto the rocks that
  were along the pool just opposite to the falls. There was a cool breeze blowing down from the
  falls; no doubt the moving water had set up a steady draft. It was a very nice resting spot.</p>

<p>The trail then continues climbing up the steep face by way of switchbacks. After the first
  switchback, the trail returns to the stream at a point above the top of the lower falls. There
  you get an impressive view of the stream flowing down a steep, rocky shute and into a little
  pool just above the lip of the lower falls. You also get a nice view out into the valley
  below.</p>

<p>It's clear that Nancy Cascades is really a large complex of falls and cascades. However, the
  trail does not return to the stream again until it reaches the very top of the steep slope some
  several hundred feet above the foot of the falls. From the upper location there is another nice
  view of the valley below (farther below now!). To really see all of Nancy Cascades, you would
  have to bushwack up the side of the mountain right alongside of Nancy Brook. The extremely steep
  grade and the thick spruce forest would combine to make this a very difficult climb. In any
  case, hiking off the trails in the <b>White Mountain National Forest</b> is discouraged because
  of the high use the area gets from hikers.</p>

<p>I went on another 1.0 miles from the falls (and 500 more feet of climbing) to see what <b>Nancy
  Pond</b> was like. I wasn't terribly impressed. Nancy Pond is a secluded mountain pond, but it
  didn't seem very special to me. There were no nice views (and no good places to sit). If you're
  into mountain ponds, you should check it out, otherwise don't bother. I will admit, however,
  that it looked like a good place to find moose. I didn't see any today.</p>

<p>The guide books do say that the spruce forest above Nancy Cascades is a virgin forest. I'm not
  enough of a forest person (yet) to have noticed anything special about it. The trees were not
  particularly large, although I did notice that they came in all sizes. I know that land that has
  been logged recently often has trees that are all the same age.</p>

<p>The water in Nancy Brook was brownish. At first, I thought this might have been because of the
  rain we had last night. However, the color was more noticeable the higher I got. I wonder if the
  spruce forest at the top of the stream's watershed had anything to do with it.</p>

<p>All in all, I liked Nancy Cascades, and I wouldn't hesitate to recommend it to someone who
  wasn't afraid of a little hiking.</p>

