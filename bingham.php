<h3>Sunday, April 1, 1995</h3>

<p>Bingham Falls is located in <b>Stowe</b> just off Rt 108 about a half-mile North of the <b>Mt
  Mansfield</b> auto road. There are no signs. There is a dirt turn-off just before Rt 108 crosses
  a small bridge. From the turnoff, there is an unmarked trail that goes down about a quarter mile
  to the falls.</p>

<p>The trail comes to the river above the falls. There the river flows in a narrow serpentine
  channel. It looks like you could almost jump across the gorge in a couple of places, yet the
  gorge is about 20 feet deep. There are lots of round potholes in the gorge that have been carved
  out by the water. However, it's difficult to actually see the stream. Of course, the ice and
  snow that was still around today made it tricky to get close to the edge of the gorge!</p>

<p>The trail becomes very rough and follows the river downstream a bit. You have to scramble down
  a steep embankment using your hands to hold onto roots and stones. The stream flows out of its
  narrow gorge and over a very beautiful waterfall into a large circular pool. The main falls are
  about 15 to 20 feet. After a lot of scrambling, you can get to the rocks at the outlet of the
  pool where the view of the falls is excellent. You can feel the spray against your face.</p>

<hr/>
<h3>Sunday, May 28, 1995</h3>
<p>Today I visited Bingham Falls again with my friend John. There was no ice around this time and
  that was good. There were also more people around.</p>

<p>I spent a little more time today checking out the narrow gorge above the main falls. Since I
  didn't have to worry as much about slipping to my doom, I was able to get some better views of
  the stream as it wound its way among the rocks. The very first gorge that the stream goes
  through is quite interesting because of all the potholes ground into the rock one right after
  another.</p>

<p>John and I scrambled down the embankment for a look at the main falls as well. It was even more
  impressive than I had remembered. Very nice falls with a beautiful pool. <a
      href="photos/bingham.jpg">Picture</a> [111k].</p>

