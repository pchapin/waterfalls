<h1>Waterfall Summaries</h1>
<p>Most of my waterfall descriptions have a summary listing at the top of each description. My
  intention is to digest all the significant facts about each waterfall into that summary so that
  you can tell at a glance if you want to read the entire description or not. What facts about a
  waterfall are significant? That depends on what you want to know. Here is a list of the summary
  headers that I use and what they mean. Not all falls will have all headers.</p>

<dl>
  <dt>
    <b>Overview</b>
  </dt>
  <dd><p>This is the summary of the summary. In this one line I will try to give you a feeling for
    the waterfall. Is it large and impressive, or is it small and friendly? Is it wild and remote,
    or is it something you can bring your sick grandmother to see? Here is where you will find
    your answer.</p></dd>

  <dt>
    <b>Location</b>
  </dt>
  <dd><p>The town and state where the falls are located. I make an effort to be accurate about the
    location. I avoid using the names of crossroads or nearby tourist attractions. Some of the
    towns which contain these waterfalls are small, and I try to be respectful of them by
    acknowledging them properly. For example, there is no "Killington, VT." The Killington Ski
    Resort is located in the town of Sherburne. However, some places, notablely central and
    northern Maine, don't have towns in the usual sense of the word. In those cases I may identify
    a location using nearby natural landmarks. If a waterfall is located inside the borders of a
    state park or similar place, I may indicate its location in terms of that. Such parks tend to
    be more widely known and recognized than small towns, and they are (usually) still respectful
    to the natural forces I am trying to celebrate with this page.</p></dd>

  <dt>
    <b>Stream</b>
  </dt>
  <dd><p>The name of the stream that flows over the falls.</p></dd>

  <dt>
    <b>Height</b>
  </dt>
  <dd><p>The height of the falls. Mostly these numbers are my estimates. In some cases, I might
    have actual facts to back these numbers up. Measuring the height of a waterfall is difficult
    because it isn't always clear where a waterfall starts and stops.</p></dd>

  <dt>
    <b>Finding</b>
  </dt>
  <dd><p>This is my estimate of how difficult it is to find the waterfall. I strongly suggest that
    if you are interested in waterfalls, you get a good road atlas that shows <em>all</em> of the
    back roads in every town. Check out my <a
        href="references.html">references</a> page for some ideas. However, even with a good map,
    some falls are difficult to find. If I say "difficult" in this header, that means use a
    topographic map and a compass. If I say "easy" I may mean "easy with a good map." Some falls,
    however, are very trivial to find because they are so well-marked. I will try to make that
    clear in this header if that is true.</p></dd>

  <dt>
    <b>Access</b>
  </dt>
  <dd><p>This is my estimate of how difficult it is to get to the waterfall. This is different
    from finding it. Some falls are easy to find but hard to reach. If you are out of shape and
    don't want to walk a long way to see the waterfall, pay attention to this header. I will try
    to distinguish between falls that are a short walk, but that still requires some climbing,
    from those that are literally wheelchair-accessible. If the falls require hiking, as many do,
    I will try to indicate how difficult a hike it is. Keep in mind that an "easy" hike is much
    harder than a "short walk, no climbing" rating. As soon as hiking is involved, my rating
    system changes to that which is appropriate for hikes.</p></dd>

  <dt>
    <b>Basking</b>
  </dt>
  <dd><p>Do you just like to sit beside a waterfall and read? Do you like to stretch out on the
    rocks and sunbathe? If you do, then check out the "basking" header. Here is where I try to
    give you a sense of how easy it is to relax around the waterfall. At some falls, it's hard to
    find a place to sit down. At others, there is plenty of space to spread out a blanket and have
    a picnic on the rocks beside the stream.</p></dd>

  <dt>
    <b>Privacy</b>
  </dt>
  <dd><p>This is my assessment (often based on just one visit) of how private a falls is. Some
    waterfalls are very popular spots that get visited by many people. Other waterfalls are
    obscure or remote and are rarely visited. Do you like to be alone with a waterfall? Do you
    want to sunbathe nude on the rocks or go skinny-dipping? This header can provide you with some
    idea of which falls are secluded and which ones are not. Keep in mind that waterfalls attract
    visitors. No matter how private a waterfall might seem, there is always a chance that your
    solitude will be interrupted without warning by a troop of people. Making love beside a
    roaring waterfall might sound romantic, but I don't think I'd recommend it regardless of how
    secluded a fall seems unless you are prepared to cope with interruption!</p></dd>

  <dt>
    <b>Legal</b>
  </dt>
  <dd><p>Some waterfalls are on private land. In some cases the landowners may <em>not</em> want
    their land displayed and discussed on the Internet for all to see. I want to be respectful of
    that. A vast majority of the waterfalls described on this site are on public land or on land
    explicitly made publicly accessible. If I do display a waterfall on private land, I will make
    every effort to either not reveal it's exact location or to have the permission of the
    landowner to publish information about the falls here on the Internet. <em>Please let me know
      if I am publishing information about falls on private land inappropriately!</em>
  </p></dd>
</dl>

