<h3>Monday, August 14, 1995</h3>
<p>These falls were located just off Rt 26 in <b>Grafton Notch</b>. About a half-mile south of
  Grafton Notch State Park and immediately south of the bridge over <b>Wight Brook</b> there was
  an inconspicuous gravel driveway on the east side of the road. A few yards from the road, there
  was a parking area nestled under some pine trees.</p>

<p>Step Falls was located on land owned by the <b>Nature Conservancy</b> (I'm a member!). The
  Conservancy had a couple of signs next to the parking area, so if you see them, you know you're
  in the right spot. Keep in mind that camping and fires are not allowed at this site.</p>

<p>From the parking area, there was a reasonably well-marked trail that went to the falls. It was
  about a 0.5-mile walk over generally easy grades. The only real climbing occurred once I reached
  the falls. The trail roughly followed Wight Brook for a while and ran right alongside the brook
  as it approached the falls.</p>

<p><img src="photos/step2.jpg" style="float: right; padding: 10px" alt="Step Falls"/> The falls
  themselves were fascinating! As with <a href="glendale.html">Glendale Falls</a> in
  Massachusetts, Step Falls was really a long sequence of short falls and cascades that flowed
  over a wide expanse of rock. The water was quite low today, and so I was able to walk out onto
  the rocks and hop across the stream with ease. There were so many different interesting little
  spots, I could never fully describe the falls here!</p>

<p>I was impressed by the size and depth of some pools. The rock has been well carved by the
  flowing water; some pools must have been ten feet deep (and only perhaps five feet across!). In
  many places the rocks were polished smooth and had grooves and pockets that were dry today but
  clearly wet during high water. All in all, it was a fascinating waterfall. Highly
  recommended.</p>

<p>If you are in the area of Step Falls, be sure to also check out <a href="screwauger1.html"
>Screw Auger Falls</a> just a couple of miles farther up into Grafton Notch.</p>

