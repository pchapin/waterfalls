<h2>Notes</h2>
<dl>
  <dt><b>May 2016</b></dt>

  <dd><p><i>Peter Chapin</i>. Today I visited these falls for the first time. They were indeed
    very lovely! The river was quite full due to the early season making for a spectacular scene
    (see the picture at the top of this page). I was less impressed with the lower falls, although
    taken by themselves they would have been somewhat interesting. The water was very cold today
    and nobody was swimming.</p></dd>

  <dt><b>January 16, 1999</b></dt>

  <dd><p><i>Jon Binder</i>. I was in the area, and decided the take the short detour to check out
    Split Rock falls in the winter. There was a lot of ice around, yet the falls were not actually
    frozen. The cascade at the bottom flowed over ice. It was interesting, but not that pretty.
    Also, it was slippery walking along the ravine, so I opted to return safely to the car.</p>
  </dd>

  <dt><b>October 3, 1998</b></dt>

  <dd><p><i>Jon Binder</i>. I visited the falls on the way back from a hike up Cascade and Porter
    Mountains. I wanted to see the falls with some color around. No one else was there, and the
    falls had a good amount of water flowing. It was late in the afternoon, so the light was
    coming in beautifully; that along with the red leaves on the ground provided for a good
    picture.</p>
  </dd>

  <dt><b>August 28, 1998</b></dt>

  <dd><p><i>Jon Binder</i>. I was on my way to an overnight trip in the High Peaks. I remembered
    Split Rock Falls was nearby, and because it wasn't a very hot or sunny day, I hoped the falls
    wouldn't be too crowded. I climbed down to the base of the cascade to find two men drying off
    next to the pool. They said how they liked to visit the falls on cloudy days since fewer
    people liked to swim then. I went for a swim, enjoying the solitude and the cold water! When I
    was walking back to the car, there was a kid jumping from the side of the ravine into the
    water between the main falls and the lower cascade. The water can be very strong here, so it's
    not something I would recommend. There's no way to tell how deep the water is, and you could
    hit a rock on the way down, or be pulled over the cascade by the current. In fact, not long
    ago a man drowned when he jumped in the swift, high water after a thunderstorm. It's safe to
    swim in the pool at the bottom, and probably more pleasant too.</p></dd>

  <dt><b>August 14, 1998</b></dt>

  <dd><p><i>Jon Binder</i>. Today I was looking at a contour map of the area, and noticed Split
    Rock Falls just a short distance from where I was. I had heard of the falls before, and had
    even seen a postcard of it in a store somewhere, but I didn't know where it was. I decided to
    try and locate it using the map. To my surprise, I easily found the falls, knowing I was at
    the right place because of the parking area on the right. There were about 4 cars parked here
    today. I followed one of the paths from the parking lot along the edge of the ravine, passing
    a fence at one point (which is meant to discourage people from getting to close to the edge or
    attempting to climb out onto the cliff next to the falls), and climbed steeply down to the
    base of the falls. There was a family swimming in the large pool, and a boy playing on the
    lower cascade (I wouldn't recommend that, it has to be very slippery, and a fall could mean
    hitting your head on the rocks). A woman was perched up on the rock next to the falls,
    ignoring the fence. The water wasn't too high, it was late, and the crowd was discouraging, so
    I left quickly. I was happy to find the falls, so I will be back very soon.
  </p></dd>
</dl>

<p>Overall, Split Rock Falls is beautiful and easily accessible. It's a great swimming spot, but
  pick a day to go when it's somewhat cloudy if you don't want a crowd.</p>

