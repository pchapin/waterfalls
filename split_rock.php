<h2>Directions</h2>
<p><b>From I-87</b></p>

<p>Split Rock Falls is easy to reach. From exit 30 on the Adirondack Northway (I-87) go northwest
  on NY route 9/73 toward Lake Placid for 2.4 miles (3.8 km) to a large, strange, but well-marked
  intersection. Take a right, continuing north on route 9 to a small bridge over the Boquet River
  just above Split Rock Falls. This point is 2.3 miles (3.7 km) from route 73 and 4.7 miles (7.5
  km) from the Northway (Coming from the north, the bridge is 3.9 miles (6.2 km) south of the New
  Russia post office).</p>

<p>Just over the bridge, coming from the south, there is a parking lot on the east (right) side of
  the road. No Parking signs surround the parking area on both sides of the road for some
  distance, so be aware that if the small lot is full (as it might be on a hot summer day) you may
  have to walk a distance to reach the falls. From the parking lot take one of several unmarked
  paths that lead along the edge of the steep-sided ravine and steeply down to the base of the
  falls. The distance from the parking area to the falls can be measured in feet. Explore the
  paths for different views of the falls and be sure to get to the very bottom, especially if you
  want to swim. Because it is so close to the road, the falls are a popular swimming spot and a
  good place to stop on the way to or from a day's hike in the nearby High Peaks.</p>

<hr/>
<h2>Description</h2>

<p>Split Rock Falls is a very pretty spot. In high water the falls can be very powerful. The main
  falls consists of two drops, each about 15 feet in height. The upper drop is split in two by a
  mass of rock, hence the name. The two upper drops collect in a mid-level pool before dropping
  straight down another 15 feet over the lower drop. From here water flows through a steep, rocky
  ravine for about 50 yards, before making a beautiful cascade over little stones into a large,
  deep pool. The pool at the bottom is great for swimming. It's beautiful, deep (maybe up to 8
  feet in the middle), and sometimes fairly crowded. Although the falls are so close to the road,
  only from the main falls is a roadsign and the bridge just barely visible.</p>

