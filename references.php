<h1>Printed References</h1>
<hr/>

<p>There are a number of books and guidebooks published that focus on just waterfalls. Gregory
  Plumb has compiled a <a href="http://www.mymaps.com/h2ofalls.htm">bibliography</a> of such
  guidebooks. His list covers waterfalls from all over the United States. If you are looking for
  on-line references, check out my <a href="other.html">Other Links</a> page.</p>

<p>The following printed materials might be of interest to any waterfall enthusiast focusing on
  the New England region (New England, and bordering New York and Québec).</p>

<ul>
  <li><p><b><i>New England Waterfalls</i></b> (third edition) by Greg Parsons &amp; Kate
      B. Watson. <a href="http://www.countrymanpress.com">The Countryman Press</a>, &copy; 2019.
      ISBN 978-1-68268-118-3 (<a
          href="https://www.amazon.com/New-England-Waterfalls-Guide-Cascades/dp/1682681181/">On
        Amazon</a>). This is an excellent book for anyone interested in the waterfalls of New
      England. It has a scope and style similar to this website, although it contains many falls
      not covered by me here. The book has summary information, descriptions, and directions
      (including GPS coordinates) for all falls covered as well as many pictures. Highly
      recommended.</p></li>

  <li><p><b><i>Hiking Waterfalls in New England</i></b> by Eli Burakian. <a
          href="http://falcon.com/">Falcon Guides</a>, &copy; 2022. ISBN 978-1493063604 (<a
          href="https://www.amazon.com/Hiking-Waterfalls-New-England-Waterfall-dp-149306360X/dp/149306360X">On
        Amazon</a>). Although this book covers many of the same falls as the book above, it
      emphasizes, as the title suggests, falls that requiring hiking to reach. The book contains
      maps for each of the falls and information about reaching them that are relevant for hikers.
    </p></li>

  <li><p><b><i>Hiking Waterfalls Maine</i></b> by Greg Westrich. <a
          href="http://falcon.com/">Falcon Guides</a>, &copy; 2020. ISBN 978-1493041916 (<a
          href="https://www.amazon.com/Hiking-Waterfalls-Maine-States-Waterfall/dp/1493041916/">Amazon</a>).
      As you can surmise from the title, this book focuses on Maine waterfalls that can be reached
      by hikers.</p></li>

  <li><p><b><i>Hiking Waterfalls New York</i></b> by Randi Minetor. <a
          href="http://falcon.com/">Falcon Guides</a>, &copy; 2025. ISBN 978-1493079483 (<a
          href="https://www.amazon.com/Hiking-Waterfalls-New-York-Waterfall-dp-1493079484/dp/1493079484">On
        Amazon</a>). This book is in a similar style to the other Falcon Guides mentioned above.
      In fact, there are Falcon Guides for waterfall hikes in many states.</p>
  </li>

  <li><p><b><i>Vermont Waterfalls</i></b> by Russell Dunn. <a
          href="http://www.countrymanpress.com">The Countryman Press</a>, &copy; 2015. ISBN
      978-1-58157-240-7 (<a
          href="https://www.amazon.com/Vermont-Waterfalls-Russell-Dunn/dp/1581572409/">On
        Amazon</a>). This excellent book by fellow waterfall enthusiast Russell Dunn focuses on
      Vermont. It is organized with a similar style to this website, although it contains
      interesting historical information about each fall.</p></li>

  <li><p><b><i>Waterfalls of the White Mountains</i></b> (Second Edition) by Bruce, Doreen, and
      Daniel Bolnick. Backcountry Publications, &copy; 1999. ISBN 0-88150-464-5. This book is a
      must on the shelf of any New England region waterfall enthusiast. The authors give detailed
      descriptions of about 100 waterfalls in the White Mountain area of New Hampshire. Some of
      the falls described are near roads, but others require considerable hiking to reach. The
      book contains not only directions and descriptions of the falls, but also includes
      historical information about many of the falls as well.</p></li>

  <li><p><b><i>Make A Splash</i></b> by Jason Minor. Master Studios, &copy; (date unknown).
      ISBN=0-9663556-0-1. This book primarily focuses on swimming holes in Vermont. However, in
      the process the author talks about many interesting Vermont waterfalls as well.</p></li>

  <li><p><b><i>A Guide to Natural Places in the Berkshire Hills</i></b> by Ren&eacute;
      Laubach. Berkshire House, &copy; 1992. ISBN 0-936399-25-2. This book describes a number of
      interesting natural areas in Berkshire County, Massachusetts. Several waterfalls are
      described: <a href="campbell.html">Campbell Falls</a>, <a href="bashbish.html">Bash Bish
        Falls</a>, <a href="wahconah.html">Wahconah Falls</a>, and <a
          href="wjambs.html">Windsor Jambs</a>. For each area described, the author focuses on the
      entire ecosystem of that area. For example, he describes the plant and animal communities to
      be found there. This broad treatment puts a nice perspective on the falls.</p></li>

  <li><p><b><i>AMC White Mountain Guide</i></b> by the <a
          href="http://www.outdoors.org">Appalachian Mountain Club</a>; published by the AMC, 29th
      edition &copy; 2012. This book contains a complete description of the trails in the White
      Mountain Area. It is probably the most authoritative guide available to the extensive White
      Mountain trail system. Many waterfalls are mentioned in the trail descriptions. If you plan
      to hike in the White Mountains, you need this book.</p></li>

  <li><p>The Adirondack Mountain Club's <b><i>Guide to Adirondack Trails</i></b> comes in seven
      volumes to cover the entire Adirondack region. In addition to being one of the most
      authoritative sources of information about hiking in the Adirondacks, these guides also
      describe many waterfalls in the area. Don't travel in the Adirondacks without having the
      proper ADK guide on hand!</p></li>

  <li><p>Waterfalls of New York State by Scott A. Ensminger, David J. Schryver, and Edward M. 
      Smathers. Firefly Books, &copy; 2012. ISBN 978-1554079865 (<a
          href="https://www.amazon.com/Waterfalls-York-State-Scott-Ensminger/dp/1554079861/">Amazon</a>).
      This book contains many wonderful photographs along with descriptive text about more than
      100 waterfalls around New York.</p></li>

  <li><p><b><i>Waterfalls of the Adirondacks and Catskills</i></b> by Derek Doeffinger and Keith
      Boas. McBrooks Press, &copy; ISBN 0-935526-62-5. This is picture book that contains many
      fine photographs of waterfalls in New York. Each photograph is accompanied by a few
      paragraphs of text describing the falls or commenting on waterfalls in general. Directions
      to the falls are not generally provided. The introduction contains some overview material on
      the geology of waterfalls in the Adirondacks and Catskills mountains.</p></li>
</ul>

<p>Before venturing out into the country looking for obscure waterfalls, you will want to acquire
  some good maps. Today it is common for people to use smartphone apps for road and trail
  navigation. Many such apps are available; I do not attempt to list or review them here (at this
  time). However, good paper maps are still recommended as backup in cases where phone service is
  not available, your phone battery discharges, or your phone is damaged or lost.</p>

<p>Standard road maps typically only show the major routes. While useful for getting to an area,
  they are not usually detailed enough to make the final approach to a waterfall. The best maps to
  get are from the USGS's topographic series. However, topographic maps are expensive&mdash;especially
  if you want to cover a large area with them.</p>

<p>I have found the <a href="https://buy.garmin.com/en-US/US/p/575993">DeLorme Maps</a> to be an
  excellent substitute for USGS topographic maps. DeLorme maps can be purchased for every state.
  In addition to providing complete coverage of back roads, these maps also provide listings of
  various natural features, recreation areas, historic sites, and so forth. Some (not all) include
  information on waterfalls as well. Be aware, however, that in some cases, the markings on the
  DeLorme maps are off. There are instances where the location marked for a waterfall is
  significantly far away from the actual location.</p>

