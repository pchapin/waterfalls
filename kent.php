<h3>Saturday, April 26, 1997</h3>
<p>My friend John and I were doing some genealogical research in western Massachusetts today. When
  we were finished, I convinced him to take a side trip with me into Connecticut to look at these
  falls. This is the first Connecticut waterfall I've seen for this website, and I felt like I was
  overdue in adding Connecticut to my New England Waterfalls page.</p>

<p>Locating these falls was simple. <b>Kent Falls State Park</b> is right along US Rt 7 between
  the towns of <b>Cornwall Bridge</b> and<b>Kent</b>. There were many signs marking the way, and
  the entrance to the falls was obvious. In fact, we could see the falls themselves from the
  highway.
</p>

<p>If you visit Kent Falls, do not expect a wilderness experience. There was a generous parking
  area, a large grassy field, and a paved trail that approached the falls. When John and I visited
  the falls today, there were quite a few people out enjoying the spring weather. There were
  people just basking on the grass. There were people throwing Frisbees back and forth. There were
  people sitting at the picnic tables (which had grills).</p>

<p>The falls were located at the back end of the lawn, and they were quite impressive. A
  reasonably large stream launched itself over three or four cascades with a total drop of perhaps
  60 feet. It was possible to climb up onto the rocks and sit beside the shallow pool at the foot
  of the second cascade. In fact, while John and I were there, quite a few people did just
  that.</p>

<p>There was also a rough trail that climbed through the woods up the bank beside the falls to
  what appeared to be an overlook above the top of the falls. The trail was officially closed when
  John and I visited today, so we did not explore it.</p>

<p>This was a very nice waterfall, but the highly groomed atmosphere of the park detracted, in my
  opinion, from the raw beauty of the falls.</p>

<hr/>
<h3>Saturday, January 24, 1998</h3>
<p>A reader of this page who is more familiar with these falls than I sent me a couple of
  corrections. First, he pointed out that the admission fee is only applicable during the summer
  months from Memorial Day to Labor Day. He wasn't sure if it was charged during the weekdays or
  not.</p>

<p>He also pointed out that because John and I didn't follow the trail to the upper portion of the
  falls, we missed "over half" of the view. Apparently there is quite a bit more farther up. I'll
  have to return this coming summer to explore these falls more fully.</p>

<hr/>
<h3>Monday, April 17, 2000</h3>
<p>A different reader of this page offered the following directions to an upper parking lot above
  the falls: <i>Heading south on Route. 7 take your first left (Carter Road) and another (quick)
    left on Duga Road. It is a dirt road that will take you to a parking lot atop the falls</i>.
  Thanks for the tip!</p>

