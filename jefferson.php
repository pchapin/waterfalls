<h2>Directions</h2>
<p><b>From Jeffersonville</b></p>

<p>Take Vermont Rt. 108 south toward Smuggler's Notch. A short distance from town, after the road
  begins to run alongside the river, look for Canyon Road forking to the left. Canyon road
  immediately dips downward, making the intersection a little tricky to notice. It occurs as the
  embankment on the right side of Rt. 108 (heading south) rises steeply upward. Also, be aware
  that another, minor road forks to the left immediately before Canyon Road and could easily be
  confused for Canyon Road.</p>

<p>Canyon Road almost immediately comes to the Grist Mill Covered Bridge. Right before crossing
  the bridge, there is a parking area on the right. This is the Brewster River Park, a public area
  where people swim in the river and bask on the rocks and grass. You can park there or,
  alternatively, cross the bridge and park along the road immediately on the other side.</p>

<p>The trail, officially called the Alden Bryan Brewster River Trail (ABBRT) starts on the north
  side of Canyon Road immediately after crossing the bridge. It is a wide, mostly level path.
  Follow the trail for maybe 0.5 miles until it ascends moderately to the top of a rise. On the
  right there is an obvious side trail that leads to an overlook where you can view down into the
  Brewster River Gorge and see the falls. Be aware that there are a couple of other side trails on
  the way to the falls. The correct one is short and obvious.</p>

<hr/>
<h2>Description</h2>

<p>The river flows through a very narrow gorge, and the only view is from almost directly above.
  The gorge is interesting, but it is difficult to get a good view of the falls. Despite this, it
  is still an enjoyable place to visit. The walk to the falls is not long nor difficult, yet gives
  one an opportunity to approach a natural waterfall through the woods. The trail never veers far
  from the stream.</p>

