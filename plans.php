<h1>Future Plans</h1>

<hr/>
<h2>New Material?</h2>

<p>It has been a long time since I've done much visiting of waterfalls. The rest of my life has
  kept me busy. I will be retiring shortly. When that happens, I hope to have more time for being
  out in the wilder part of the world (and for working on this website).</p>

<hr/>
<h2>Images</h2>

<p>After a several-year gap, I've finally gotten access to a scanner again. I can now scan photos
  that I've had for months (years!), but that have never appeared on this site. I can also rescan
  photos that are on this site, but that are of poor quality (largely due to my lousy
  post-processing technique). Look for steady improvements in the quality of the images on this
  site over the coming months. See the "<a href="new.html">What's New?</a>" page for the details.
</p>

<p>Unlike what I've done in the past, I intend to keep archival copies of any scans that I do so
  that I can post-process the images several different ways as my needs (and skills) change. I
  hope to write a document detailing my procedures for those who want to use similar techniques or
  who are just plain curious.</p>

<p>I'm still planning to eventually put together some portfolios of the more photogenic waterfalls
  or perhaps a waterfall "gallery" of some kind. I haven't worked out the details yet, but I think
  what I want will become clearer as I work with the new images/scans that I hope to make
  soon.</p>

<hr/>
<h2>Trip Planner</h2>

<p>Quite some time ago I started work on a <a href="trips.html">Waterfall Trips</a> page that
  summarizes some "prepackaged" trips (or tours) people might take. I still think that's a good
  idea, and I hope to extend that page. I'm also considering creating regional summaries of
  waterfalls in the manner of my <a href="NH/wmfalls.html">White Mountain Waterfalls</a> page.
  That way those of you interested in planning your own trips would have an easy way to choose
  falls based on location (rather than the alphabetical order that I use on my main list). It
  might be nice for me to create some sort of clickable map where you could find out information
  about falls based entirely on geographic location.</p>

<hr/>
<h2>Various Things</h2>

<p>In addition, I'd like to put some information on this page about water conservation issues and
  the natural history of waterfalls (biology, geology, and so forth). It would also be cool to
  include some stuff on the historical significance of New England waterfalls (water power playing
  an important role in New England mill towns). This is a major project, and I have no idea when
  I'll get to it.</p>

<p>Finally, I hope to dress up this site a bit by using newer technologies. I started this site in
  1995, and it still uses technologies that were current at that time! I've started doing this
  already with the addition of a site-wide style sheet for giving my pages a consistent look. I
  might also explore the use of frames in some of my menu-oriented pages, and I might see what I
  can do with JavaScript, XML, and other such technologies. I have been experimenting quite a bit
  with XML on my <a
      href="../Iapetus/">astronomical observation notebook</a> page, and I may be able to use some
  of the same techniques here.</p>

<p>Stay tuned for developments!</p>

