<h2>Directions</h2>
<p>
  <b>From Pawlet</b>
</p>
<p>Starting at the center of <span class="town-name">Pawlet</span> at the junction of routes 30
  and 133, drive north on <span class="road-name">route 30</span> for three or four miles, to the
  junction with route 153. Continue on route 30 for a short distance and turn left on <span
      class="road-name">Button Falls Road</span>. There is a bridge over the <span
      class="river-name">Mettawee River</span> a hundred yards or so down the road. Park well
  before the bridge (there is no parking in the immediate vicinity of the river).</p>

<hr/>
<h2>Description</h2>

<p>This is a spectacular waterfall, but it is on private land and very difficult to access. The
  only possible views are from the road where the falls are obscured by trees and private
  property. The land around the falls is heavily posted and fenced off with barbed wire.</p>

<p>The Mettawee River flows into a narrow gorge over a drop of 20 to 25 feet. The gorge turns
  shortly after the falls and goes under the Button Falls Road bridge.</p>

