<ol>
  <li>Although the oval pool is very deep early on, it gradually fills with gravel through the
    summer. Each spring, its depth is renewed from the spring thaw torrent off the mountain. I
    jumped into it late one August and was surprised to find my knees driven into my chest. The
    depth was about 4 ft, and I was grateful I didn't dive.
  </li>

  <li>These falls are usually crowded with local college students on warm summer weekends. That
    can be a good or bad thing depending on whether or not you are looking for a college crowd.
  </li>

  <li>I left out that there is a dramatic view to the south from the top of the falls. The stream
    side walls frame a majestic foothill of <a href="../../100highest/camel.html">Camel's Hump</a>
    Mountain.
  </li>

  <li>Unless you're part sea-lion, the water is too cold to endure until July. Even then, you are
    reminded of your gender when you plunge in.
  </li>

  <li>I'm no meteorologist. By trade, I usually estimate dimensions in angstroms. Outdoors, using
    feet, I tend to underestimate dimensions so I tried to estimate generously.
  </li>

</ol>

