<h3>Sunday, October 21, 2012</h3>

<p>Today my wife and I visited this location. It was the first time I've attempted to view the
  falls here. Following the description sent to me by John Roberts 16 years ago (below) we
  searched for the parking area. Unfortunately, no obvious parking spot could be found. Instead,
  in what appeared to be the vicinity of the falls, there were fences and no parking signs. All
  other areas appeared to be private property. Our conclusion is that these falls are no longer
  accessible.</p>

<p>A quick Internet search shows a <a
      href="http://www.swimmingholes.org/vt.html#BOLTON%20POTHOLES">notation on
    www.swimmingholes.org</a> that says the town of Bolton has banned parking at the location due
  to excessive illegal parking. So unfortunately, the popularity of this spot as a swimming hole
  has apparently been its demise.</p>

<h3>Description Received October 1, 1996</h3>
<p>
  <i>The following description and photographs were sent to me by John Roberts. I have not yet
    visited these falls, but may do so soon. Thanks, John, for the information!</i>
</p>

<p>From the west, say <b>Burlington</b>, take Rt 2 to <b>Bolton</b>. There is an intersection at
  Bolton where Rt 2 passes beneath Rt 89, the expressway. Turn left onto the paved road that
  parallels the stream. Within a quarter mile, the road inclines sharply and there are widened
  shoulders for parking.</p>

<p><img src="guest_photos/bolton2.gif" style="float: right; padding: 10px"
        alt="Bolton Potholes"/> From the parking spot, follow any of several paths to the stream.
  If you can ignore the almost ever-present litter, you will see God's playground. At the top of
  the falls is a narrow pool roughly 30 ft long, 15 ft wide, and 8 ft deep. The sides are solid
  granite and there are short falls at the upstream end. This pool empties into a 20 ft fall with
  an oval pool beneath. The oval pool is about 12 ft wide at its narrowest dimension. I swam here
  many times and can tell you that beneath the surface, the pool becomes very round and according
  to local legend, is 25 ft deep. I can't confirm this since I haven't found the bottom personally
  (claustrophobia usually takes hold before I'm 10 ft down).</p>

<p>Although this is enough to rank number one on my list of waterfalls, there's more. The oval
  pool empties like a pitcher over a 10 ft free fall into a 30 ft diameter round pothole. This
  pothole is 8 to 10 ft deep at a point near the inlet and is stone and gravel filled to 3 to 5 ft
  elsewhere. Local daredevils/idiots jump from the highest cliffs above to the deep spot. Despite
  the thrill, these episodes usually end with the water turning pink.</p>

<p>The outlet of the pothole pours over a kind of natural stone lip. The stream narrows and flows
  over steeply inclined bare granite to another short fall. If you're careful, by sitting on your
  hands (palms down) you can slide down this incline and off the falls below. As you slide off the
  falls, you have to push off to preserve your tailbone. The pool below is deep enough for this,
  although I haven't taken the time to measure it. One of my family's favorite distractions
  resides here. There are hundreds of two-inch minnows that have red-orange pectoral fins. If you
  stand still in the water, these minnows crowd around your feet and toes and nibble on them. It
  feels quite eerie.</p>

<p>
  <i>After sending me the information above, John followed up with some more points.</i>
</p>
