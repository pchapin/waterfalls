<h3>Late 1980's</h3>
<p>This waterfall is quite beautiful. However, it is on private property and the last information
  I had was that the property owner does <b>not</b> want any trespassers. My friend John took the
  picture above of me standing at the base of the falls sometime in the late 1980s (I believe). At
  that time the falls were more open and were visited by many people. I have not visited these
  falls for a long time, and I may not get a chance to do so again. However, I thought the
  Internet community might enjoy the picture at least, even if I can't reveal the exact location
  of this beauty.</p>

