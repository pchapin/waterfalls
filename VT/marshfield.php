<h2>Description</h2>
<p>Marshfield Falls is a long sloping cascade. The lower falls are wider and shorter, resembling in
  some ways a fan that spreads out over the rocks. The upper falls are longer and narrower. It
  flows through a steep wall channel with trees growing along and over it. In some respects this
  is really two separate waterfalls in one.</p>

<p>It's hard to say how large the falls complex really is. When standing on the bridge below the
  upper falls, one can see them stretching back quite some distance. It is easy to imagine that
  there might be more falls farther upstream.</p>

<p>While the falls themselves are quite interesting and easily visible from the road, there aren't
  many options for exploring around them. The falls seem to be surrounded by private property, and
  thus access to other sections of them is limited.</p>

