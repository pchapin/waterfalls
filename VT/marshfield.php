<h2>Directions</h2>
<p>
  <b>From Marshfield</b>
</p>
<p>In the center of <b>Marshfield</b>, turn from Route 2 onto the side road next to the Rainbow
  Bakery. This is a right turn if you are approaching from the south and a left turn if you are
  approaching from the north.</p>

<p>The side road soon crosses the <b>Winooski river</b> and in a short distance forks. Either fork
  will bring you to the falls in just a few feet. The right-hand fork crosses <b>Marshfield
    Brook</b> just below the lower section of the falls. The upper fork crosses the brook just
  below the upper section of the falls. You should definitely check out both sections.</p>

<p>I recommend parking by the lower crossing and just walking to the upper crossing. There is more
  parking along the lower road, and the distance from there to the upper falls is short.</p>
