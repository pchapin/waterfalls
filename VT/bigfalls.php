<h3>Saturday, October 26, 1996</h3>

<p>The weather was quite beautiful today. It was clear and sunny. It was cool, but not cold. By
  the time I got to the falls in the later part of the afternoon, the sun had already dipped
  behind the trees. The river was in shadow, but the sky was still a bright blue. It was really
  quite pretty.</p>

<p>Big Falls are located right along a side road in <b>Troy</b>, VT. They are marked in DeLorme's
  <a href="references.html">Vermont Atlas and Gazatter</a> (both the old and new editions). With
  the help of that atlas, they are easy to find. I approached the falls from the south. A few
  hundred yards before the falls themselves, the <b>Missisquoi River</b>, already a significant
  stream despite being many miles from its mouth, started to flow over and around many hard rocky
  outcropings. At the falls themselves, there is an obvious unmarked parking area. The place was
  clearly well visited; there was quite a bit of litter in the area, and it had the look of a
  major party spot.</p>

<p>The falls poured over a ledge of hard rock in one small step and two big steps. Then the river
  immediately funneled into a narrow gorge. On one side of the gorge there was a huge, imposing
  block of rock that towered above the river. It was easy to climb up on top of that rock to get
  an overview of the entire area. However, the best view of the falls was from a spot just beside
  them. When you get out of your car, walk toward the left. Avoid the rocks right along the river,
  but instead look for an unmarked trail leading through the woods and between a couple of rocky
  masses.</p>

<p>Actually, it was difficult to get a really good view of these falls. The top of the gorge was
  too high, and the spot beside the falls was not at the best vantage point. The best view would
  be from inside the gorge itself, but that would require swimming up the riverâ€”something that
  looked rather difficult to do.</p>

<p>Although these falls were not that high, there was quite a bit of water flowing over them. That
  made them fairly impressive anyway. Despite the litter, it was a beautiful spot.</p>

