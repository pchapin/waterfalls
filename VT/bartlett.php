<h3>Tuesday, February 20, 1996</h3>

<p>I visited these falls again to see what they looked like in the winter. They were surprisingly
  uninteresting! They were almost entirely covered with ice and so you could see very little. They
  just looked like a large ice-covered rock face and not like a waterfall at all. The pool below
  the falls was also covered with snowy ice, and so it looked like a field and not like a
  river.</p>

