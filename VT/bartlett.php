<h3>Saturday, April 22, 1995</h3>

<p>The <b>New Haven River</b> flows down from Lincoln, VT and through <b>Bristol</b>, VT. Right
  where the road to <b>Lincoln</b> meets Rt. 17 the river passes over some interesting rock
  structures.</p>

<p>There are actually three areas of interest. Right at the junction of the roads mentioned above,
  the river flows around and about some large boulders, creating quite a torrent. A quarter of a
  mile or so up the road to Lincoln, the river flows over some wide, flat rocks that spread it out
  into a thin, fast-moving sheet.</p>

<p>However, between these two areas, the river goes over a very substaintial fall. The falls
  probably have a 20-foot drop. Although the drop is not completely abrupt, it is very steep and
  the river is totally white over the entire falls. At the base of the falls, the river becomes a
  long, narrow, and deep pool with steep rock ledges on the sides. There were several people there
  today fishing in that pool.</p>

<p>The falls are quite spectacular. The New Haven River is a much larger stream than I usually see
  going over a waterfall. Most rivers this size have eroded a fairly even channel for themselves.
  The sound and energy of all that water going over such an abrupt drop is intense. Also, it is
  possible to climb on the rocks right up close to the falls and get a personal look at the water.
  Intense.</p>

<p>You can walk to all three of the areas I mentioned above from any one of them. Since the river
  flows along the road to Lincoln, you just walk down the road. Alas, because the river is so
  close to the road, there is also a fair amount of litter in the areaâ€”at least by Vermont
  standards.</p>

