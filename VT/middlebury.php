<h3>Saturday, April 5, 1997</h3>
<p>I'm not sure if I have the name of this waterfall correct or even if it has an official name.
  I've heard it just called "the falls" by <b>Middlebury</b> residents. This waterfall is located
  right in the center of town and is thus an example of an <a href="urban.html"
  >urban waterfall</a>. In fact, this waterfall was used for many years to provide power to a
  significant waterworks. This is very typical for New England towns. However, the falls in
  Middlebury are larger and nicer than average. They are definitely worth checking out if you are
  driving through the area.</p>

<p>My first introduction to these falls was given to me by a woman friend of mine named Kim. As a
  resident of Middlebury, she was very familiar with the falls. She showed me the basic layout of
  the area around the falls last year, but at the time I did not have my camera with me. I
  returned to Middlebury today to take some pictures and to make my visit to these falls
  official.</p>

<p>I parked in downtown Middlebury along the Green and then walked over the bridge on route 125.
  Immediately over the bridge on the west side of the <b>Otter Creek</b> there is a small,
  alley-like road that goes steeply downhill. I followed that road behind the buildings that line
  the street to a small, rather pleasant spot. Nestled in that nook was a deck that overlooked the
  falls, from which I was able to get a very good view.</p>

<p>I then followed the narrow road farther downhill and soon came to a foot bridge that crossed
  over Otter Creek below the falls. From there a trail climbed up the other bank to a small park
  complete with picnic tables from which I could watch the falls (at a distance).</p>

<p>Do not be fooled by the name of Otter Creek. It is a rather sizable river. When I visited the
  falls today, The Creek was running high with spring run-off. The force and volume of the water
  pouring over the low, wide waterfall was quite impressive. It was a sunny day and altogether a
  very nice spot considering the urban location.</p>

