    <p>It has been a while since I last visited these falls (although not as long as it seems
      looking at the dates on my notes; I have not recorded every one of my visits). Today I came
      with my wife Sharon. We were looking for a nice way to spend the afternoon before I started
      the fall semester.</p>
    <p>The weather was very nice: cool, dry, and partly sunny. I was a little concerned that I
      would be too cold in the ravine with just a tee shirt, but it wasn't a problem. The water
      level in the stream was medium. There was enough water to give the falls a good flow, but
      the level was also low enough to make it easy to get around on the rocks.</p>
    <p>There was an unusually large number of people visiting the falls today. At least a dozen
      others were there, and the small parking area was almost full. I'm not sure why the spot was
      so popular today... unless it is common now for there to be so many people visiting these
      falls.</p>
    <p style="clear: left"></p>
  </dd>

  <dt>
    <b>Tuesday, July 8, 1997</b>
  </dt>
  <dd>
    <p>Today I came to these falls with my friend, Sue, her two children, my daughter, and my
      daughter's new puppy, Priscylla. The weather was excellent. It was cool and clear. In fact,
      it was almost too cool in the ravine by the falls. We splashed around in the water some and
      sat on the rocks below the lower falls. Priscylla dug in the sand.</p>
    <p>There was not a lot of water in the falls today. Although the stream was not nearly as dry
      as it was in the summer of 1995, the falls were not raging like they do in the spring. It
      was, as always, a very pleasant spot.</p>
  </dd>

  <dt>
    <b>Sunday, May 4, 1997</b>
  </dt>
  <dd>
    <p>Today Hillary, her friend Tory, and I all visited these falls. It was sunny, but
      unseasonably cool. The water was high and raging. We did climb up to the rocky platform to
      get a closer look, and the spray from the falls blew over us in a steady fog. We also
      scrambled over to a rocky spot near the bottom of the lower falls and sat there for a while
      too. The water was too high and too cold to do any wading in the stream, however.</p>
  </dd>

  <dt>
    <b>Thursday, July 11, 1996</b>
  </dt>
  <dd>
    <p>Today my daughter Hillary and I visited these falls. This year the water was running high.
      We were not able to wade in the pool beneath the falls; if we had tried to do so, we would
      have been carried away to our deaths. We did wade around in the stream a little below the
      falls, however. We sat on the rocks in the middle of the stream and had a few snacks.</p>
    <p>It was a beautiful day. There were several other people visiting the falls&#x2014;some to
      swim in the stream. On our way home, we passed by a person carrying a camera bag and a
      tripod. Another waterfall enthusiast, perhaps?</p>
  </dd>

  <dt>
    <b>Saturday, March 16, 1996</b>
  </dt>
  <dd>
    <p>Today my friend Kim and I visited these falls. The snow was melting, but there was still
      quite a bit around. The river was high, but not exceptionally so. We trudged down the trail
      through a few inches of soft snow. In the ravine itself there was very little snow. However,
      there was some very slippery ice. Walking was treacherous.</p>
    <p>The falls themselves were quite impressive. There was a snow-covered ice bridge over the
      top of the lower falls. The water appeared to pour out from under this bridge almost as if
      it was gushing out from underground. There were many interesting ice formations around the
      edge of the opening. We got a good look at them with the binoculars that we brought.</p>
    <p>I then climbed up to a rocky platform next to the upper part of the falls. It was very
      slippery and difficult. Kim wisely didn't risk her neck. I wanted a picture. It was worth it
      even though the picture didn't really come out very well. The main falls poured over a large
      tapered ice formation in a dramatic way. The ice almost looked like a curtain that the water
      was playing about and behind. I've seen some other falls in the winter, and they were too
      frozen up to be interesting. However, the view today was an excellent combination of open
      water and ice. It was a very interesting.</p>
  </dd>

  <dt>
    <b>Wednesday, July 12, 1995</b>
