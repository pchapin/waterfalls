<h2>Description</h2>
<p>
  <img src="photos/oldcity-03.jpg" style="float: right; padding: 10px" alt="Old City Falls"
  />
</p>
<p>Old City Falls is one of my favorite waterfalls. It is the closest significant waterfall to
  where I live (that I know about), and thus I visit it often. It is easily accessible, yet it
  retains a wild feeling that I enjoy. On hot summer days, it is always cool and comfortable in
  the ravine by the falls. My daughter and I have visited these falls several times in the hot
  months to picnic on the rocks and splash around in the stream.</p>

<p>At the parking area there is a small lawn with a few picnic tables and outhouse facilities. I
  noticed a couple of grills, but I'm not sure how usable they would be. To visit the falls
  themselves, you must follow a trail that starts at the back part of the lawn. It is marked with
  a green sign.</p>

<p>The trail is not blazed, but it is obvious. It follows the top edge of the ravine for a while
  and then turns to descend toward the stream. An unmarked trail continues along the ravine after
  the turn. I have never explored it. I'm mentioning it here so that you will know to look out for
  it. You might miss the turn and continue on the wrong trail if you weren't paying attention.</p>

<p>The descent into the ravine is steep, but short. Once at the bottom, the trail doubles back and
  heads upstream toward the falls. You can see the falls through the trees at this point. It's a
  very beautiful sight, but I've never attempted to capture it on film. I think it would be a
  difficult shot to make.</p>

<p>When you reach the falls, there are a number of rocks where you can sit and enjoy them. In
  times of high water, your choices of rocks might be limited, but in low water you can usually
  hop from rock to rock and take in the falls from many angles.</p>

<p>There are two sections to Old City Falls. The lower section is about 10 feet high. The stream
  flows over a steep rock face into a pool. The upper section is a vertical drop of maybe 25 feet.
  It feeds a second pool from which the lower section flows. The upper section is slightly hidden
  behind a rock outcropping that makes it difficult to see well from below and difficult to
  approach. There is a rough trail that scrambles up the left side of the falls to a rocky
  platform only a few feet from the rushing water of the upper section. It is a very dramatic
  place to be when the water is high, but climbing the trail can be tricky. Do not attempt the
  climb unless you are comfortable using your hands when hiking. Please <em>be careful!</em> In
  wet weather that short little trail can be treacherous. The spray from the falls in high water
  can also make it slick.</p>

<p>In low water it is much easier to get around. In the summer of 1995 the water was so low that I
  was able to easily and safely stand in the pool between the upper and lower falls. It was very
  unusual to be able to view the falls from that vantage point. In some ways Old City Falls is a
  nicer waterfall to visit in low water because it is so much easier to get near it&#x2014;especially
  if you are willing to do a little wading. In high water, the stream's current is so intense that
  even wading across it would be something of a problem. Under those conditions, standing in the
  pool between the falls would be out of the question!</p>

<p>If you visit Old City Falls, I suggest you do it on the hottest day of the year. The deep,
  well-shaded ravine and cool water keeps the space around the falls cool and comfortable. Yet
  there is still enough sun shining down on the rocks to keep the spot from seeming dark and
  gloomy. Bring a picnic or a book, but don't expect to be alone. Although Old City Falls is not
  widely known outside the local area, there is almost always a few other people there.</p>

<p>A very nice waterfall!</p>

