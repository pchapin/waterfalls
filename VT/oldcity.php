<pre>
Overview: A beautiful falls in a deep ravine. One of my favorites.
Location: Strafford, VT
Stream  : Old City Brook
Height  : 35 feet (estimate) in two drops.
Finding : Not difficult with a good road map or good directions.
Access  : Short walk. Steep in places.
Swimming: Depends on how high the water is.
Basking : Some good spots, especially in low water.
Legal   : Area maintained by the town of Strafford. Open to the public.
</pre>
<hr/>
<h2>Directions</h2>
<p>
  <b>From I-89 exit #2 in Vermont</b>
</p>

<p>Starting at the exit (0.0 miles), follow VT route 132 north (or is it east?) toward <span
    class="town-name">Strafford</span> and away from <span class="town-name">Sharon</span>. The
  road climbs out of the valley of the <span class="stream-name">White River</span> and then
  descends into the valley of the <span class="stream-name">West Branch of the
        Ompompanoosuc River</span>. At 6.4 miles you will come to a tee intersection in the center
  of South Strafford. Route 132 turns right. You should turn left.</p>

<p>At 8.8 miles you will come to the village of Strafford. Bear to the right through the village
  center, following the main road. At 9.5 miles, look for <span class="road-name">Old
        City Falls Road</span> on the right. It is marked with a small green road sign. It is the
  first right turn after the village of Strafford.</p>

<p>Turn onto Old City Falls Road (paved) and follow it a short distance until you come to a tee
  intersection at 10.3 miles. Turn left at the tee. In only a few yards you will cross a bridge.
  Immediately after the bridge, the road turns to dirt. At that point the entrance to Old City
  Falls is on the left. There is a short driveway to a parking area that will hold a handful of
  cars.</p>

