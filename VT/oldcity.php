      I was able to wade right up to the bottom of the falls and take some pictures from that
      unusual vantage point. The water was cold, but not very cold. When I stood in the pool just
      below the falls, it almost felt like I was about to step into a cold shower. If I had tried
      to stand in the same place earlier this year, I would have been swept away for sure.</p>
