<h2>Notes</h2>
<dl>
  <!-- Use reverse chronological order -->
  <dt><span class="notes-entry-header">Saturday, November 14, 2020</span></dt>
  <dd>
    <p>I have visited these falls a couple of times over the years since 2006. Today was a brisk,
      cloudy day, but with no snow or ice. The falls were in good form with enough water to
      create an interesting view, but not overwhelming.</p>

    <p>I hiked to the pond as well. At one point the trail, which had been following the stream on
      the left, turned right (south) away from the stream. Just before this point there was a <a
          href="https://www.google.com/maps/place/44%C2%B002'04.9%22N+73%C2%B004'39.7%22W/@44.03469,-73.079887,17z/data=!3m1!4b1!4m6!3m5!1s0x0:0x0!7e2!8m2!3d44.0346896!4d-73.0776934">stream
        junction below and to the left of the trail</a>. The right-hand fork of the stream
      appeared to come down a steep grade, and there were hints of another waterfall visible
      through the trees. Accessing the area would require bushwhacking about 100 yards down the
      embankment on the left side of the trail, across the stream, and then up the opposite grade.
      I did not try that today, but it might be worth exploring the area more in the future.</p>
  </dd>

  <dt><span class="notes-entry-header">Sunday, May 7, 2006</span></dt>
  <dd>

    <p>Today my daughter Hillary and I visited these falls for the first time. It was a
      beautifully clear day but not at all hot. It was also too early in the season for bugs. We
      were surprised at how close to the parking lot and how easily accessible the falls were. We
      had expected more of a hike to get to them.</p>

    <p>Because of significant amounts of recent rain, the water in the stream seemed reasonably
      high and the falls were in very good form. It might be interesting to return to these falls
      at a later time in the season to see what they are like.</p>

    <p>After spending some time at the falls, we decided to continue on to the pond. That hike
      involved a lot more climbing but it was interesting because it followed along the stream for
      some distance. The pond itself had a nice, remote feeling.</p>
  </dd>
</dl>

