<h2>Directions</h2>

<p>The trail head is <a
    href="https://www.google.com/maps/place/Abbey+Pond+trail+head/@44.0309049,-73.0904556,17z/data=!4m12!1m6!3m5!1s0x4cb542a02a7b48a9:0x2d74fae2b7e0fcd6!2sAbbey+Pond+trail+head!8m2!3d44.0309049!4d-73.0882616!3m4!1s0x4cb542a02a7b48a9:0x2d74fae2b7e0fcd6!8m2!3d44.0309049!4d-73.0882616">here</a>.
</p>
<p><b>From East Middlebury</b></p>

<p><img src="photos/abbey_upper-02.jpg" alt="Abbey Pond Cascades, Upper Falls" style="float:
    right; padding: 10px"/>Take <span class="road-name">Rt. 116</span> north from its intersection
  with <span class="road-name">Rt. 125</span> in the center of <span
      class="town-name">East Middlebury</span>. Travel a little over four miles to a dirt road on
  the right (East). There is a small sign for Abbey Pond Cascades along the road, but it would be
  easy to miss if you are not watching for it.</p>

<p>Immediately after turning off the main road, the dirt road forks. Follow the right-hand fork;
  there is no parking at the main road. After a short distance (perhaps half a mile) there is a
  turnoff on the left into a moderately large parking area. The turnoff is marked with a sign
  for the falls.</p>

<p>A trail leaves the back of the parking area. It skirts around a gravel pit and shortly crosses
  into the Green Mountain National forest. From there it is only a few yards to the falls. The
  trail crosses the stream on a wooden bridge between the lower and upper falls. To view the
  entire falls, you will want to explore both downstream and upstream from the bridge.</p>

<p>The trail continues on to Abbey Pond. It's a moderate hike of about two miles to the pond.</p>

<p style="clear: right;"></p>
<hr/>
<h2>Description</h2>

<p><img src="photos/abbey_lower.jpg" alt="Abbey Pond Cascades, Lower Falls" style="float:
    left; padding: 10px"/> Abbey Pond Cascades is a complex of waterfalls in a heavily wooded
  grotto. From the bridge one can look upstream into a narrow gorge and see a significant drop at
  the end of the gorge. By scrambling up alongside the stream you can see even more cascades above
  the gorge. The rocky crags make approaching the stream somewhat difficult, but it is easy to
  find places above the water to sit.</p>

<p>If you don't mind getting your feet wet, you can wade in the stream from the bridge to a rock
  shelf that goes into the gorge just below the falls there. If you attempt this, proceed with
  caution and use as many stepping stones as possible!</p>

<p>To see the lower falls, you will want to scramble downstream a bit. The main part of the lower
  cascades is right below the bridge, so you don't have to go far. There are some excellent
  vantage points on some rocks opposite the bridge from which the entire lower section can be
  easily seen (see a photograph to the left).</p>

