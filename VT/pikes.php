<h3>Sunday, April 20, 1997</h3>
<p>Today my daughter Hillary and I explored this waterfall. Locating it was a bit tricky. It is in
  <b>Jamaica</b> along <b>Pike's Falls Road</b>. At a point about a mile from the border between
  Jamaica and <b>Stratton</b> is a small collection of houses called "Pike's Falls." At that point
  <b>Day Road</b> connects to Pike's Falls Road from the south.</p>

<p>The falls themselves were a bit to the east of the crossroads that carried their name. There
  was no sign and no obvious turnoff. Hillary and I located the falls only by asking a local
  resident. A few yards to the east of the cross-road, Pike's Falls Road made a sharp bend. At
  that point, on the north side of the road and above the road, there was an old white house that
  marked the place to stop. There was a slightly wide spot in the road there as well, but it was
  nothing special. Once we got out of the car, Hillary and I could see a rough trail that headed
  down the embankment toward the stream. Down the trail a few yards and below the level of the
  road, there was a sign that let us know we had found the right place.</p>

<p>The trail was not difficult, but it was not marked either. Today there were no leaves on the
  trees, so it was easy enough to see where we were going. I'm not sure how the trail would be in
  a different season. It snaked its way down hill, steeply in places, a short distance until it
  came out on the large pool at the base of the falls just opposite to the falls.</p>

<p>The pool was one of the largest I've seen in Vermont. It looked like it would have been a
  wonderful swimming hole in lower water. Today spring run-off had swollen the river to a powerful
  flow of icy water, but in the summer I bet it would be a much calmer spot. The falls themselves
  were nice, but not particularly impressive.<b>North Branch Brook</b> flowed down over a rocky
  outcrop and split into two major sections that flowed around opposite sides of a large rock. The
  river then rejoined and poured into the pool with considerable force. However, the entire height
  of the falls that we could see only totaled about 35 feet.</p>

<p>Hillary and I didn't have time to fully investigate the area. We were late for meeting
  Hillary's mother, and we had to leave sooner than I would have liked. I guess that just gives me
  an excuse to return someday.</p>

<p>These falls have been mentioned to me by more than one reader of this website. Last December
  Dean Goss included these falls in a<a href="vtwater.html">list of Vermont waterfalls that I
    should investigate</a>. Jamaica is also home to the impressive <a
      href="hamilton.html">Hamilton Falls</a>. If you find yourself in the area be sure to check
  that one out as well.</p>

