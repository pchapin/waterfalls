<h2>Directions</h2>
<p>
  <b>From the Vermont Route 30</b>
</p>

<p>Starting in the center of Townshend at the junction of Vermont Routes 30 and 35 (0.0mi/0.0km)
  drive west on route 30 to West Townshend (4.6mi/7.4km). Turn north onto the paved Windham Hill
  Road. At 9.0mi/14.4 km, turn left onto the unpaved Burbees Pond Road. After a short distance
  this road turns to the right and comes to Burbees Pond. Just before the pond at 9.9mi/15.8km
  turn left onto West Windham Road. This road skirts the edge of the pond and soon turns to the
  south. There are a couple of side roads that turn off West Windham Road, but you should follow
  the main route in each case. Eventually the road becomes quite narrow, and it may be in poor
  condition at certain times of the year. At 12.8mi/20.5km, shortly after a sharp right turn, you
  will come to an informal parking area on the left just across from what looks like a small
  sawmill. There aren't any signs by the road. However, if you come to a stream crossing, you have
  gone too far.</p>

<p>From the parking area walk directly into the woods, and in a few yards you will see the stream
  to your right and in front of you. Down by the stream's edge is a sign warning you not to swim
  in the falls and reminding you of the people who have died trying it. From there you can
  scramble down the side of the falls to the best viewing location at the bottom. It is very steep
  and inclined to be slippery. Please exercise caution! There is no trail.</p>

<p>Alternatively, a few yards east along the road from the parking area, there is a trail that
  leads to the falls. The junction between the trail and the road is a bit obscure and is
  unmarked. It is located in a dip in the road maybe 50 yards from the parking area. This trail
  leaves the south side of the road and soon becomes quite obvious. It descends gradually with the
  stream and falls some distance to the right. A hundred yards or so from the road, there is an
  obvious sign that points down a blue blazed side trail that leads back upstream to the base of
  the falls. This approach is a bit longer, but much less difficult than the direct scramble down
  the side of the falls.</p>

<p>I understand that these falls can also be approached by trail from Jamaica State Park. I have
  not tried that, so I can't comment on what it is like. However, judging from the map, I would
  say that such an approach would require at least two miles of hiking one way.</p>
<hr/>

<h2>Description</h2>

<p>This is an impressive waterfall! In high water it is an awesome sight. In low water it is still
  graceful and interesting. The waters of Cobb Brook first flow into a couple of deep, circular
  pools at the top of the falls. Although they make tempting swimming holes, I don't think
  swimming there would be very wise. Immediately after flowing out of the lower pool, the stream
  slids down a steep rock face and then leaps out into space for a free fall of at least 15 feet
  or more. At the bottom of that free fall, the stream slams into a rock wall and sprays in all
  directions. There it gathers itself again and flows down over another steep, undulating rock
  face into the pool at the bottom of the falls.</p>

<p>The rock by the falls has been eroded into many odd shapes that dramatically display the power
  of water to cut and smooth solid rock. When the water in the stream is low, it is possible to
  scramble out onto the rocks and get many interesting views of the stream's channel. If you do
  this please exercise extreme caution. The terrain around Hamilton Falls is very steep and
  rugged. One slip could have disastrous consequences!</p>

<p>The best overall view of the falls is from the bottom. There, on the far side of the lower
  pool, you can look up at the entire falls and take in the majestic sight in one view. The
  picture at the top of this page was taken from that spot. Although the scramble to the bottom of
  the falls can be difficult and time-consuming, it is well worth it. The views along the edge of
  the falls are interesting, but not nearly as all-encompassing as the view from the bottom.</p>

