<h2>Notes</h2>
<dl>
  <dt>
    <b>Sunday, October 19, 1997</b> <br/>
  </dt>
  <dd>
    <p>Today my daughter Hillary, her dog Priscylla, and I visited these falls. Hillary saw them
      back in early 1995 but didn't get a chance at that time to really get a good look at them.
      Today we had all the time in the world, and we carefully picked our way to the bottom of the
      falls for a proper look. The water was very low. It flowed over the lower rock face in only
      two narrow streams. The pool at the bottom of the falls was almost still, and it was
      possible to stand on rocks near the bottom that during my earlier visits had been totally
      covered with raging white water.</p>

    <p>There were several people at the falls today. Although I wouldn't say it was crowded, it's
      not exactly a secluded location either. Today was a nice fall weekend day, and there were
      two or three cars in the parking area besides ours.</p>
  </dd>
  <dt>
    <b>Sunday, October 22, 1995</b> <br/>
  </dt>
  <dd>
    <p><i>Superb!</i></p>

    <p>It was a beautiful fall day: clear, cool, and with lots of nice colors about. A few days
      earlier we had a major rainstorm so the rivers were all running high. Yet, unlike during my
      previous visit, I was able to climb all the way down to the bottom of the falls. From there
      I got a full view of the entire waterfall. I have to say that Hamilton Falls may be the most
      awesome falls I have yet seen in Vermont!</p>
  </dd>
  <dt>
    <b>Sunday, April 9, 1995</b> <br/>
  </dt>
  <dd>
    <p>I visited this waterfall with my daughter Hillary. We didn't have much time there because
      we were on the way to meet Hillary's mother and we were late. In addition, the weather was
      poor. It was raining. The wet rocks and leaves made climbing around the falls difficult.</p>

    <p>As a result of the poor footing, we didn't get the best views in the world. However, what
      we did see was impressive. First, the stream filled a large, deep pothole. Then it dropped
      over a 20-foot fall into what appeared to be another large pothole. The second pothole
      emptied into a narrow channel that dropped steeply another 30 feet or more. We will have to
      come back some time to get a proper look.</p>
  </dd>
</dl>
