<h2>Notes</h2>
<dl>
  <dt>
    <br/> <b>Saturday, March 30, 2002</b> <br/> <br/>
  </dt>
  <dd>
    <p>I have visited Texas Falls several times since my last entry. My wife and I often bring
      friends and visitors here because it is such a nice waterfall and because it is near where
      we live. Today we brought some of my wife's family here to see the falls.</p>

    <p>There was snow and ice everywhere, making the steps down to the bridge over the gorge very
      slippery and dangerous. However, the view from the bridge was excellent and well worth the
      trouble to get there. The water was quite high today (due to the warm, spring weather), and
      the stream raged. Large ice formations hung from the rocks and dangled above the stream in a
      most interesting way. They seemed both delicate and solid at the same time. The water itself
      seemed to have a white-blue color as it churned through the narrow passage, making a loud,
      glorious noise.</p>
  </dd>

  <dt>
    <br/> <b>Thursday, July 10, 1997</b> <br/> <br/>
  </dt>
  <dd>
    <p>Today I visited these falls with my friend Sue, her two children, my daughter Hillary, and
      my daughter's puppy, Priscylla. We were on our way to the picnic spot just up the road from
      the falls, and we took in the falls as part of that trip.</p>

    <p>As usual, there were quite a few people around. Texas Falls is a popular spot. The water
      was low, but not excessively so. We took in the view from above the falls, from the bridge
      over the ravine, and from the observation "platform" below the main falls. We didn't spend
      any time scrambling around on the rocks in the ravine itself, although it would have been
      possible today; the water level was low enough for it.</p>

    <p>The picnic area above the falls is a nice spot. It's right along the stream and very well
      wooded and pleasant. There were some bugs around today, however, but it could have been
      worse.</p>
  </dd>

  <dt>
    <br/> <b>Summer 1995</b> <br/> <br/>
  </dt>
  <dd>
    <p>My daughter Hillary and I visited these falls together. I'm writing this description long
      after our visit, so I don't remember the exact date. We prowled around on the rocks and had
      a picnic lunch. I remember watching various water bugs skittering around on the top of a
      quiet section of the stream.</p>

    <p>Here is a <a href="photos/texas.jpg">picture</a> [115 KBytes] of one section of the falls.
      Because of the unusually low water this summer I was able to climb right down into the gorge
      and get a very nice shot. You get a good feeling of the gorge, but the falls don't look very
      impressive because of the low water level.</p>
  </dd>

  <dt>
