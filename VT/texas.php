<h2>Directions</h2>
<p>
  <b>From VT route 100</b>
</p>

<p>At the junction of VT routes 100 and 125 in the center of<b>Hancock</b>, turn west on VT route
  125. Several miles along the road, before it starts to climb significantly toward <b>Middlebury
    Gap</b>, look for a Green Mountain National Forest sign for Texas Falls. Turn on to the road
  indicated by the sign and drive a short distance (less than a mile) to the parking area for the
  falls. There is a picnic area just a few more yards down the road beyond the falls.</p>

<hr/>
<h2>Description</h2>

<p>Texas Falls is probably one of the most popular falls in Vermont. Its image appears on many
  postcards and probably calendars and puzzles as well. Even if you have no idea where Vermont is
  on a map of the United States, you may have seen this waterfall.</p>

<p>The falls are inside the borders of the Green Mountain National Forest, and the forest service
  has set up guard rails and walkways to make it easier for people to see the falls. The falls are
  easily accessible, being just a short way off of Rt 125 and right next to a forest service road.
  There is a picnic area a little ways up the road and some other trails that are good for
  cross-country skiing or casual hiking.</p>

<p>The falls are in a narrow ravine that is not particularly deep, but that has very sheer sides.
  The stream flows over a couple of steps separated by round, deep pools that have been carved out
  by the rushing water. The forest service has created a bridge that crosses the ravine and gives
  visitors a good view of the main drop. It is fun to stare down into the clear, rushing water and
  watch it churn and boil as it flows through the narrow passage.</p>

<p>There are a few trails in the area that you can also follow. One trail brings you down to an
  observation platform, of sorts, at the bottom of the falls. From there in dry conditions you can
  climb out on the rocks and up into the gorge somewhat. There is also a trail that takes you to
  the top of the upper falls where you can see the stream gather itself for the plunge below.</p>

<p>Texas Falls is not particularly high, nor does it have sheer drops. Nevertheless, it is an
  interesting waterfall and well worth the trip. It can be easily accessed by almost anyone
  (although I would suggest exercising extreme caution if you do any exploring off the
  trails).</p>

