<h3>Saturday, March 30, 1996</h3>

<p>It was a beautiful early spring day. The sky was cloudless and the air was (fairly) warm. There
  was very little snow about. My friend, Kim, and I visited these falls as an afternoon outing.
  They were located just off Rt. 103 right near the border of <b>Clarendon</b> and
  <b>Shrewsbury</b>. There was no sign for the falls, but there was a sign for the crossing of the
  <a href="http://www.fred.net/kathy/at.html">Appalachian Trial</a> (AT) and the Long Trail. The
  two trails share the same path at that point. The place to stop is right at where the railroad
  tracks cross Rt. 103.</p>

<p>You can follow the AT down to the gorge. It's only a couple of hundred feet from the road. Kim
  and I, however, followed an unmarked side trail to the gorge's edge, and then followed another
  informal trail along the top of the gorge through a dense pine forest to the AT.</p>

<p>The AT crosses the <b>Mill River</b> at the head of the gorge on a suspension bridge. The
  bridge was in excellent shape, but it still felt funny to walk across the narrow, swaying bridge
  thirty feet or so above the swirling water. Nevertheless, the view from the bridge was
  excellent. On one side we could see the Mill River as a wide, shallow stream. On the other side,
  we could see the river concentrated into a narrow channel as it funneled between a rather large
  rock and the sheer side of the gorge. The water had a beautiful, rich green color. No doubt it
  was carrying sediment from the spring run-off, yet it wasn't muddy or cloudy looking.</p>

<p>With some difficulty, Kim and I climbed down the far side of the gorge to sit on the rocks just
  above the stream. We were struck by all the potholes that had been ground into the rocks. The
  rocks almost looked partly melted. All the sharp edges had been worn smooth. The abraded surface
  extended for at least ten feet above the water level, suggesting that the river regularly runs a
  great deal higher than it was today. In flood conditions, it must be quite a sight to see an
  overflowing river force its way into that narrow channel.</p>

<p>Kim and I watched the deep, green water bubble and curn by the rock we were on. Although there
  were no significant falls in the gorge, there was a lot of water noise. I found the sound to be
  very relaxing, and I almost feel asleep leaning against Kim's shoulder.</p>

<p>We decided not to bother walking down the gorge. From what we could see, it widened out as you
  moved farther from the entrance. We both agreed, however, that it would be a fun place to return
  to either during exceptionally high water or during exceptionally low water.</p>

<p>I wonder if this gorge is a water gap. The Mill River appears to flow from east to west across
  the main backbone of the Green Mountains. Perhaps the river was there before the mountains, and
  cut a channel for itself as the mountains rose. If so, Clarendon Gorge might be the focus point
  for its latest cutting. Perhaps in the past, before the gorge existed, there was a lake on the
  east side of the gorge and a large fall that drained the lake into the vally of <b>Otter
    Creek</b>. Over time those falls would have cut back into the rock, eventually reaching the
  east side and draining the lake. The result would have been the current gorge with the remnants
  of those once magnificent falls still lingering at its eastern mouth.</p>

