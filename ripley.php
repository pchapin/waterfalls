<h2>Directions</h2>
<p>The access road to Ripley Falls is right along US Route 302 in Crawford Notch. Look for it on
  the west side of the road a couple of miles below the height of land. The Appalachian Trail
  crosses Route 302 at the point where the access road meets the highway.</p>

<p>Drive up the access road a short distance to a small parking area. The road ends at the parking
  area, so you won't miss it! There are signs for the trail and the falls.</p>

<p>The trail rapidly angles "to the right" and soon crosses the railroad tracks. Not long after
  that the trail splits with the route to the falls taking the left fork and the Appalachian Trail
  taking the right fork. The trail climbs gradually for a total distance of about a half-mile.
  Finally, it decends a steep, rocky section to the stream's edge where you get your first and
  best view of the falls.</p>
<hr/>

<h2>Description</h2>
<p>Avalanche Brook flows over a large rock face at Ripley Falls. Although there is no truly
  vertical drop (to speak of), the rocks are very steeply inclined, making for a beautiful
  waterfall. The falls seem to have two distinct sections over which the stream flows in slightly
  different directions, and throughout its fall it is spread widely over the rocks.</p>

<p>The end of the trail is just below the falls and pretty much in the ideal spot for viewing the
  falls. There are a number of rocks that one could scramble over to get closer to the falls, and
  that is worthwhile. However, I felt no particular urge to attempt to climb the steep ground
  alongside the falls. That would have been very treacherous anyway.</p>

<p>All in all, Ripley Falls is quite beautiful. I highly recommend it.</p>

