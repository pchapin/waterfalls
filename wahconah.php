<h3>Friday, April 5, 1996</h3>
<p>This state park is located right at the junction of the towns of <b>Dalton</b>, <b>Windsor</b>,
  and <b>Hinsdale</b>. Along route 9 there is a sign marking the turn onto <b>Wahconah Falls
    Road</b>. From there you have to drive a short distance over a relatively rough dirt road to
  the park entrance. You won't miss the entrance. There is a large parking lot there.</p>

<p>I visited the falls today with my friend John and his girlfriend. Due to the early season, there
  was hardly anyone else there. It was just the way I like it.</p>

<p>We walked a short distance from the parking lot down to the falls. They were quite spectacular.
  The stream flowed over several steps into a very large pool. The stream was good sized and
  filled with spring run-off. There was a lot of foam and a great roaring sound! We took several
  pictures while perched out on the wet rocks. Very nice. My pictures came out too dark, but at
  least John got some good ones (see above).</p>

<p>After spending some time admiring the main falls, I explored upstream a little. I definitely
  recommend it. Although there were no other falls as large as the lower one, there were a couple
  of smaller falls that spilled over steps several feet high. It looked like the upstream falls
  are not often visited and probably are relatively quiet even in the busy summer months. It might
  make a good picnic spot if you don't mind eating out of your lap.</p>

<p>These falls are a "must-see" if you are in Western Massachusetts.</p>

