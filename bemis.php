<p>Today my daughter and I spent the day in Crawford Notch enjoying waterfalls. Bemis Brook Falls
  is located a little way up the <i>Bemis Brook Trail</i>, just above <a
      href="coliseum.html">Coliseum Falls</a>. To get to these falls, you must start hiking down
  the <i><a href="arethusa.html">Arethusa Falls</a> Trail</i>. The Bemis Brook Trail diverges left
  from that trail after a short distance.</p>

<p>Bemis Brook Falls are located in a shady glen. There are two main drops with a smaller, third
  drop. It takes a bit of a scramble to climb down into the glen and to get up close and personal
  with the falls, but it's worth it. With only a little trouble you can climb out on the rocks
  between the upper and the middle drop. From there you get a wonderful view of the upper falls
  (pictured at the right). The large pool at the base of the upper falls is very inviting.</p>

<p>A nice waterfall. It's not especially spectacular, but the location is quite senic.</p>

