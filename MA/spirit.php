<h2>Description</h2>

<p>Spirit Falls is a waterfall surrounded by forest. Tall trees grow up on either side of the
  small stream that flows over the waterfall, making the spot very well shaded. Little Pond, the
  source of the stream, is a swampy body of water without an inlet. The water in the stream
  flowing out of it is stained brown by the tannic acid liberated by all the decaying vegetation
  in the swamp. To be honest, I find the color a bit disconcerting. Although it is not due to
  human pollution, most New England waterfalls have crystal clear streams flowing over them. The
  brownish water flowing over these falls took some getting used to.</p>

<p><img src="photos/spirit4.jpg" style="float: left; padding: 10px" alt="Spirit Falls"/> The upper
  falls are only a few yards below the bend in the trail that I mentioned in the directions. They
  are about ten feet high and flow over a large, mostly flat outcropping of rock in a single drop.
  It is a nice waterfall, although not very remarkable.</p>

<p>The lower falls are the main event. They are located about 30 yards or so below the upper
  falls. To get the best view, however, you should plan to scramble down the steep hillside to the
  bottom of the cascade. Don't try this scramble unless you have some experience with hiking on
  rough, steep terrain. The ground is wet and slippery in spots, and it would be easy to have an
  accident there.</p>

<p>The stream spills over several rock steps and then flows among some rocks and boulders at the
  bottom of the falls before collecting itself together again. There are some nice places at the
  bottom of the falls where you can stand on the rocks and get a perfect view of the water as it
  splashes downwards. I imagine that these falls are quite spectacular in the spring, although
  climbing down to the bottom of them would probably be rather treacherous at that time as
  well.</p>

<p>Spirit Falls is a nice waterfall located in the woods. If you are in Royalston looking at
  waterfalls (such as <a href="royalston.html">Royalston Falls</a> or <a href="doanes.html"
  >Doane's Falls</a>), don't forget to check this one out as well. However, my guess is that
  during low-water season these falls might be nearly dry.</p>

