    <p>Today my friend John and I visited these falls. We didn't take the most direct route to
      them because we weren't quite sure where we were going. We ended up visiting the overlook
      first. However, it was a worthwhile stop. The sky was clear today (although it was also a
      bit warm) and the view was great. When we finally found the falls, we spent quite a bit of
      time scrambling around them and taking pictures. I took several pictures, but I had to go to
      1/4 second exposures to get enough light (ASA 200 and ASA 100 film). That should give you
      some idea of what the spot is like. I'm glad I thought to bring my tripod.</p>

