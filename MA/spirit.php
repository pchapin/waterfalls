<hr/>
<h2>Directions</h2>
<p>
  <b>From the center of Royalston</b>
</p>

<p>Follow route 68 west out of town for about 0.6 miles. On the left you will see a small sign for
  the Jacob Hill Reservation, a property owned by <span class="other-name">The
        Trustees of Reservations</span>. There is a small turnout there with space for two or
  three cars. Some additional cars can park along the road.</p>

<p><img src="photos/spirit2.jpg" style="float: right; padding: 10px" alt="Spirit Falls"/> At the
  back of the turnout a yellow blazed trail leads to the falls. It is consistently marked, but the
  marking is not always obvious. The trail is not heavily traveled, and in places it is a bit
  obscure. About halfway to the falls another yellow blazed trail forks to the right. If you are
  not alert you might miss this fork and end up following the wrong trail. Take the left-hand fork
  to the falls. The right-hand fork climbs to the ridge where it joins another trail. Along the
  ridge to the right (uphill) the trail leads to a nice overlook with a beautiful view of <span
      class="other-name">Long Pond</span> and <span
      class="stream-name">Tully River</span>. Along the ridge to the left (downhill) the trail
  leads to the falls and loops back to become the left hand fork.</p>

<p>The direct route to the falls keeps more or less level and soon begins to border alongside a
  marsh. This marsh is part of Little Pond, a spring-fed body of water that is the headwaters for
  Spirit Falls. The trail then comes to a stream flowing out of the marsh, and it turns to follow
  that stream downhill. After a few yards, the trail makes an abrupt right-hand turn to follow the
  ridge uphill toward the overlook I mentioned above. You should leave the trail at this point and
  continue following the stream downhill. There is an informal, unmarked trail that you can
  follow. In a short distance you will come to the upper part of Spirit Falls. A little ways
  beyond that you will come to the larger, lower part of the falls.</p>

<p style="clear: right"/>

