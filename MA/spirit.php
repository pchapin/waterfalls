Overview: A pleasant, well shaded waterfall with a secluded feeling.
Location: Royalston, MA
Height  : 50 feet total (estimate) in two separated drops.
Finding : Trail a bit confusing to follow.
Access  : Requires about 1 mile of hiking. Moderate terrain. Some scrambling required.
Swimming: No.
Basking : Not much sun. Heavily forested.
Legal   : Public access.
