<p>Mike sent information on these falls to me. It sounds like they are probably on private land.
  However, the existence of the dirt parking area near the falls also makes it sound like they are
  well visited. If you do try to visit these falls, please be sure to honor any "No Trespassing"
  signs that you might find.</p>

<p>Here is Mike's description.</p>

<p>
  <i>Here's a photo of a waterfall I found in Weymouth, Mass. I'm not sure if it has a name. I
    think the pond that flows into it is called Whitman's Pond. But it seems like I am the only
    person who knows about this waterfall. I have yet to find a person who lives in Weymouth who
    knows about this waterfall or if it even has a name. It's located off of Iron Hill road.
    There's a dirt parking lot to the right. You park there and walk up a slight hill, and you'll
    see a herring run. You have to leap over the herring run and jump down 5 or 6 feet. Then hike
    down a hill to see the waterfall. It's kind of difficult to get a good angle on it. I think
    you may have to cross the waterfall and get to the other side to get the best angle. I have
    not found a way across the waterfall yet. There are some houses to the right of the waterfall,
    but I did not notice any way to get to the falls without cutting through someone's yard.</i>
</p>

<p>On December 16, 1999 a reader of this web page who grew up in Weymouth sent me some additional
  information. Here is what she said.</p>

<p><i>I was born in Boston and raised in Weymouth. I know of the waterfall you are talking about,
  the name of the pond that goes into the falls is Whitman's Pond, I swam in the pond as a child.
  As far as I know the falls have no name. I moved away from Weymouth in 1963. I haven't been back
  to that area of Weymouth for a long, long time. So what it looks like today I have no idea. Back
  in the fifties we use to play by the falls and also follow the herring to the ladders on Iron
  Hill Street</i>.</p>

