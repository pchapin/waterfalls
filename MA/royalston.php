<h3>June 22, 1998</h3>
<p>
  <i>Roy Sunter emailed me the note below describing an alternative route to Royalston Falls that
    might be of interest to some of you. Thank you, Roy, for this information!</i>
</p>

<p>From the intersection of Routes 119 and 32 in Richmond, NH, take Route 32 S (towards Athol,
  MA). A mile or two down the road (sorry, I haven't paced it off) look for a left turn onto an
  unpaved road named Greenwood Road, and take it.</p>

<p>Greenwood Road eventually forks, and the right-hand fork will bring you to the Royalston Falls
  parking area. I would not attempt this road without a high-clearance 4WD vehicle.</p>

<p>The other thing you overlooked is that part of the Metacomet-Monadnock trail (itself a
  tributary of the Appalachian Trail) follows the brook upstream from the top of the falls to
  Greenwood Road. It's a lovely, scenic walk, through some magical places where the water runs
  underground, out of sight but not out of hearing; past a pool fed by another tiny waterfall; and
  along a pretty little gorge. It's an easy walk, not too difficult for older children. It's
  especially charming at the height of foliage season.</p>

