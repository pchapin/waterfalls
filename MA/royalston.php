<h3>Sunday, April 27, 1997</h3>
<p>I approached this waterfall from the south. I started in the town of <b>Royalston</b> and drove
  west on MA Rt 68. A short way out of town, I turned onto <b>Falls Road</b> to the north. It was
  the first right-hand turn after leaving Royalston. It was marked. I then followed Falls Road for
  a little over 2 miles. At that point it became an unmaintained road. The change was obvious.
  There was space here and there for a handful of cars to park along the first part of the
  unmaintained road. I parked my car and started walking.</p>

<p>The road would have been impassible for a normal vehicle. However, it would have been possible
  for a four-wheel drive vehicle with high clearance to have navigated the road. The road
  descended to a stream crossing and then climbed the hill on the opposite side of the stream.
  After about a mile I came to a parking area on the left that was clearly marked as Trustees of
  Reservations property. It seemed strange to find even a rough parking area attached to such a
  rough road, but there it was: complete with a nice, neat sign. When I arrived today, there was a
  jeep parked there. I was not alone.</p>

<p>From the back of the parking area, I followed a yellow-blazed trail through rolling woods for
  about 0.5 miles. Finally, the trail descended toward the sound of rushing water, and I came to
  the falls. There were quite a few people there today practicing their rock climbing skills in
  the gorge cut by the stream. It was interesting to see all of their equipment and to look over
  the edge at the people clinging to the side of the rock face.</p>

<p>The Trustees of Reservations had installed a cable fence along the top of the gorge to prevent
  people from slipping to their doom. Normally such devices detract from the wild nature of a
  waterfall, but the secluded location of these falls made it seem wild enough so that it didn't
  matter. In fact, the extra security was welcome.</p>

<p>Falls Brook flowed over a single, large drop of 60 feet to splash into a round, circular pool
  below. The walls of the gorge were sheer. Leaning over the cable fence to look down into the
  pool was thrilling. A bit downstream, a block of rock jutted out over the chasm. Standing on top
  of that rock (thank goodness for the fence) allowed me to get a decent picture of the falls.
  However, from that vantage point it was impossible to see the entire drop.</p>

<p>I bushwacked downstream for a few yards to a place where I could scramble down to the stream. I
  wanted to then walk up the stream at the bottom of the gorge so that I could get a view (and a
  picture) of the entire falls from below. Alas, a large, deep circular pool blocked the way.
  Sheer rock walls and deep waters prevented me from even getting close enough to see the falls
  that way. Perhaps later in the year when the water is lower, wading up the stream might prove
  fruitful.</p>

<p>I noticed that the circular pool that blocked my path looked very similar to the circular pool
  into which the falls were spilling. I realized that I was looking at an ancient pool that was
  once filled by the falls. In time the falls cut into the rocks, backing gradually into the
  hillside. Currently, it's creating another pool several yards upstream, leaving the downstream
  pool to be just a stopping point on the water's seaward path.</p>

<p>Just before leaving, I talked for a minute or two with some of the rock climbers. One of them
  commented on the ice still clinging to the west wall of the gorge. He said that last week when
  they had come to inspect the gorge for their practice, the entire wall was covered with ice. "I
  want to learn ice climbing," he said. "If I had known what this place was like in the winter, I
  would have come here!" Apparently last week the water was also higher. "A roaring torrent," I
  was told, "the level was 10 or 15 feet higher at the bottom of the gorge." Neat.</p>

<p>On my way back, I encountered three more groups of people walking to the falls. Despite its
  secluded location, it appeared to be a popular spot.</p>

<p>A very nice waterfall.</p>
