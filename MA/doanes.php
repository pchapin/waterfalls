<h3>Sunday, April 27, 1997</h3>
<p>
  <img src="photos/doanes5.jpg" style="float: right; padding: 10px" alt="Doanes Falls"/>
</p>

<p>A reader of this website who grew up in the area of Doane's Falls informed me via email that,
  in fact, there were many more waterfalls to see below the first one that Hillary and I saw last
  year. Today I revisted Doane's Falls to investigate.</p>

<p>The water was higher today than it had been last October. The falls just below the road were
  considerably more impressive than they had been during my last visit. After enjoying the upper
  falls a little, I followed the yellow-blazed trail downhill. In a few yards I came to a place
  where the stream sliced through a narrow, steep walled chasm. There were several well-worn
  overlooks where I could stand and look down into the chasm comfortably.</p>

<p>Just below the chasm, the stream poured over a large cascade into a large pool. From a rock
  outcropping I could look down into a wide bowl of rock and water to admire the impressive falls.
  I scrambled down toward the edge of the water for a picture from below, but I couldn't get to
  the ideal spot due to slippery rocks. At a time of lower water, I suspect this would be a very
  photogenic falls. I did snap a picture of the falls while huddled against the rock outcropping.
  I could feel the spray of the water on my face.</p>

<p>The stream appeared to level out, but I followed the trail farther anyway. I had already
  underestimated this waterfall once, and I was not about to do it again! In a few yards, the
  stream began to fall some more. This time it flowed through a narrow curving channel of rock
  that opened out into a small pool. I was able to easily climb onto the rocks right above the
  stream and get a good look at the rushing water. It was very dramatic.</p>

<p>
  <img src="photos/doanes4.jpg" style="float: left; padding: 10px" alt="Doanes Falls"/>
</p>

<p>The pool emptied almost immediately by pouring over a 15-foot unobstructed drop into a large
  pool at the bottom of the falls (see the photo at the top of this page). This final fall was,
  perhaps, the most impressive of them all, for it was both graceful and powerful at the same
  time.</p>

<p>This is an excellent waterfall!</p>

