<h3>Sunday, October 6, 1996</h3>
<p>
  <img src="photos/doanes1.jpg" style="float: right; padding: 10px" alt="Doanes Falls"/>
</p>
<p><i>(During this visit Hillary and I missed most of the falls because we didn't hike down the
  trail far enough to see them. Check out my visit of April 27, 1997 below for more
  information)</i></p>

<p>This property is owned by the <a href="http://www.thetrustees.org/">Trustees of
  Reservations</a>. I found it in their catalog of properties. Doane's Falls is located along<b>Athol
  Road</b> in Royalston. Only a short distance north of the border of <b>Athol</b> the road
  crosses over <b>Lawrence Brook</b>. At that point <b>Doane Hill Road</b> comes in from the west.
  There is a small parking area between the stream and the road junction, and an obvious enough
  sign marking the spot.</p>

<p>Hillary and I visited these falls together. We followed a trail that skirted the edge of a
  ravine through which the stream flowed. A few yards down the trail, we were able to scramble
  down the side of the ravine and get close to the water. From there we had a good view of the
  falls.</p>

<p>The stream flowed over two or three steps each between five and ten feet in height. There
  wasn't a huge amount of water in the stream today, so it snaked back and forth across the rocky
  channel as it flowed down toward <b>Tully Lake</b>. There was a large pool at the bottom of the
  falls, but the Trustees of Reservations have marked the area with many "No Swimming" signs. They
  even posted a list of people who have been injured or died there to help make their point.</p>

<p>There were quite a few people visiting this spot when Hillary and I were there today. I'm not
  exactly sure what made it so popular. Although people were stopping by the falls, many of them
  continued hiking down the trail toward the lake. It almost makes me wonder if there are more
  falls downstream. Hillary and I didn't investigate that.</p>

<p>The falls are nice, but not special (at least... the ones we looked at).</p>
