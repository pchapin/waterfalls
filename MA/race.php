<h2>Directions and Description</h2>

<p>Race Brook flows down the steep east side of Mt. Everett, located in the southwest corner of
  Massachusetts. Route 41, aptly named Under Mountain Road, follows the base of the Mt. Everett
  ridge for several miles. At a point just a few yards north of where Salisbury Road comes in on
  the east, there is a turn-off on the west side of the road. It is obviously managed by the state
  of Massachusetts since there is a large sign there showing the various parks and forests around
  the state.</p>

<p>The Race Brook Falls Trail leaves that turn-off on an unmarked path. It parallels a small
  stream for a short distance and then comes to a sign confirming its destination. The trail edges
  along some (probably private) fields before entering the woods.</p>

<p>After a short distance the trail forks. The right-hand fork comes to a dead end at the lower
  falls on the northern side of the stream The left-hand fork is the main trail that climbs to the<a
      href="http://www.fred.net/kathy/at.html"><b>Appalachian Trail</b></a> "via the falls." The
  left-hand fork is the one to take if you are interested in visiting all the falls and not just
  the lower one. Take the left fork.</p>

<p>The trail quickly comes to the stream. It isn't a very large stream, and in low-water
  conditions the falls are almost non-existent. However, it doesn't take a great deal of water to
  make a spectacular waterfall! The stream is clear, but the water has a distinct grayish tinge.
  Perhaps the color is due to silt in the runoff, or perhaps it is just due to the color of the
  rocks beneath the water. In any case, the water looks fresh and cold.</p>

<p>The trail crosses the stream and starts climbing in earnest. It isn't a long hike to the falls,
  but it is more than a short walk. All in all, it must be about a mile to the top of the falls,
  and some of the climbing is quite steep and moderately difficult.</p>

<p>Shortly the trail forks again. The left-hand fork goes up to Race Brook Campground, and the
  right-hand fork goes to the falls. It is a "loop trail" that connects with the main trail again
  just below the upper falls. However, the right fork climbs next to the stream and visits several
  other falls along the way. Take the right fork.</p>

<p>After a short distance, a side trail diverges to the right to the "lower falls." Scramble down
  the steep embankment to the stream and step out onto the rocks before a beautiful waterfall!
  (See the picture at the top of this page). Race Brook spills over a steep rock face and plunges
  down maybe forty feet into a nice pool. From there the stream babbles through a wide, steep rock
  field. There are few trees that can grow in the rock field, so the space feels very open and
  airy. The steep slope of Mt. Everett walls in one side and makes the place feel like a huge
  amphitheater. The falls are like some great orator, loudly pronouncing the mysteries of Water
  for all with ears to hear.</p>

<p><a href="photos/race2.jpg"> <img src="photos/race2.jpg" style="float: right; padding: 10px"
                                    alt="Race Brook Falls"/> </a> As soon as the main trail mounts
  to the top of the lower falls (from which there is a rather nice view to the east), you can see
  another fall higher up through the trees. This one is as not as high, but it is wide and seems
  to have a lower and an upper section. Above that fall there is yet another one, and above that
  still another!</p>

<p>Finally, the trail comes to the upper falls. Higher than any of the others, the upper falls
  must be at least 75 feet. The stream cascades down a steep chute in a frenzy of spray, changing
  directions a couple of times on its way down. Below the falls the stream is open and fairly
  level. This is the spot where the side trail "to the falls" joins back with the main trail. The
  main trail then crosses the stream and takes a long switchback to climb to the top of the upper
  falls. The trail diverges so far from the falls that the sound of water is lost. But have faith!
  The trail does return to the stream eventually.</p>

<p>Shortly before reaching the Appalachian Trail, and well above the falls, the trail comes to
  Race Brook Campground. It is a primitive camping area, but it looks nice.</p>

