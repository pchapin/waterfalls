<h2>Notes</h2>
<dl>
  <dt>
    <b>Sunday, September 28, 1997</b>
  </dt>
  <dd>I received some email today from Neal Coates. He and his family explored these falls the day
    before. He reports that the water was very low and that the falls were only a "series of fast
    drips." He also reports that the initial right-hand fork to the lower falls is a shorter, less
    difficult climb to those falls than that taken by the main trail. Thanks, Neal, for the
    information!
  </dd>

  <dt>
    <b>Thursday, April 4, 1996</b>
  </dt>
  <dd>The stream was fairly high today when I visited these falls for the first time. In addition,
    there was still some ice and snow around that made the footing on the steep terrain especially
    tricky. However, the extra caution was well worth the effort. Race Brook Falls were in good
    form and were very beautiful and exciting. I ate my lunch on the rocks at the top of the upper
    falls. I'd say Race Brook Falls is one of the best New England has to offer. Plan on spending
    at least two hours on the trail if you visit.
  </dd>
</dl>

