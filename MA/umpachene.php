<h3>Saturday, April 26, 1997</h3>
<p>My friend John and I were doing some genealogical research in <b>New Marlborough</b> today. I
  noticed Umpachene Falls marked on the topographic map and said, "let's check this out." The
  falls were not along any major roads, nor were there any obvious signs for them. If you want to
  find these falls, be sure to get a good map. A street atlas for the town of New Marlborough
  should suffice.</p>

<p>A short way east of <b>Southfield</b> was a complex junction of four roads. At that point the
  <b>Umpachene River</b> flowed under a bridge, and two roads connected on each side of the bridge.
  Beside one of the roads was a small, easily missed sign for the falls pointing down a dirt road
  that ran parallel to the stream.</p>

<p>We followed the dirt road to the bottom of the hill. There, on the south side of the road, was
  a surprisingly large parking area containing a surprising number of cars. It appeared to be a
  small park complete with picnic tables (no grills) and swings. The <b>Konkapot River</b> flowed
  by the park <em>toward</em> the sound of the falls. "Isn't that river flowing the wrong way?"
  John asked me. In fact, the Konkapot and the Umpachene join just past the park so that both
  streams were really flowing toward a common point of drainage.</p>

<p>The falls themselves were nice. They consisted of two parts. The upper position had a drop of
  maybe 10 feet. It fell into a nice pool that then drained down a steep watercourse to another
  small drop. The pool at the base of the falls might be swimmable. There were plenty of open
  rocks around that would have made for a very nice picnic spot (why use the tables when there are
  rocks by the falls?).</p>

<p>I wouldn't travel a long distance to see this waterfall, but it was certainly worth the stop.
  If you do find yourself in New Marlborough, however, be sure to also check out <a
      href="campbell.html">Campbell Falls</a> as well.</p>

