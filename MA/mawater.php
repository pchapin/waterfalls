<h1>Other Massachusetts Waterfalls</h1>
<p>
  <i>The following falls in Massachusetts were brought to my attention by various sources. These
    are all falls that I have not yet seen myself. As I visit these falls, I will take them off
    this list and add them to my main list.</i>
</p>
<hr/>
<ul>
  <li>
    <p>ADAMS: <b>Peck Brook Falls</b>.</p>
  </li>
  <li>
    <p>BARRE: <b>Cook's Canyon</b> &amp; Nature Center. Cook's Canyon is named for the dramatic
      gorge at the south end of the property. The sanctuary includes coniferous planation and
      mixed hardwoods bordering the brook, a waterfall and a pond, and more than 100 species of
      wild native birds. There is a lovely view of the Ware River Valley from Look Out Road.
      Trails connect all of the interesting places. There is snowshoeing and cross-country hiking
      in the winter, nature study field trips, and organized Nature History Workshops, though no
      public camping is allowed. It is owned by the Massachusetts Audubon Society.</p>
  </li>
  <li>
    <p>BELCHERTOWN: <b>Holland Glen</b>. A steeply sloped ravine with a stream cascading through a
      hemlock forest. It is a short climb and is preserved by the Belchertown Historical Society.
    </p>
  </li>
  <li>
    <p>BLANFORD: <b>Sanderson Falls</b> &amp; Surrounding area. A waterfall on Sanderson Brook in
      Chester-Blanford State Forest. The area is very scenic, reached by hiking trails.</p>
  </li>
