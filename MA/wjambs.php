<h3>April 16, 1995</h3>
<p>Windsor Jambs is located in Windsor State Forest in Windsor, MA. It isn't difficult to find,
  but you should take a good map with you. It's necessary to drive down some dirt roads to get to
  the falls, and the roads are not marked.</p>

<p>The falls are not spectacular. However, the topology of the area is very interesting. The
  stream flows through a deep, narrow gorge. In places the sides of the gorge might be 50 sheer
  feet. The stream itself churns over many small to medium-sized drops and through many narrow
  passages.</p>

<p>Alas, the state of Massachusetts has put a fence in place at the edge of the gorge to keep
  people from falling in. I think this detracts very significantly from the beauty of the
  location. In addition, all views of the stream are from above, and that is not the best way to
  look at waterfalls.</p>

