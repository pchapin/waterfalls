<h3>Friday, April 5, 1996</h3>
<p>I visited these falls again with my friend John and his girlfriend. There was a lot of snow
  around today. The ground was almost totally covered with a layer several inches deep. However,
  the snow was hard and compacted, so it didn't make walking difficult.</p>

<p>The gorge was more impressive than I remembered it from last year. The water was higher, and
  perhaps that explains it. There was still a lot of ice and snow in the gorge, but that only added
  interest. It didn't significantly obscure the view of the water.</p>

<p>We walked along the edge of the gorge and took several pictures. However, the poor viewing
  angle (from above) kept me from really getting the picture I want. Someday I'll have to come
  back and wade up the stream right into the gorge. I think that is the only way to get a good
  shot of these falls.</p>

