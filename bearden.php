<h3>Sunday, October 6, 1996</h3>

<p>This property is owned by the <b> <a href="http://www.thetrustees.org/">Trustees of
  Reservations</a> </b>. I found it in their catalog of properties. Bear's Den is located on <b>Nielson
  Road</b> in North New Salem. It's a short distance off route 202. With a good map, it's not hard
  to find. There is a wide spot in the road where you can park, but there is no obvious sign.
  However, there is an obvious trail with a small sign a few yards along it letting you know that
  you are at the right place.</p>

<p>My daughter Hillary and I visited these falls together. There really wasn't much there. The <b>Swift
  River</b> fell over a rocky outcropping into a grotto. The falls themselves were small, but the
  grotto was interesting and a bit unusual. My father indicated to me that there was more to see
  upstream, so Hillary and I did some exploring. We walked for 1/4 mile or so, but didn't see any
  other falls. Furthermore, the ground was flat and didn't suggest anything more. We went back to
  the grotto and lounged around for a while before leaving. It was a pleasant spot.</p>

