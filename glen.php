<h3>Monday, April 1, 1996</h3>

<p>I expected these falls to be difficult to find. I purchased a topographic map of the area
  (Fairlee quadrangle), and it did not indicate the location of the falls in any way. It showed
  <b>Glen Falls Brook</b>, but I had no idea where along the brook the falls were. So I scanned
  the map looking for the steepest section of the brook on the guess that the falls would be
  there. As it turned out that section was not far from where the brook emptied into <b>Lake
    Morey</b>.</p>

<p>I drove down the road on the west side of Lake Morey and came to a place where Glen Falls Brook
  flowed under the road. Directly across the street from a tennis court and right next to the
  brook, there was a trail that led a short distance up to the falls. There was no sign. I didn't
  see the trail at first, so I parked at the entrance to a defunct gravel pit across the street
  from a public boat landing (and right near the tennis court). As soon as I got out of my car, I
  could hear the falls. A good sign!</p>

<p>I followed my ears and climbed over the side of the gravel pit and then through the woods a
  short distance to the stream. There I found a small, but cute little waterfall. It poured not
  more than 10 feet over an interesting rock formation. Below the falls the stream flowed down
  over slabs of exposed rock. The rock was a kind of slate-like material that was flaking off in
  many thin layers. The orientation of the layers looked to be almost vertical. As a result, the
  water flowed between and about the layers of rock in a most interesting way.</p>

<p>I almost believed that cute little waterfall was the main event. But after spending some time
  watching it, I decided to explore a little further upstream. I followed an informal trail along
  the water and soon came to the real main event! A twenty or thirty foot falls poured through a
  rocky crevasse and into a small pool. The main falls were covered over with an ice blanket, so I
  wasn't able to see them very well (there was very little ice and snow around otherwise). I tried
  to take some pictures, but it was very dark in the glen and I had to use 1/8-second exposures. I
  hadn't brought my tripod. Yet despite that, I managed to get a fairly good picture anyway (see
  the top of this page).</p>

<p>I followed the trail up the side of the glen. Above the main falls, the stream flowed through a
  narrow, serpentine channel that it had eroded into the rock. There were several other smaller
  falls and chutes to look at. The trail went high on the embankment, however, so most of the
  viewing angles were from above.</p>

<p>All around it was quite an interesting fall. I'll have to return when the ice is off the main
  part. Next time I will remember to bring my tripod.</p>

