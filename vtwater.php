<h1>Other Vermont Waterfalls</h1>
<p><i>The following falls in Vermont were brought to my attention by various people. These are all
  falls that I have not yet seen myself. As I visit these falls, I will take them off this list
  and add them to my main list.</i>
</p>

<hr/>
<p>
  <i>The following notes were sent to me by Dean Goss on December 10, 1996 with some supplemental
    material sent to me on May 1, 1997. Thank you, Dean for this information! Dean has also sent
    me information about some <a href="nhwater.html">New Hampshire waterfalls</a> as well.</i>
</p>
<ul>
  <li>
    <p>USGS maps show a <b>Blake Falls</b> in Woodbury. Pretty small, maybe 12' of total drop,
      right at the roadside, worth a quick look, but only if you're going by. (Rt 14, approx 3.5
      miles south of Hardwick) There was also a <b>nice, seasonal fall</b> approx 2 miles south of
      Hardwick. I set out to see what I could see. The stream (about the size of Glen Falls Brook)
      forked at the top of the falls. Most of the water came from the north fork, and about 2/10
      mile upstream was a 40' waterfall. Kind of a nice surprise, but the true gem was the south
      fork. Perhaps 100' of cascades over talus, crowned by an 80' sheer drop. The climb was an
      SOB (loose rocks, whipping branches, and ice). but well worth the effort to gain the bottom
      of the falls.</p>
  </li>

  <li>
    <p><b>Boltonville Falls</b>: a 35' drop of the Wells river, alas, marred by a dam.</p>
  </li>

  <li>
    <p><b>Brockway's Mills</b>: another 35' drop marred by a dam, but the view of the gorge from
      the railroad bridge is spectacular.</p>
  </li>

  <li>
    <p><b>Carver Falls</b>: The trick is finding the access by road. No access by road in Vermont.
      In Fair Haven, take West road 2 miles, take the 1st right after the bridge over the Poultney
      river, then take the 1st right on Carver falls road. This waterfall is very interesting in
      either low or high water. In low water, the river completely bypasses the lower falls,
      opting for a subterranean route, to well up like a spring at the base of the cliff. There is
      also a large cave about 1/2 mile downstream, at the head of a side stream.</p>
  </li>

  <li>
    <p><b>Cascade Falls</b>: an 84' drop on the Weathersfield trail on Ascutney.</p>
  </li>

  <li>
    <p><b> <a href="claren.html">Clarendon gorge</a> </b>: has a lower gorge, adjacent to Rt. 7B,
      which I find more spectacular than the upper gorge.</p>
  </li>

  <li>
    <p><b>Downer Glen</b>: in Manchester, easily combined with <a href="lyebrook.html">Lye Brook
      Falls</a>.</p>
  </li>

  <li>
    <p><b>Duck Brook Cascades</b>: A neat series of small drops, perhaps 150' total, but none over
      10'.</p>
  </li>

  <li>
    <p><b>Furnace Brook</b>: in Pittsford Mills, there is rumored to be a 118' waterfall, but I
      have yet to find one.</p>
  </li>

  <li>
    <p><b>Huntington Gorge</b>: The best examples of sculpted rock in the state. The layer of
      schist that the river traverses here is to be found on virtually every river north and south
      of the winooski. Examples include The <a href="bolton.html">Bolton Potholes</a>, Preston
      Brook Falls, Duck Brook Falls, Ridley Brook, Crossett Brook, etc.</p>
  </li>

  <li>
    <p><i>Dean's comments from late April 1997</i>: Big rocks, not as much water as I'd hoped for,
      but worth the effort. Rt 108 in Jeffersonville take a left on Canyon Rd (just past the old
      gristmill), the parking area is marked and is adjacent to a covered bridge. I elected to
      cross the bridge and bushwack up the far bank, rather than attempt a springtime fording of
      the river. The falls are 1/2 mile upstream. The gorge is perhaps the most constricting I've
      seen in the state.</p>
  </li>

  <li>
    <p><b>Little Falls</b>: The infamous Gazetteer is off, but only a mile this time. Look for the
      falls a mile downstream from their suggested location.</p>
  </li>

  <li>
    <p><b>McLaughlin falls</b>: This 25' series is delightful. On the Wheelerville road, between
      Mendon, and Rutland. Just below the newly constructed bridge in the middle of nowhere.</p>
  </li>

  <li>
    <p><b>Rock River falls</b>: high on the rock river in Dover. Very remote.</p>
  </li>

  <li>
    <p><b>The melt water falls in Smugglers Notch</b> (Jeffersonville side) is flowing (<i>Late
      April 1997</i>), and while the road is still officially closed, you'd have the notch all to
      yourself. It's very tall, perhaps 300'. It flows all year, but is greatly diminished in
      mid-summer. I suspect it is the outlet of Lake of the Clouds.</p>
  </li>

  <li>
    <p><b>Trout River Falls</b> in Montgomery Center is raging (<i>Late April 1997</i>). Less than
      1/10 of a mile on 242 past the 58/118/242 intersection, there is an abandoned parking lot
      behind a white house. There is a well-defined trail from the back of the lot. There are 2
      falls here: The actual Trout River Falls, and a feeder stream that cascades into the pool
      below the falls.</p>
  </li>
</ul>

<p>
  <i>In addition to the falls above, I've also been encouraged to look for <b>Molly's Falls</b> in
    Marshfield. I understand them to be on the stream that flows out of Molly's Falls Pond and
    into the Winooski. A topographic map of the area (Marshfield Quadrangle) shows the stream
    flowing over a steep decline just before joining the river.</i>
</p>

<p>
  <i>I investigated this area with my wife on April 13, 2003, but the stream appeared to be on
    private property and there were no obvious approaches. I saw no signs, parking areas, or
    trailheads along Rt. 2 or along the side road that runs alongside the Winooski in the same
    general area. This deserves more investigation at a later time.</i>
</p>

