<h1>Waterfall Pictures</h1>
<hr/>
<p>This is a list of all the large waterfall pictures that I have on my site. I (Peter Chapin)
  took Most of these pictures. However, some of them, as noted below, were taken by others. These
  pictures are intended to be my best. You can find smaller versions of many of these pictures, as
  well as additional pictures not represented here, on the description pages of the falls.</p>

<p>Photographing waterfalls can be difficult. If you are interested in trying it, here is some <a
      href="photo_technique.html">advice</a> I might offer.</p>

<p>My favorite pictures are those of <a href="VT/photos/hamilton1.jpg">Hamilton Falls</a>, <a
